/* * 출자금일일거래명세서 */ SELECT A.MOUI_NO AS MOUI_NO,
    CASE A.MOUI_DVSN_CD WHEN '0' THEN B.USER_NM ELSE A.OGNZ_NM END AS MOUI_DVSN_CD,
    D.DEAL_BK_LEDG_BL,
    C.DR_AMOUNT AS DR_AMOUNT,
    C.CR_AMOUNT AS CR_AMOUNT,
    E.DEAL_AF_LEDG_BL
FROM TBCSPNBK001M A, TBDCMACM001M B, ( SELECT MOUI_NO AS MOUI_NO,
    SUM(CASE DEPTS_PMT_DVSN_CD WHEN 'C' THEN INV_AM ELSE 0 END) AS DEPTS_PMT_DVSN_CD,
    SUM(CASE DEPTS_PMT_DVSN_CD WHEN 'C' THEN 0 ELSE INV_AM END) AS DEPTS_PMT_DVSN_CD,
    MAX(INV_DEAL_SLN) AS INV_DEAL_SLN,
    MIN(INV_DEAL_SLN) AS INV_DEAL_SLN
FROM TBCSPNBK102L WHERE INV_DEAL_DT = #closingDt# AND CNL_YN = 'N' GROUP BY MOUI_NO ) C, TBCSPNBK102L D, TBCSPNBK102L E WHERE A.MOUI_NO = C.MOUI_NO AND A.TRGT_NOP_ENO (+)= B.TRGT_NOP_ENO AND D.MOUI_NO = A.MOUI_NO AND D.INV_DEAL_DT = #closingDt# AND D.INV_DEAL_SLN = C.MIN_INV_SEQ AND E.MOUI_NO = A.MOUI_NO AND E.INV_DEAL_DT = #closingDt# AND E.INV_DEAL_SLN = C.MAX_INV_SEQ ORDER BY MOUI_NO