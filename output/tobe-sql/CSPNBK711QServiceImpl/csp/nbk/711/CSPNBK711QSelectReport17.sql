    SUM(CASE DEPTS_PMT_DVSN_CD WHEN 'D' THEN DEAL_PRN ELSE 0 END) AS DEPTS_PMT_DVSN_CD,
    SUM(CASE DEPTS_PMT_DVSN_CD WHEN 'C' THEN 1 ELSE 0 END) AS DEPTS_PMT_DVSN_CD,
    SUM(CASE DEPTS_PMT_DVSN_CD WHEN 'C' THEN DEAL_PRN ELSE 0 END) AS DEPTS_PMT_DVSN_CD,
    SUM(CASE INT_AM WHEN 0 THEN 0 ELSE 1 END) AS INT_AM,
    SUM(CASE INT_AM WHEN 0 THEN 0 ELSE INT_AM END) AS INT_AM
