/* * 현금을 제외한 당일기표되었던 것에 대한 합계와 건수 */ SELECT /* CSPNBK711QSelectReport12 */ SUM(A.T1) AS T1,
    SUM(A.T2) AS T2,
    SUM(A.T1) AS T1,
    B.ACCT_NM,
    SUM(A.T5) AS T5,
    SUM(A.T4) AS T4,
    SUM(A.T4) AS T4
FROM ( SELECT CASE DS_MNST_CLU_CD WHEN '110102' THEN 0 ELSE DSPS_RQT_AMT END AS DS_MNST_CLU_CD,
    CASE DS_MNST_CLU_CD WHEN '110102' THEN 0 ELSE 1 END AS DS_MNST_CLU_CD,
    CASE DS_MNST_CLU_CD WHEN '110102' THEN CRDT_MNST_CLU_CD ELSE DS_MNST_CLU_CD END AS DS_MNST_CLU_CD,
    CASE DS_MNST_CLU_CD WHEN '110102' THEN CRDT_TAX_SITM_CD ELSE DS_TAX_SITM_CD END AS DS_MNST_CLU_CD,
    CASE CRDT_MNST_CLU_CD WHEN '110102' THEN 0 ELSE DSPS_RQT_AMT END AS CRDT_MNST_CLU_CD,
    CASE CRDT_MNST_CLU_CD WHEN '110102' THEN 0 ELSE 1 END AS CRDT_MNST_CLU_CD
FROM TBCSPNBK301M WHERE SMB_DEAL_DT = #closingDt# AND CNL_YN = 'N' ) A , TBCSPNBK501M B WHERE A.T3 = B.MNST_CLU_CD AND A.T6 = B.TAXR_SITM_SECD GROUP BY B.MNST_CLU_CD, B.TAXR_SITM_SECD, B.ACCT_NM ORDER BY MNST_CLU_CD