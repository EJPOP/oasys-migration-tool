/* * 대출금검산장 * V_LEND_KND_CD * 1 : 일반대출 * 2 : 임차보증대출 */ SELECT A.MOUI_NO,
    CASE B.MOUI_DVSN_CD WHEN '0' THEN NVL(C.USER_NM,'') ELSE NVL(B.OGNZ_NM,'') END AS MOUI_DVSN_CD,
    CASE A.DEPTS_PMT_DVSN_CD WHEN 'D' THEN A.DEAL_PRN ELSE 0 END AS DEPTS_PMT_DVSN_CD,
    CASE A.DEPTS_PMT_DVSN_CD WHEN 'C' THEN A.DEAL_PRN ELSE 0 END AS DEPTS_PMT_DVSN_CD,
    NVL(A.INT_AM, 0) AS INT_AM
FROM TBCSPNBK205L A , TBCSPNBK001M B , TBDCMACM001M C WHERE A.MOUI_NO = B.MOUI_NO AND A.CNL_YN = 'N' AND B.TRGT_NOP_ENO (+)= C.TRGT_NOP_ENO AND A.LEND_DEAL_DT = #closingDt# AND A.LEND_KND_CD = '1' ORDER BY A.MOUI_NO