SELECT D1.ADT_YR AS ADT_YR /* 감사년도 */,
    D1.ADT_NO AS ADT_NO /* 감사번호 */,
    D1.DSPS_RQT_SQNO AS DSPS_RQT_SQNO /* 처분요구순번 */,
    F_MNG_KND_AUDYRNO(D1.ADT_YR, D1.ADT_NO, '', '-') ||'-'|| D1.DSPS_RQT_SQNO AS ADT_CLSF_NO /* 감사분류번호 */,
    D1.DSPS_RQT_KDCD AS DSPS_RQT_KDCD /* 처분요구종류코드 */,
    F_CODE_NM('1000002',D1.DSPS_RQT_KDCD ) AS DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */,
    D1.DSPS_RQT_NM AS DSPS_RQT_NM /* 처분요구명 */
FROM TB_BADEAR001M D1 , TB_BADEAR002D D2 WHERE D1.ADT_YR = D2.ADT_YR AND D1.ADT_NO = D2.ADT_NO AND D1.DSPS_RQT_SQNO = D2.DSPS_RQT_SQNO AND D1.ADT_YR = #CVLCPT_ADT_YR# AND D1.ADT_NO = #ADT_NO# AND D1.DSPS_RQT_SQNO IN #DSP_RQ_SNO[]# AND D2.SDIN_ATRZ_DOC_NO = #SDIN_ATRZ_DOC_NO# ORDER BY D1.ADT_YR ,D1.ADT_NO ,D1.DSPS_RQT_SQNO