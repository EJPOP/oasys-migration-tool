    D1.CD,
    D1.CD_NM,
    D1.USR_DFN_TXT_1,
    D1.OTPT_SEQ
FROM TBDCMACM013C D1 WHERE D1.CMM_CD_DVSN_CD = '1000165' AND D1.CD != '000000000' AND D1.USE_YN = 'Y' D1.CD = '0000' ) D3 , ( SELECT D2.TRAC_KND_CD,
    F_CODE_NM( '1000165', D2.TRAC_KND_CD ) AS F_CODE_NM,
    D2.MOUI_NO,
    D2.DDCT_AM,
    D2.DDCT_AM
FROM TBCSPNBK103M D2 WHERE D2.MOUI_NO = #mouiNo# ) D4 WHERE D3.CD = D4.TRAC_KND_CD(+) ORDER BY OTPT_SEQ