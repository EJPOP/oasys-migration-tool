    D3.CD,
    D3.CD_NM,
    #mouiNo# AS #MOUINO#,
    NVL(D4.DDCT_AM, 0) AS DDCT_AM,
    NVL(D4.CH_DDCT_AM, 0 ) AS CH_DDCT_AM,
    D3.USR_DFN_TXT_1,
