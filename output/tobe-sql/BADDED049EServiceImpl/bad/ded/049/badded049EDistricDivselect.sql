    F_CODE_NM('1000001',ARVL_RGN_KDCD) AS F_CODE_NM,
    ARVL_RGN_KDCD /* 도착지역종류코드 */,
    RGN_SECD /* 지역구분코드 */,
    MDFCN_YMD /* 수정일자 */,
    CPRG_DSTNC_SECD /* 수도권거리구분코드 */
FROM TB_BADDED014B A, TBDCMACM013C B WHERE 1=1 AND B.CMM_CD_DVSN_CD = '1000001' AND A.ARVL_RGN_KDCD = B.CD AND A.DPTRE_RGN_SECD = #DPTRE_RGN_SECD# /*지역코드*/ AND B.CD_NM LIKE '%'||#strRgnNm#||'%' /*지역코드*/ AND A.RGN_SECD = #RGN_SECD# /*지역코드*/ ORDER BY F_CODE_NM ('1000001', ARVL_RGN_KDCD)