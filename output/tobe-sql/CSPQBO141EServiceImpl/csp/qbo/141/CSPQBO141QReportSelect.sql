SELECT F_USR_INFO(B.LAG_INV_CNB,'2') AS F_USR_INFO /* 대출자명 */,
    F_USR_INFO(B.LAG_INV_CNB,'1') AS F_USR_INFO /* 대출자ID */,
    F_DPT_INFO(F_USR_INFO(B.LAG_INV_CNB,'5'), '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    F_CODE_NM('1000173', F_USR_INFO(B.LAG_INV_CNB, '3')) AS F_CODE_NM /* 직급명 */,
    TO_CHAR(TO_DATE(B.BOO_LEND_DT),'YYYY-MM-DD') AS TO_CHAR /* BOO_LEND_DT */,
    DECODE(B.BOO_REN_YN_CD,'0',TO_CHAR(TO_DATE(B.BOO_REN_APT_DT),'YYYY-MM-DD'),'') AS BOO_REN_YN_CD /* 도서반납예정일 */,
    A.BOO_NO /* 도서번호 */,
    A.BOO_NO||'-'||A.BOO_VLE_NO AS BOO_NO /* 도서번호 */,
    A.BOO_VLE_NO /* 도서권번호 */,
    A.CAT_SYM_NM /* 분류기호명 */,
    SUBSTR(A.CAT_SYM_NM,1,1)||'-'||SUBSTR(A.CAT_SYM_NM,2,3)||'-'||SUBSTR(A.CAT_SYM_NM,5,3) AS SUBSTR,
    A.BOO_TIT_NM /* 도서제목명 */,
    A.RSCH_RPTP_AUT_NM /* 연구보고서저자명 */,
    A.BOO_KND_CD /* 도서종류코드 */,
    A.COPT_CAT_CD /* 소관분류코드 */,
    B.LAG_INV_CNB /* 대출자전산번호 */,
    F_USR_INFO(B.LAG_INV_CNB,'5') AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    F_USR_INFO(B.LAG_INV_CNB, '3') AS JBGD_SECD /* 직급구분코드 */,
    B.BOO_LEND_SLN /* 도서대출연번 */,
    NVL(B.BOO_REN_YN_CD,'1') AS BOO_REN_YN_CD
FROM TBCSPQBO001M A , TBCSPQBO002H B WHERE 1=1 AND A.COPT_CAT_CD = B.COPT_CAT_CD AND A.BOO_KND_CD = B.BOO_KND_CD AND A.BOO_NO = B.BOO_NO AND A.CAT_SYM_NM = B.CAT_SYM_NM AND A.BOO_VLE_NO = B.BOO_VLE_NO AND A.COPT_CAT_CD = #strCOPT_CAT_CD# AND B.BOO_REN_YN_CD = '0' AND B.LAG_INV_CNB = #strLAG_INV_CNB# AND B.BOO_LEND_DT = TO_CHAR(SYSDATE,'YYYYMMDD') ORDER BY A.BOO_NO DESC