SELECT /* CSPNBK221EExprMouiListSelect */ D1.MOUI_NO,
    SUM(D1.LEND_AM) AS LEND_AM,
    MAX(D3.USER_NM) AS USER_NM /* 사용자명 */,
    SUM(D1.LEND_BL) AS LEND_BL,
    SUM(D1.GRT_BL) AS GRT_BL,
    MAX(D1.LEND_DT) AS LEND_DT,
    MAX(D1.EXPR_DT) AS EXPR_DT,
    MAX(D1.LEND_KND_CD) AS LEND_KND_CD
FROM TBCSPNBK203D D1 , TBCSPNBK001M D2 , TBDCMACM001M D3 WHERE D1.MOUI_NO = D2.MOUI_NO AND D2.TRGT_NOP_ENO = D3.TRGT_NOP_ENO AND D1.LEND_KND_CD = #exprLendKndCd# AND D1.CNL_YN = 'N' AND D1.LEND_BL > 0 D1.EXPR_DT BETWEEN #exprDt# AND TO_CHAR(TO_DATE(#exprDt#, 'YYYYMMDD')+15, 'YYYYMMDD') D1.EXPR_DT BETWEEN #exprDt# AND TO_CHAR(ADD_MONTHS(TO_DATE(#exprDt#, 'YYYYMMDD'),1), 'YYYYMMDD') D1.MOUI_NO = #exprMouiNo# D3.USER_NM LIKE '%' || #exprMouiNm# || '%' GROUP BY D1.MOUI_NO ORDER BY MOUI_NO