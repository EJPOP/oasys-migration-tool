SELECT A.INSP_REGI_DT,
    A.INSP_REGI_NO,
    A.INSP_LVL_CNT,
    A.INSP_DVSN_CD,
    F_CODE_NM('1000308', A.INSP_DVSN_CD) AS F_CODE_NM,
    A.INSP_TIT_ENN,
    A.INSP_ENTX,
    A.INSP_ECN,
    A.INSP_USR_EID,
    A.INSP_USR_NAM,
    A.INSP_ATT_FL_EID,
    A.INSP_DOC_TYP_CD,
    A.SLCT_STTS_YN /* 청탁상태여부 */,
    DECODE(B.INSP_REGI_DT,NULL,'N','Y') AS INSP_REGI_DT,
    B.INSP_REGI_DT,
    B.INSP_REGI_NO,
    B.INSP_LVL_CNT,
    A.INSP_DVSN_CD,
    B.INSP_TIT_ENN,
    B.INSP_ENTX,
    B.INSP_ECN,
    B.INSP_USR_EID,
    B.INSP_USR_NAM,
    B.SLCT_STTS_YN /* 청탁상태여부 */
FROM (SELECT *
FROM TBCSPCIS001M A WHERE INSP_LVL_CNT = 0) A, (SELECT *
FROM TBCSPCIS001M A WHERE INSP_LVL_CNT <> 0 ) B WHERE A.INSP_REGI_DT = B.INSP_REGI_DT(+) AND A.INSP_REGI_NO = B.INSP_REGI_NO(+) AND A.SLCT_STTS_YN = #SLCT_STTS_YN# AND A.INSP_REGI_DT = #dt# AND A.INSP_REGI_NO = #no# ORDER BY A.INSP_REGI_DT DESC, A.INSP_REGI_NO DESC