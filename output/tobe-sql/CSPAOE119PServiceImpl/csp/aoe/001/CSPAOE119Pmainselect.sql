    D1.EVL_CMTR_CNB,
    F_DPT_INFO(D2.SRECS_DEPT_CD, '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    F_CODE_NM('1000173', D2.JBGD_SECD) AS JBGD_NM /* 직급명 */,
    D1.EVL_YR,
    D1.HLYR_DVSN_CD,
    D1.SRECS_DEPT_CD /* 심사재심의부서코드 */
