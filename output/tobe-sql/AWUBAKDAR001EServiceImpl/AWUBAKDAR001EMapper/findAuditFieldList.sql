SELECT T1.FRS_MDM_SQNO /* 포렌식매체순번 */,
    TO_CHAR(TO_DATE(T1.RLS_YMD, 'YYYYMMDD'), 'YYYY-MM-DD') AS RLS_YMD /* 공개일자 */,
    T1.PRCS_DEPT_NM /* 처리부서명 */,
    F_AWU_CODE_NM('10000013', T1.AUD_SPH_DTL_CD) AS F_AWU_CODE_NM,
    T1.RSCH_ASMT_TTL /* 연구과제제목 */
FROM TBAWUBAKDAR001M T1 WHERE 1=1 AND T1.RLS_YMD||OPEN_HM AND T1.RSCH_ASMT_TTL LIKE '%'||#{auditReportSearch}||'%' AND T1.AUD_SPH_CD = #{audSphCd} AND T1.AUD_SPH_DTL_CD = #{audSphDtlCd} AND T1.OPEN_DT >= REPLACE(#{openDtSt},'-','') AND T1.RLS_YMD order by T1.RLS_YMD DESC