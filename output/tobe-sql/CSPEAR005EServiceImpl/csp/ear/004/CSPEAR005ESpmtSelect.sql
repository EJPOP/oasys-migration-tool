SELECT B.SPMT_RQT_SQNO /* 출고요구순번 */,
    B.SPLMNT_RQT_YMD /* 보완요구일자 */,
    B.SPMT_RQT_CN /* 출고요구내용 */,
    B.SPLMNT_CMPTN_YMD /* 보완완료일자 */,
    A.CLM_SECD /* 청구구분코드 */,
    A.CLM_YR /* 청구년도 */,
    A.CLM_RCPT_NO /* 청구접수번호 */,
    B.SPLMNT_SECD /* 보완구분코드 */,
    B.TRGT_INST_NM /* 대상기관명 */,
    B.SPMT_RQT_STTS_SECD /* 출고요구상태구분코드 */
FROM TB_CSPEAR001M A ,TB_CSPEAR003L B WHERE A.CLM_SECD = #CLM_SECD# AND A.CLM_YR = #CVLCPT_RCPT_YR# AND A.CLM_RCPT_NO = #CVLCPT_RCPT_RCPT# AND A.CLM_SECD = B.CLM_SECD AND A.CLM_YR = B.CLM_YR AND A.CLM_RCPT_NO = B.CLM_RCPT_NO ORDER BY B.SPMT_RQT_SQNO DESC