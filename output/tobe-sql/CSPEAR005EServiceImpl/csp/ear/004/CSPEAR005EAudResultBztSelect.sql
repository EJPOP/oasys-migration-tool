SELECT D1.ADT_YR /* 감사년도 */,
    D1.ADT_NO /* 감사번호 */,
    D1.DSPS_RQT_SQNO /* 처분요구순번 */,
    F_MNG_KND_AUDYRNO(D1.ADT_YR, D1.ADT_NO, D3.ADT_KDCD, '-') ||'-'|| D1.DSPS_RQT_SQNO AS F_MNG_KND_AUDYRNO,
    D1.DSPS_RQT_NM /* 처분요구명 */,
    F_CODE_NM('1000002',D1.DSPS_RQT_KDCD ) AS DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */
FROM TB_BADEAR001M D1 ,TB_BADEAR035L D2 ,TB_BADDED001M D3 WHERE D1.ADT_YR = D2.ADT_YR AND D1.ADT_NO = D2.ADT_NO AND D1.DSPS_RQT_SQNO = D2.DSPS_RQT_SQNO AND D1.ADT_YR = D3.ADT_YR AND D1.ADT_NO = D3.ADT_NO AND D2.DSPS_RQT_NM_2 = #DSPS_RQT_NM_2# AND D2.DSPS_RQT_NM_1 = #DSPS_RQT_NM_1# AND D2.DSPS_RQT_NM_3 = #DSPS_RQT_NM_3#