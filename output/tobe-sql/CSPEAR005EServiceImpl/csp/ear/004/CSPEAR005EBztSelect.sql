SELECT A.CLM_SECD /* 청구구분코드 */,
    A.CLM_YR /* 청구년도 */,
    A.CLM_RCPT_NO /* 청구접수번호 */,
    B.ADT_YR /* 감사년도 */,
    B.ADT_NO /* 감사번호 */,
    DECODE(B.ADT_YR,NULL,'',B.ADT_YR||'-'||B.ADT_NO) AS ADT_CLSF_NO /* 감사분류번호 */,
    C.BZTRP_YR /* 출장년도 */,
    C.BZTRP_SQNO /* 출장순번 */,
    C.BZTRP_SECD /* 출장구분코드 */,
    TO_CHAR(TO_DATE(C.ADT_BGNG_YMD),'YYYY-MM-DD')||'~'|| TO_CHAR(TO_DATE(C.ADT_END_YMD),'YYYY-MM-DD') AS TO_CHAR /* 실시기간 */,
    C.ADT_BZTRP_KDCD /* 감사출장종류코드 */,
    C.ADT_DCN /* 감사일수 */,
    F_ORG_CNT_NM(C.BZTRP_YR,C.BZTRP_SQNO) AS F_ORG_CNT_NM
FROM TB_CSPEAR001M A, TB_BADDED001M B, TB_BADDED002M C WHERE A.CLM_SECD = #CLM_SECD# AND A.CLM_YR = #CVLCPT_RCPT_YR# AND A.CLM_RCPT_NO = #CVLCPT_RCPT_RCPT# AND A.CLM_SECD = B.CLM_SECD AND A.CLM_YR = B.INFO_RCPT_YR AND A.CLM_RCPT_NO = B.CVLCPT_NO AND B.ADT_YR = C.REL_ADT_YR AND B.ADT_NO = C.REL_ADT_NO AND C.ADT_BZTRP_KDCD = '00'