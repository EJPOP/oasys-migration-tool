SELECT A.DATA_CLCT_BZTRP_SECD /* 자료수집출장구분코드 */,
    A.CLM_SECD /* 청구구분코드 */,
    A.CLM_YR /* 청구년도 */,
    A.CLM_RCPT_NO /* 청구접수번호 */,
    B.ADT_YR /* 감사년도 */,
    B.ADT_NO /* 감사번호 */,
    DECODE(B.ADT_YR,NULL,'',B.ADT_YR||'-'||B.ADT_NO) AS ADT_CLSF_NO /* 감사분류번호 */,
    C.TPOF_STTS_SECD /* 제보상태구분코드 */,
    A.PREI_RJTN_YN /* 선람전각하여부 */,
    TO_CHAR(C.TPOF_DISCON_DT,'YYYYMMDD') AS TPOF_DISCON_DT /* 제보취하일시 */,
    DECODE(C.TPOF_DISCON_DT,NULL,'','취하') AS TPOF_DISCON_DT /* 제보취하일시 */
FROM TB_CSPEAR001M A, TB_BADDED001M B, TB_CSPDCP101M C WHERE A.CLM_SECD = #CLM_SECD# AND A.CLM_YR = #CVLCPT_RCPT_YR# AND A.CLM_RCPT_NO = #CVLCPT_RCPT_RCPT# AND A.CLM_SECD = B.CLM_SECD(+) AND A.CLM_YR = B.INFO_RCPT_YR(+) AND A.CLM_RCPT_NO = B.CVLCPT_NO(+) AND A.CLM_SECD = C.CVLCPT_CLM_SECD AND A.CLM_YR = C.TPOF_RCPT_YR(+) AND A.CLM_RCPT_NO = C.TPOF_RCPT_NO