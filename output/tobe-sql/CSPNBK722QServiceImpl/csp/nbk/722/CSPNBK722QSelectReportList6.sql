    A.LEND_BL + B.LEND_BL AS LEND_BL
FROM ( SELECT COUNT(*) AS P_CNT,
    SUM(B.LEND_BL ) AS LEND_BL
FROM TBCSPNBK001M A , TBCSPNBK202M B , TBDCMACM001M C WHERE A.SCS_YN = 'N' AND B.LEND_KND_CD = #LEND_KND_CD# AND A.MOUI_NO = B.MOUI_NO AND A.MOUI_DVSN_CD = '0' AND A.TRGT_NOP_ENO = C.TRGT_NOP_ENO ) A, ( SELECT COUNT(*) AS M_CNT,
