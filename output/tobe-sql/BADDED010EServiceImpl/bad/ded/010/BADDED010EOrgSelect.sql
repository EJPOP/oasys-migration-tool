FROM TBBADDED006L WHERE 1=1 AND FT_SQNO = #FT_SQNO# AND BZTRP_SQNO = #BZTRP_SQNO# AND BZTRP_YR = #BZTRP_YR# AND FT_SQNO = #FT_SQNO# AND BZTRP_SQNO = #BZTRP_SQNO# AND BZTRP_YR = #BZTRP_YR# GROUP BY BZTRP_YR ,BZTRP_SQNO ,FT_SQNO ,FT_TRGT_INST_SQNO) D2 WHERE 1=1 AND D1.BZTRP_YR = D2.BZTRP_YR(+) AND D1.BZTRP_SQNO = D2.BZTRP_SQNO(+) AND D1.FT_SQNO = D2.FT_SQNO(+) AND D1.FT_TRGT_INST_SQNO = D2.FT_TRGT_INST_SQNO(+) AND D1.FT_SQNO = #FT_SQNO# AND D1.BZTRP_SQNO = #BZTRP_SQNO# AND D1.BZTRP_YR = #BZTRP_YR# AND D1.FT_SQNO = #FT_SQNO# AND D1.BZTRP_SQNO = #BZTRP_SQNO# AND D1.BZTRP_YR = #BZTRP_YR# ORDER BY D1.FT_SQNO , D1.BZTRP_SQNO , D1.BZTRP_YR , D1.FT_TRGT_INST_SQNO