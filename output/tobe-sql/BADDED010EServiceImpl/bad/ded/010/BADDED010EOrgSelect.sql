    D1.BZTRP_YR AS BZTRP_YR /* 출장년도 */,
    D1.INST_CD AS INST_CD /* 기관코드 */,
    F_ORG_INFO(D1.INST_CD,'1') AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    D1.RGN_CD AS RGN_CD /* 지역코드 */,
