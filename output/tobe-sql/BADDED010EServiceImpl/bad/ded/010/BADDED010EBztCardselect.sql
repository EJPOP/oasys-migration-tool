SELECT AUD.ADT_YR /* 감사년도 */,
    AUD.ADT_NO /* 감사번호 */,
    SITU.ADT_STP_SQNO /* 감사단계순번 */,
    AUD.ADT_YR || '-' || SUBSTR(AUD.ADT_ASMT_SECD,-1) || '-' || AUD.INST_SECD || '-' || SUBSTR(AUD.ADT_KDCD, -1) || '-' || AUD.SPRVSN_DEPT_CD || '-' || SUBSTR(AUD.ADT_MTHD_SECD,-1) || '-' || AUD.ADT_NO || '-' || SITU.ADT_STP_SQNO AS ADT_YR /* 감사년도 */,
    /*감사연번호*/ SITU.BZTRP_NM AS BZTRP_NM /* 출장명 */,
    /*감사사항명*/ PSON.ADT_BGNG_YMD AS ADT_BGNG_YMD /* 감사시작일자 */,
    /*감사기간 FROM*/ PSON.ADT_END_YMD AS ADT_END_YMD /* 감사종료일자 */,
    /*감사기간 TO*/ PSON.ADT_DCN AS ADT_DCN /* 감사일수 */,
    /*감사일수*/ PSON.MAN_DAY,
    /*연인원*/ PSON.PSON_CNT,
    /*인원*/ SITU.BZTRP_CN AS BZTRP_CN /* 출장내용 */,
    /*출장내용(감사중점에 배치)*/ USR.USER_NM AS USER_NM /* 사용자명 */,
    /*출장책임자*/ DEPT.CNSTN_MBCMT_DEPT_NM AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    /*출장책임자 주관부서명*/ TTO.INST_CONT AS INST_CONT /* 기관개수 */,
    /*대상기관수*/ ORG.PROF_RBPRSN_INST_NM AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */
FROM TB_BADDED001M AUD, TB_BADDED002M SITU, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    MIN(ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    SUM(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    COUNT(*) AS PSON_CNT
FROM (SELECT TMA.BZTRP_YR /* 출장년도 */,
    TMA.BZTRP_SQNO /* 출장순번 */,
    TMA.BZTRVR_ENO /* 출장자직원전산번호 */,
    MIN(PTD.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(PTD.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    SUM(PTD.ADT_DCN) AS ADT_DCN /* 감사일수 */
FROM TBBADDED005L TMA, TBBADDED006L PTD WHERE PTD.BZTRP_YR = TMA.BZTRP_YR AND PTD.BZTRP_SQNO = TMA.BZTRP_SQNO AND PTD.FT_SQNO = TMA.FT_SQNO AND PTD.BZTRVR_ALTMNT_SQNO = TMA.BZTRVR_ALTMNT_SQNO AND PTD.BZTRP_YR = #REL_ADT_YR# /*화면 인자값 감사년도*/ GROUP BY TMA.BZTRP_YR, TMA.BZTRP_SQNO, TMA.BZTRVR_ENO) PTD1 GROUP BY BZTRP_YR, BZTRP_SQNO ) PSON, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    MIN(INST_CD) AS INST_CD /* 기관코드 */,
    COUNT(*) AS INST_CONT /* 기관개수 */
FROM TBBADDED008L WHERE BZTRP_YR = #REL_ADT_YR# /*화면 인자값 감사년도*/ GROUP BY BZTRP_YR, BZTRP_SQNO) TTO, TBDCMACM001M USR, TBDCMACM002M DEPT, TBDCMACM015M ORG WHERE SITU.REL_ADT_YR = #REL_ADT_YR# /*화면 인자값 감사년도*/ AND SITU.REL_ADT_NO = #REL_ADT_NO# /*화면 인자값 감사순번*/ AND SITU.ADT_STP_SQNO = #CVLCPT_ADT_STP_SQNO# /*화면 인자값 감사단계*/ AND AUD.ADT_YR = SITU.REL_ADT_YR AND AUD.ADT_NO = SITU.REL_ADT_NO AND SITU.BZTRP_SECD ='1' AND PSON.BZTRP_YR = SITU.BZTRP_YR AND PSON.BZTRP_SQNO = SITU.BZTRP_SQNO AND TTO.BZTRP_YR(+) = SITU.BZTRP_YR AND TTO.BZTRP_SQNO(+) = SITU.BZTRP_SQNO AND USR.TRGT_NOP_ENO(+) = SITU.BZTRP_TL_ENO AND DEPT.SRECS_DEPT_CD(+) = SITU.BZTRP_TL_DEPT_CD AND ORG.INST_CD(+) = TTO.REP_ORG_CD