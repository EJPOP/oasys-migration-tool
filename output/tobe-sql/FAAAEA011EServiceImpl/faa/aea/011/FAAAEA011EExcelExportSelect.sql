SELECT A.INST_CD /* 기관코드 */,
    /*관리기관ID*/ A.PROF_RBPRSN_INST_NM AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    /*관리기관명*/ A.MNG_GD_CD,
    /*관리부서ID*/ A.MNG_GD_NM,
    /*관리부서명*/ A.RSCH_ASMT_TTL AS RSCH_ASMT_TTL /* 연구과제제목 */,
    /*문서제목*/ A.DOC_NO AS DOC_NO /* 문서번호 */,
    /*문서번호*/ TO_CHAR(A.RPT_DH,'YYYYMMDDHH24MISS') AS TO_CHAR,
    /*보고(접수)일자*/ TO_CHAR(A.REG_DT,'YYYYMMDDHH24MISS') AS REG_DT /* 등록일시 */,
    /*등록(접수)일자*/ A.RPT_GD_CD,
    /*보고(접수)부서ID*/ A.RPT_GD_NM,
    /*보고(접수)부서명*/ /*부서표시명*/ A.RPT_PEN_NM,
    /*보고(접수)자*/ A.PROF_RBPRSN_APRVR_NM AS PROF_RBPRSN_APRVR_NM /* 증명책임자결재자명 */,
    /*결재(업무담당)자*/ A.OPEN_YN_NM,
    /*공개여부*/ A.ELCT_DOC_YN_NM,
    /*전자문서여부*/ A.ENF_RN_NM,
    /*시행범위*/ TO_CHAR(A.ENF_DH,'YYYYMMDDHH24MISS') AS TO_CHAR,
    /*시행일자*/ A.RCPT_OM_ORG_NM,
    /*수(발)신기관명*/ A.REGI_DVSN_NM,
    /*등록구분*/ A.REGI_NO_NM,
    /*생산등록번호*/ A.LST_OPEN_YN /* 목록공개여부 */
FROM TBFAAAEA046M A WHERE 1=1 AND A.INST_CD = #INST_CD# /* strOrgCd */ AND A.RPT_DH BETWEEN #strStrRptDt# AND #strEndRptDt# AND A.MNG_GD_CD =#strMngGdCd# /*strMngGdCd */ AND A.RSCH_ASMT_TTL LIKE '%' || #strTitNm# || '%' AND A.DOC_NO LIKE '%' || #strDocNo# || '%' AND A.RCPT_OM_ORG_NM LIKE '%' || #strSendRecvName# || '%' AND A.PROF_RBPRSN_APRVR_NM LIKE '%' || #strApvrNm# || '%' A.REGI_DVSN_NM IN #strRegiDvsnNm[]# A.REGI_DVSN_NM IN #strRegiDvsnNm[]# A.ELCT_DOC_YN_NM IN #strElctDocYnNm[]# AND A.OPEN_YN_NM = #strOpenYnNm# ORDER BY A.REG_DT DESC