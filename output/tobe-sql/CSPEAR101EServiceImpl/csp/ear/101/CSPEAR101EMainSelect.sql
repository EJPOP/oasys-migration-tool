SELECT CASE WHEN CLM_SECD = '3' THEN '공익' WHEN CLM_SECD = '2' THEN '국민' ELSE '' END AS CLM_SECD /* 청구구분코드 */,
    CVLCPT_RCPT_YR /* 민원접수년도 */,
    NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    CVLCPT_RCPT_YR||'-'||F_CODE_NM('1000740',CLM_SECD)||'-'||NTFCTN_RCPT_SQNO AS CVLCPT_RCPT_YR /* 민원접수년도 */,
    SLCT_RPD_NM /* 청구명 */,
    CLMT_NM /* 청구인명 */,
    TKCG_DEPT_NM /* 담당부서명 */,
    PIC_NM /* 담당자명 */,
    RCPT_YMD /* 접수일자 */
FROM TB_CSPEAR101M WHERE 1=1 AND CLM_SECD = #CLM_SECD# AND RCPT_YMD &gt;= #strACP_DHS# AND RCPT_YMD &lt;= #strACP_DHE# AND CLM_NM LIKE '%'||#strTIT_NM#||'%' AND CVLCPT_RCPT_YR = #CVLCPT_RCPT_YR# AND NTFCTN_RCPT_SQNO = #NTFCTN_RCPT_SQNO# ORDER BY CVLCPT_RCPT_YR DESC, NTFCTN_RCPT_SQNO DESC