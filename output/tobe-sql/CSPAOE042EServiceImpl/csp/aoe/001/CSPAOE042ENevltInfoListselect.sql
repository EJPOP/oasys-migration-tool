SELECT D3.EVL_YR /* 평가년도 */,
    D3.HLYR_DVSN_CD /* 반기구분코드 */,
    D3.NEVLT_CNB /* 피평가자 전산번호 */,
    D3.TSK_NO /* 업무번호 */,
    #EVL_CMTR_CNB# AS #EVL_CMTR_CNB# /* 평가자전산번호 */,
    D3.#N/A /* 부서코드1 */,
    D3.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    D3.JBGD_SECD /* 직급구분코드 */,
    D5.DEPT_TPCD /* 부서유형코드 */,
    DECODE( D5.DEPT_TPCD, 'S', D3.PSN_TSK_ASC, 0) AS DEPT_TPCD /* 부서유형코드 */,
    DECODE( D5.DEPT_TPCD, 'A', D3.PSN_TSK_ASC, 0) AS DEPT_TPCD /* 부서유형코드 */
FROM ( SELECT D1.EVL_YR AS EVL_YR /* 평가년도 */,
    D1.HLYR_DVSN_CD AS HLYR_DVSN_CD /* 반기구분코드 */,
    D1.PTCPT_CNB /* 피평가자 전산번호 */,
    D1.TSK_NO AS TSK_NO /* 업무번호 */,
    D2.HIRK_DPT_CD AS #N/A /* 부서코드1 */,
    D1.CHR_DPT_CD AS SRECS_DEPT_CD /* 부서코드 */,
    D1.JBGD_SECD AS JBGD_SECD /* 직급구분코드 */,
    ROUND( TO_NUMBER(#TSK_ASC#) * (D1.CTB_RT/100), 2) AS ROUND /* 개인업무평점 */
FROM TBCSPAOE027M D1 , TBDCMACM002M D2 WHERE D1.CHR_DPT_CD = D2.SRECS_DEPT_CD AND D1.EVL_YR = #EVL_YR# AND D1.HLYR_DVSN_CD = #HLYR_DVSN_CD# AND D1.CHR_DPT_CD = #DPT_CD# AND D1.TSK_NO = #TSK_NO# ) D3 , TBDCMACM001M D4 , TBDCMACM002M D5 WHERE D3.NEVLT_CNB = D4.TRGT_NOP_ENO AND D4.SRECS_DEPT_CD = D5.SRECS_DEPT_CD