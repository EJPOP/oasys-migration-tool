FROM TB_CSPDCP105L) AND (A.CLM_SECD, A.CVLCPT_RCPT_YR, A.CVLCPT_RCPT_RCPT) NOT IN ( SELECT CVLCPT_MCA_CLM_SECD /* 민원합건청구구분코드 */,
    TPOF_MCA_RCPT_YR /* 제보합건접수년도 */,
    CVLCPT_MCA_CVLCPT_RCPT_NO /* 민원합건민원접수번호 */
