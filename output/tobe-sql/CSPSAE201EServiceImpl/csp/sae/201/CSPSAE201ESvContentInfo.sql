SELECT #strYR_CD# AS LINK_YR /* 연계년도 */,
    #strEDU_CRS_CD# AS #STREDU_CRS_CD#,
    #strCRS_CDN# AS CRS_CDN /* 과정기수 */,
    B.SV_QEST_NO,
    A.SV_TIT_CD,
    B.SUB_QEST_NO,
    B.SV_QEST_TXT,
    B.QEST_DVSN_CD,
    C.SV_CLU_NO,
    C.SV_CLU_TXT,
    0
FROM TBCSPSAE121C A , TBCSPSAE122C B , TBCSPSAE123C C WHERE 1=1 AND B.SV_TIT_CD = A.SV_TIT_CD AND C.SV_TIT_CD = B.SV_TIT_CD AND C.SV_QEST_NO = B.SV_QEST_NO AND A.SV_TIT_CD = #strSvTit# ORDER BY B.SV_QEST_NO, C.SV_CLU_NO