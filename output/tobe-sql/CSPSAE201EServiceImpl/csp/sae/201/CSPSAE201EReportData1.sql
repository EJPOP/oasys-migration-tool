SELECT A.LINK_YR /* 연계년도 */,
    A.CRS_CDN /* 과정기수 */,
    A.CRS_NM /* 과정명 */,
    A.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    A.INFO_RLPR_NM /* 정보관계자명 */,
    A.PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    A.JBGD_NM /* 직급명 */,
    A.OFC_TELNO /* 사무실전화번호 */,
    A.SLCT_CVLPR_MPNO /* 청탁민원인휴대전화번호 */,
    A.EML_ADDR /* 이메일주소 */
FROM ( SELECT D.LINK_YR /* 연계년도 */,
    D.CRS_CDN /* 과정기수 */,
    C.CRS_CD,
    C.CRS_NM /* 과정명 */,
    D.INFO_RLPR_NM /* 정보관계자명 */,
    (SELECT AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */
FROM TBDCMACM015M WHERE INST_CD = D.INST_CD) AS PROF_RBPRSN_INST_NM , D.JBGD_NM , D.OFC_TELNO , SUBSTR(D.SLCT_CVLPR_MPNO, 0, 3) || '-' ||SUBSTR(D.SLCT_CVLPR_MPNO, 4, 4) || '-' || SUBSTR(D.SLCT_CVLPR_MPNO, 8, 4)AS SLCT_CVLPR_MPNO , D.EML_ADDR , D.GRNDS_EXMN_AEX_SQNO , D.TANE_ID FROM TBCSPSAE103C C , TBCSPSAE301D D WHERE 1=1 AND D.CRS_CD = C.CRS_CD AND D.LINK_YR = #LINK_YR# AND D.CRS_CD = #strEDU_CRS_CD# AND D.CRS_CDN = #CRS_CDN# ORDER BY D.GRNDS_EXMN_AEX_SQNO ) A ORDER BY A.GRNDS_EXMN_AEX_SQNO, A.TANE_ID