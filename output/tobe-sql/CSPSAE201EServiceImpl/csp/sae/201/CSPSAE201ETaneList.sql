SELECT D.LINK_YR /* 연계년도 */,
    B.EDU_CAT_NM,
    C.CRS_NM /* 과정명 */,
    D.CRS_CD,
    D.CRS_CDN /* 과정기수 */,
    D.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    D.TANE_ID,
    D.DSPS_RQT_TRPR_BRDT /* 처분요구대상자생년월일 */,
    D.INFO_RLPR_NM /* 정보관계자명 */,
    (SELECT AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */
FROM TBDCMACM015M WHERE INST_CD = D.INST_CD) AS PROF_RBPRSN_INST_NM , D.CNSTN_MBCMT_DEPT_NM , D.JBGD_NM , D.OFC_TELNO , D.SLCT_CVLPR_MPNO , D.EML_ADDR , D.FIN_YN , D.UN_FIN_RSN , CASE WHEN D.INST_CD IS NULL THEN 'N' ELSE 'Y' END AS WRITE_YN , D.FNSH_DT /*수료시간*/ , D.EVAL_RESULT /*평가결과*/ FROM TBCSPSAE102C B , TBCSPSAE103C C , TBCSPSAE301D D WHERE 1=1 AND C.EDU_DVSN_CD = B.EDU_DVSN_CD AND C.EDU_CAT_CD = B.EDU_CAT_CD AND D.CRS_CD = C.CRS_CD AND D.LINK_YR = #LINK_YR# AND D.CRS_CD = #strEDU_CRS_CD# AND D.CRS_CDN = #CRS_CDN# ORDER BY D.GRNDS_EXMN_AEX_SQNO, D.TANE_ID