SELECT A.ADT_YR /* 감사년도 */,
    A.ADT_NO /* 감사번호 */,
    F_MNG_KND_AUDYRNO(A.ADT_YR, A.ADT_NO, A.ADT_KDCD, '-') AS ADT_CLSF_NO /* 감사분류번호 */,
    A.ADT_KDCD /* 감사종류코드 */,
    F_CODE_NM('1000007',A.ADT_KDCD) AS F_CODE_NM,
    F_DPT_INFO(A.MNG_DEPT_CD,'1') AS F_DPT_INFO,
    A.MNG_DEPT_CD /* 관리부서코드 */,
    ADT_MTHD_SECD AS ADT_MTHD_SECD /* 감사방법구분코드 */,
    A.ADT_MTTR_NM /* 감사사항명 */,
    (CASE WHEN A.ADT_NO >= '900' THEN '0' || A.ADT_NO ELSE '1'|| A.ADT_NO END) AS ADT_NO /* 감사번호 */
FROM TB_BADDED001M A WHERE 1=1 AND CVLCPT_ADT_YR=#CVLCPT_ADT_YR# AND A.ADT_KDCD LIKE '%9' AND A.MNG_DEPT_CD like SUBSTR(#strUpSpvBrdvCd#,1,3)||'%' AND A.MNG_DEPT_CD =#MNG_DEPT_CD# AND A.ADT_GRNT_NO ORDER BY A.ADT_YR DESC, SORTER ASC