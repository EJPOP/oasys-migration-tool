SELECT A.CLM_SECD /* 청구구분코드 */,
    A.CLM_YR /* 청구년도 */,
    A.CLM_RCPT_NO /* 청구접수번호 */,
    A.CLM_YR||'-'||F_CODE_NM('1000740',A.CLM_SECD)||'-'||A.CLM_RCPT_NO AS NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    A.CLM_YMD /* 청구일자 */,
    A.PRCS_TERM_SECD /* 처리기한구분코드 */,
    A.CLM_TTL /* 청구제목 */,
    A.NTAS_CLM_CN /* 국회청구내용 */,
    A.PRCS_DEPT_CD /* 처리부서코드 */,
    F_DPT_INFO(A.PRCS_DEPT_CD,'1') AS PRCS_DEPT_NM /* 처리부서명 */,
    A.PMN_CMT_NM /* 상임위원회명 */,
    A.PRPSL_ASBM_NM /* 발의의원명 */,
    A.PRCS_TERM_YMD /* 처리기한일자 */,
    NVL(A.PRLG_YN,'N') AS PRLG_YN /* 연장여부 */,
    A.PRLG_PRCS_TERM_YMD /* 연장처리기한일자 */,
    A.CPLT_YMD /* 완결일자 */,
    A.DOC_ID /* 문서아이디 */,
    A.PRLG_RSN_CN /* 연장사유내용 */,
    A.REL_BSS_DOC_NO /* 관계근거문서번호 */,
    A.REL_BSS_ENFC_YMD /* 관계근거시행일자 */,
    DECODE(A.CPLT_YMD,NULL,'',TO_DATE(A.CPLT_YMD)-TO_DATE(A.CLM_YMD)) AS CPLT_YMD /* 완결일자 */,
    A.PIC_NM /* 담당자명 */,
    A.PRCS_STTS_CN /* 처리상태내용 */,
    F_CODE_NM('1000376', A.PRCS_TERM_SECD) AS F_CODE_NM,
    DECODE(B.CVLCPT_ADT_YR,NULL,NULL,F_MNG_KND_AUDYRNO(B.CVLCPT_ADT_YR, B.ADT_NO, B.ADT_KDCD, '-')) AS CVLCPT_ADT_YR /* 민원감사년도 */,
    B.CVLCPT_ADT_YR /* 민원감사년도 */,
    B.ADT_NO /* 감사번호 */
FROM TB_CSPEAR007M A ,(SELECT B.ACADT_DSCTN_NM_2 /* 실지감사내역명2 */,
    DSPS_RQT_NM_1 /* 처분요구명1 */,
    DSPS_RQT_NM_3 /* 처분요구명3 */,
    C.ADT_YR /* 감사년도 */,
    C.ADT_NO /* 감사번호 */,
    C.ADT_KDCD /* 감사종류코드 */
FROM TBBADDED030L B ,TBBADDED001M C WHERE (SUBSTR(C.ADT_KDCD,'3','1') <> '5' OR C.ADT_KDCD IS NULL) AND B.CVLCPT_ADT_YR = C.CVLCPT_ADT_YR AND B.ADT_NO = C.ADT_NO ) B WHERE 1=1 AND A.CLM_SECD = B.DSPS_RQT_NM_2(+) AND A.CLM_YR = B.DSPS_RQT_NM_1(+) AND A.CLM_RCPT_NO = B.DSPS_RQT_NM_3(+) AND A.CLM_YR = #CVLCPT_RCPT_YR# AND A.CLM_RCPT_NO = #CVLCPT_RCPT_RCPT# AND A.CLM_YMD &gt;= #strREQ_DTS# AND A.CLM_YMD &lt;= #strREQ_DTE# AND A.CLM_TTL LIKE '%'||#strAUD_REQ_TIT_NM#||'%' AND A.PRCS_TERM_SECD = #PRCS_TERM_SECD# AND A.PRPSL_ASBM_NM LIKE '%'||#strSGS_ASBM_NM#||'%' ORDER BY A.CLM_YR DESC, A.CLM_RCPT_NO DESC