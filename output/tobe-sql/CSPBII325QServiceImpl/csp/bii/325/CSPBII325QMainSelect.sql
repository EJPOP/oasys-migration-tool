SELECT T1.INFO_RLPR_NM /* 정보관계자명 */,
    T1.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    F_ORG_INFO(T1.OGDP_INST_CD,'1') AS TPOF_OGDP_INST_NM /* 제보소속기관명 */,
    T1.JBPS_NM || ' / ' || F_CODE_NM('1000173', T1.CLSS_CD) AS JBPS_NM /* 직위명 */,
    F_CODE_NM('1000908',T2.DBT_PSN_DVSN_CD) AS F_CODE_NM,
    F_CODE_NM('1000909',T2.TYP_DVSN_CD) AS F_CODE_NM,
    T2.TPOF_OGDP_INST_NM /* 제보소속기관명 */,
    T2.BLT_PST_NM,
    T2.SMM_TXT,
    T2.DSGN_YMD /* 지정일자 */,
    '요약내용보기' AS SMM_TXT2,
    SUBSTR(T1.CRU_RRNO_SCRTY,1,6)||'-*******' AS SUBSTR,
    T2.PIC_ENO /* 담당자직원전산번호 */,
    F_USR_INFO(T2.PIC_ENO,'2') AS PIC_NM /* 담당자명 */
FROM TBCSPBII300M T1 ,TBCSPBII380M T2 WHERE T1.CRU_RRNO_SCRTY = T2.CRU_RRNO_SCRTY AND T1.INFO_RLPR_NM LIKE #strNam#||'%' AND T1.CRU_RRNO_SCRTY LIKE #strEin# || '%' AND T2.DBT_PSN_DVSN_CD = #strDBT_PSN_DVSN_CD# ORDER BY (CASE WHEN T2.DBT_PSN_DVSN_CD != '30' THEN 1 ELSE 2 END) ASC, T2.DSGN_YMD DESC