SELECT TOTALDAY
FROM (SELECT CC.YYYYMMDD,
    CC.TOTALDAY,
    (CASE WHEN CC.TOTALDAY = (SELECT INFO_YR || AS HLDY_CRTR_MND /* 휴일기준월일 */
FROM TBBADDED013B WHERE INFO_YR || HLDY_CRTR_MND = CC.TOTALDAY) THEN '1' ELSE '0' END) HOLIDAY FROM ( SELECT TO_CHAR(DAYS, 'DD(DY)') AS TO_CHAR,
    TO_CHAR(DAYS, 'YYYYMMDD') AS TO_CHAR
FROM( SELECT START_DAY + LEVEL -1 AS START_DAY
FROM( SELECT TO_DATE(TO_CHAR(TO_DATE(SYSDATE) +1,'YYYYMMDD')) AS TO_DATE
FROM DUAL ) CONNECT BY START_DAY + LEVEL - 1 <= TO_CHAR(TO_DATE(SYSDATE) +15,'YYYYMMDD') ) )CC) WHERE HOLIDAY = 0 AND ROWNUM <= 3