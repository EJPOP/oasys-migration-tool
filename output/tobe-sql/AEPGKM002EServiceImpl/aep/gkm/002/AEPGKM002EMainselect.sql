SELECT A.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    A.KNB,
    A.CTG_ID,
    CTG_NM,
    KLD_NM,
    TG_CAT_ID,
    TG_CAT_NM,
    STRG_KLD_SNO
FROM TBAEPGKM004L AS A, ( SELECT CTG_ID,
    CTG_NM
FROM ( SELECT CTG_ID,
    CTG_NM,
    HIRK_CTG_ID
FROM TBAEPGKM003L WHERE TRGT_NOP_ENO = #TRGT_NOP_ENO# ) START WITH CTG_ID = #CTG_ID# CONNECT BY PRIOR CTG_ID = HIRK_CTG_ID GROUP BY CTG_ID, CTG_NM ORDER BY CTG_ID ) AS B, TBAEPGKM001M AS C, TBAEPGKM017M AS D WHERE 1=1 AND A.CTG_ID = B.CTG_ID AND A.KNB = C.KNB AND C.KLD_CAT_CD = D.TG_CAT_ID AND TRGT_NOP_ENO = #TRGT_NOP_ENO# ORDER BY STRG_KLD_SNO DESC