SELECT L.APV_SNO,
    L.TPOF_ATRZ_SECD /* 제보결재구분코드 */,
    L.ATRZ_DOC_NO /* 결재문서번호 */,
    F_USR_INFO (L.APRVR_ENO,'2') AS F_USR_INFO,
    F_USR_INFO (L.APRVR_ENO,'1') AS USER_ID /* 사용자아이디 */,
    F_DPT_INFO (L.APRVR_DEPT_CD,'1') AS F_DPT_INFO,
    F_DPT_INFO (L.APRVR_DEPT_CD,'4') AS F_DPT_INFO,
    /* 약칭부서명 추가 - 2017.03.31 yyh */ L.APRVR_DEPT_CD AS APRVR_DEPT_CD /* 결재자부서코드 */,
    F_CODE_NM ('1000173',L.APRVR_JBGD_SECD) AS F_CODE_NM,
    APRVR_JBGD_SECD /* 결재자직급구분코드 */,
    F_CODE_NM ('1000176',L.APRVR_JBPS_SECD)|| CASE WHEN ONR_PST_NM LIKE '%직무대리%' THEN ' 직무대리' WHEN ONR_PST_NM LIKE '%권한대행%' THEN ' 권한대행' ELSE '' END AS F_CODE_NM,
    L.APRVR_JBPS_SECD /* 결재자직위구분코드 */,
    L.APV_DH,
    L.APVR_STA_CD,
    L.APRVR_ENO /* 결재자직원전산번호 */,
    L.APV_OPIN,
    CASE WHEN L.APV_OPIN IS NOT NULL THEN TRIM(SUBSTRB(L.APV_OPIN,0,22)) || '...' ELSE '' END AS APV_OPIN,
    /* 자른 결재의견 추가 - 2017.03.31 yyh */ L.APRVR_KDCD AS APRVR_KDCD /* 결재자종류코드 */,
    L.APVR_SEQ,
    L.ONR_PST_NM,
    NVL((SELECT MIN(H.APV_DH)
FROM TBDCMACM023H H WHERE H.ATRZ_DOC_NO = L.ATRZ_DOC_NO AND H.APRVR_ENO = L.APRVR_ENO AND SUBSTR(H.APVR_STA_CD,0,1) = '2' AND SUBSTR(L.APVR_STA_CD,0,1) = '2' ),L.APV_DH) AS FRT_APV_DH /* 최초 결재 일자 (실지감사에서 사용) */ ,VACANCY_CD ,NVL(L.READ_YN,'N') AS READ_YN FROM TBDCMACM023L L WHERE 1=1 AND L.ATRZ_DOC_NO=#ATRZ_DOC_NO# AND L.TPOF_ATRZ_SECD=#TPOF_ATRZ_SECD# ORDER BY L.APV_SNO, L.APRVR_KDCD, L.APVR_SEQ