    CASE WHEN A.GB = '1' THEN '범죄및징계' WHEN A.GB = '2' THEN '망실및훼손' WHEN A.GB = '3' THEN '관련법령(계약체결)' WHEN A.GB = '4' THEN '관련법령(국유재산)' END AS GB,
    A.NTF_NO,
    A.KEY1,
    A.KEY2,
    A.KEY3,
    A.KEY4,
    A.SRECS_DEPT_CD /* 심사재심의부서코드 */,
