    T1.NTFCTN_SECD /* 통보구분코드 */,
    T1.NTFCTN_SQNO /* 통보순번 */,
    '' AS KEY4,
    T1.DEPT_CD AS DEPT_CD /* 부서코드 */,
    T1.NTFCTN_RCPT_YMD AS NTFCTN_RCPT_YMD /* 통보접수일자 */,
    T1.NTFCTN_NM AS NTFCTN_NM /* 통보명 */,
    T1.PRCS_PIC_ID AS PRCS_PIC_ID /* 처리담당자아이디 */,
    T1.PRCS_PIC_NM AS PRCS_PIC_NM /* 처리담당자명 */
FROM TBBADGDA001M T1 /* 통보사항기본 */ WHERE 1=1 AND T1.STT_RCPT_STTS_SECD = '71' AND (T1.PRCS_PIC_NM IS NULL OR T1.PRCS_PIC_NM = '') UNION ALL SELECT DECODE(T2.NTFCTN_STT_NTFCTN_SECD,'100','3','4') AS NTFCTN_STT_NTFCTN_SECD /* 통보법령통보구분코드 */,
    F_ORG_INFO(T2.NTFCTN_INST_CD,'1') || '-' || T2.NTFCTN_YR || '-' || T2.NTFCTN_SQNO AS F_ORG_INFO,
    T2.NTFCTN_INST_CD /* 통보기관코드 */,
    T2.NTFCTN_YR /* 통보년도 */,
    TO_CHAR(T2.NTFCTN_SQNO) AS TO_CHAR,
    T2.NTFCTN_STT_NTFCTN_SECD /* 통보법령통보구분코드 */,
    T2.DEPT_CD AS DEPT_CD /* 부서코드 */,
    T2.STT_RCPT_YMD AS STT_RCPT_YMD /* 법령접수일자 */,
    T2.NTFCTN_NM AS NTFCTN_NM /* 통보명 */,
    T2.PRCS_PIC_ID AS PRCS_PIC_ID /* 처리담당자아이디 */,
    T2.PRCS_PIC_NM AS PRCS_PIC_NM /* 처리담당자명 */
FROM TBBADGDA004M T2 /* 관련법령 통지통보 */ WHERE 1=1 AND T2.STT_RCPT_STTS_SECD = '71' AND (T2.PRCS_PIC_NM IS NULL OR T2.PRCS_PIC_NM = '') ) A WHERE 1=1 AND A.NTFCTN_RCPT_YMD BETWEEN #strAcpDtStr# AND #strAcpDtEnd# AND A.SRECS_DEPT_CD IN (SELECT SRECS_DEPT_CD /* 심사재심의부서코드 */
FROM TABLE(F_AUTH_DEPT(#strCnb#, #strDptAuth#, '', #strDptCd#))) AND A.SRECS_DEPT_CD = #SRECS_DEPT_CD# AND A.GB = #strGb# ORDER BY A.NTFCTN_RCPT_YMD DESC, NTF_NO