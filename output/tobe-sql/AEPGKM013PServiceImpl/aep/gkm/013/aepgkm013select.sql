SELECT C.ADT_YR AS ADT_YR /* 감사년도 */,
    C.ADT_NO AS ADT_NO /* 감사번호 */,
    C.ADT_YR || '-' || C.ADT_NO AS ADT_CLSF_NO /* 감사분류번호 */,
    TO_CHAR(TO_DATE( MIN(B.ADT_BGNG_YMD), 'YYYYMMDD'), 'YYYY.MM.DD')||'~'||TO_CHAR(TO_DATE( MIN(B.ADT_END_YMD), 'YYYYMMDD'), 'YYYY.MM.DD') AS TO_CHAR,
    F_CODE_NM('1000007',C.ADT_KDCD) AS F_CODE_NM,
    MAX(C.ADT_MTTR_NM) AS ADT_MTTR_NM /* 감사사항명 */,
    MIN(A.INST_CD) AS INST_CD /* 기관코드 */,
    MAX(A.FT_SQNO) AS FT_SQNO /* 출장조순번 */,
    MIN(B.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(B.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(B.ADT_DCN) AS ADT_DCN /* 감사일수 */,
    MAX(B.BZTRP_TL_ENO) AS BZTRP_TL_ENO /* 출장반장직원전산번호 */,
    MAX(B.ADT_BZTRP_KDCD) AS ADT_BZTRP_KDCD /* 감사출장종류코드 */,
    MAX(C.ADT_ASMT_SECD) AS ADT_ASMT_SECD /* 감사과제구분코드 */,
    MAX(C.INST_SECD) AS INST_SECD /* 기관구분코드 */,
    MAX(C.ADT_KDCD) AS ADT_KDCD /* 감사종류코드 */,
    MAX(C.SPVR_DEPT_CD) AS SPVR_DEPT_CD /* 감리원부서코드 */,
    D.CNSTN_MBCMT_DEPT_NM AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    MAX(C.ADT_MTHD_SECD) AS ADT_MTHD_SECD /* 감사방법구분코드 */
FROM TB_BADDED008L A ,TB_BADDED002M B ,TB_BADDED001M C ,TBDCMACM002M D WHERE 1=1 AND A.BZTRP_YR = B.BZTRP_YR AND A.BZTRP_SQNO = B.BZTRP_SQNO AND B.BZTRP_SECD = '1' AND C.ADT_YR = B.REL_ADT_YR AND C.ADT_NO = B.REL_ADT_NO AND C.SPVR_DEPT_CD = D.SRECS_DEPT_CD(+) A.INST_CD = #INST_CD# C.ADT_YR = #CVLCPT_ADT_YR# C.ADT_KDCD = #ADT_KDCD# C.ADT_YR||C.ADT_NO = #ADT_NO# C.ADT_MTTR_NM LIKE '%' || #AUD_SBJ_NM# || '%' GROUP BY C.ADT_YR ,C.ADT_NO ,C.ADT_KDCD ,D.CNSTN_MBCMT_DEPT_NM ORDER BY C.ADT_YR DESC ,C.ADT_NO