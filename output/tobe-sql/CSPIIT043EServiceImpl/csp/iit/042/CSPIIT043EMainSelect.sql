SELECT CVLCPT_ADT_YR /* 민원감사년도 */,
    ADT_NO /* 감사번호 */,
    F_MNG_KND_AUDYRNO(CVLCPT_ADT_YR, ADT_NO, ADT_KDCD, '-') AS ADT_CLSF_NO /* 감사분류번호 */,
    ADT_KDCD /* 감사종류코드 */,
    F_CODE_NM('1000007',ADT_KDCD) AS F_CODE_NM,
    ADT_MTTR_NM /* 감사사항명 */,
    TO_DATE(ADT_BGNG_YMD, 'YYYY-MM-DD') || ' ~ ' || TO_DATE(ADT_END_YMD, 'YYYY-MM-DD') AS ADT_YMD /* 감사일자 */,
    ADT_DCN /* 감사일수 */,
    F_DPT_INFO(SPRVSN_DEPT_CD,'1') AS F_DPT_INFO,
    /*주관부서명*/ SEL_YN,
    RMRK_CN /* 비고내용 */,
    ADT_RPRS_INST_CD /* 감사대표기관코드 */,
    F_ORG_INFO(ADT_RPRS_INST_CD,'1') AS F_ORG_INFO,
    ADT_BGNG_YMD /* 감사시작일자 */,
    ADT_END_YMD /* 감사종료일자 */,
    SPRVSN_DEPT_CD /* 주관부서코드 */,
    CVLCPT_DOC_ID /* 민원문서아이디 */,
    SUP_YN
FROM TBCSPIIT023M WHERE ADT_BGNG_YMD >= '20210201' AND NOT SUBSTR(F_CODE_NM('1000007',ADT_KDCD),1,2)='서면' AND NOT SUBSTR(F_CODE_NM('1000007',ADT_KDCD),1,2)='확인' AND CVLCPT_ADT_YR = #CVLCPT_ADT_YR# AND ADT_NO = #ADT_NO# AND ADT_KDCD = #ADT_KDCD#