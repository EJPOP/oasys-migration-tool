SELECT T1.TPOF_RCPT_YM /* 제보접수연월 */,
    T1.TPOF_INFO_RPTP_SQNO /* 제보정보보고서순번 */,
    T1.TPOF_INFO_TPCD /* 제보정보유형코드 */,
    T1.TPOF_DOC_ID /* 제보문서아이디 */,
    T1.TPOF_NM /* 제보명 */,
    T1.FRST_USER_ID AS FRST_USER_ID /* 최초사용자아이디 */,
    T2.USER_NM /* 사용자명 */,
    T1.TPOF_WRT_STTS_SECD /* 제보작성상태구분코드 */,
    T1.TPOF_SBMSN_DT /* 제보제출일시 */,
    F_CODE_NM('1000606', T1.TPOF_WRT_STTS_SECD) AS F_CODE_NM,
    '정보' || '-' || SUBSTR(T1.TPOF_RCPT_YM, 3, 4) || '-' || LPAD(T1.TPOF_INFO_RPTP_SQNO, 4, '0') AS SUBSTR,
    CASE WHEN SUBSTR(T1.TPOF_INFO_TPCD, 0, 2) = '01' THEN F_CODE_NM('1000607', '0100')||' - '||F_CODE_NM('1000607', T1.TPOF_INFO_TPCD) WHEN SUBSTR(T1.TPOF_INFO_TPCD, 0, 2) = '02' THEN F_CODE_NM('1000607', '0200')||' - '||F_CODE_NM('1000607', T1.TPOF_INFO_TPCD) END AS SUBSTR
FROM TB_CSPDCP501M T1 , TBDCMACM001M T2 WHERE 1=1 AND T1.FRST_USER_ID = T2.USER_ID(+) AND T1.TPOF_WRT_STTS_SECD = #SLCT_WRT_STTS_SECD# AND T1.TPOF_INFO_TPCD = #CRU_INFO_TPCD# AND T1.TPOF_NM LIKE '%'||#TIT_NM#||'%' AND TO_CHAR(T1.FRST_REG_DT, 'YYYYMMDD') BETWEEN #STARTDT# AND #ENDDT# AND TO_CHAR(T1.FRST_REG_DT, 'YYYYMMDD') BETWEEN #STARTDT# AND TO_CHAR(SYSDATE, 'YYYYMMDD') ORDER BY T1.TPOF_RCPT_YM DESC, T1.TPOF_INFO_RPTP_SQNO DESC