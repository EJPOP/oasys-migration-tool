    '판정청구' AS KND_NM,
    LINK_YR AS LINK_YR /* 연계년도 */,
    TO_CHAR(NTFCTN_RCPT_SQNO) AS TO_CHAR,
    '' AS ADT_GRNT_NO /* 감사부여번호 */,
    SRECS_DEPT_CD AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    F_DPT_INFO(SRECS_DEPT_CD,'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    RSCH_ASMT_TTL AS RSCH_ASMT_TTL /* 연구과제제목 */
