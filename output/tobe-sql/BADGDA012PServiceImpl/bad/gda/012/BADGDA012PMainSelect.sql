    '' AS ADT_GRNT_NO /* 감사부여번호 */,
    SRECS_DEPT_CD AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    F_DPT_INFO(SRECS_DEPT_CD,'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    RSCH_ASMT_TTL AS RSCH_ASMT_TTL /* 연구과제제목 */
FROM TBBADGDA001M WHERE LINK_YR = #LINK_YR# AND NTFCTN_SECD = '2' /* 손망실 */ AND OCRN_SECD IN ('3','4','5') /* 망실,훼손,멸실 */ AND '2' = #strTskDvsnCd# AND SRECS_DEPT_CD = #SRECS_DEPT_CD# AND RSCH_ASMT_TTL LIKE '%' || #strTitNm# || '%' UNION ALL SELECT '3' AS CVLCPT_TASK_SECD /* 민원업무구분코드 */,
    '판정청구' AS TSK_DVSN_NM,
