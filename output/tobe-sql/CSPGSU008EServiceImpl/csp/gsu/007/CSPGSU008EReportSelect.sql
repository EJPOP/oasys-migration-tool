SELECT DECODE(A.PRPS_ACP_NO,NULL,'',A.PRPS_YE||'-'||A.PRPS_ACP_NO) AS PRPS_ACP_NO /* 접수번호 */,
    TO_CHAR(TO_DATE(A.NTFCTN_RCPT_YMD),'YYYY-MM-DD') AS NTFCTN_RCPT_YMD /* 통보접수일자 */,
    TO_CHAR(TO_DATE(A.SBMSN_YMD),'YYYY-MM-DD') AS SBMSN_YMD /* 제출일자 */,
    A.PRPS_TIT_NM /* 제안제목 */,
    F_USR_INFO(A.PRPS_PEN_CNB,'2') AS F_USR_INFO /* 제안자명 */,
    B.JNT_PRPS_PEN_NAM /* 공동제안자 */,
    F_CODE_NM('1000075',A.PRPS_STA_CD) AS F_CODE_NM /* 진행단계 */,
    A.CUR_SYE_TXT /* 현행제도내용 */,
    A.PRB_TXT /* 문제점내용 */,
    A.IMP_ATV_TXT /* 개선대안내용 */,
    A.IMP_EFCT_TXT /* 개선효과내용 */,
    F_DPT_INFO(A.EXAM_DPT_CD,'1') AS F_DPT_INFO /* 검토부서 */,
    A.EXAM_DT /* 검토일자 */,
    F_USR_INFO(A.EXAM_PEN_CNB,'2') AS F_USR_INFO /* 검토자 */,
    A.EXAM_TXT /* 검토의견 */,
    A.EXAM_RSLT_CD /* 검토결과 */,
    DECODE(A.PRPS_STA_CD,'N','Y','M','N','') AS PRPS_STA_CD /* 채택여부 */,
    TO_CHAR(TO_DATE(A.JDG_DT),'YYYY-MM-DD') AS TO_CHAR /* 위원회일자 */,
    F_CODE_NM('1000076',A.RK_CD) AS F_CODE_NM /* 제안등급 */,
    A.PRPS_ASC /* 제안평점 */,
    F_CODE_NM('1000286',A.CNTR_PRPS_RCMD_DVSN_CD) AS F_CODE_NM /* 중앙제안추천여부 */
FROM TBCSPGSU001M A /*도서자료기본*/ , ( SELECT PRPS_YE,
    PRPS_SLN,
    AGGR_CONCAT(F_USR_INFO (A.PRPS_PEN_CNB,'2'),'/') AS AGGR_CONCAT
FROM TBCSPGSU002L A WHERE 1=1 AND A.PRPS_YE = #strPRPS_YE# AND A.PRPS_SLN = #strPRPS_SLN# AND A.GRNDS_EXMN_AEX_SQNO <> 1 GROUP BY PRPS_YE, PRPS_SLN) B WHERE 1=1 AND A.PRPS_YE = #strPRPS_YE# AND A.PRPS_SLN = #strPRPS_SLN# AND A.PRPS_YE = B.PRPS_YE(+) AND A.PRPS_SLN = B.PRPS_SLN(+)