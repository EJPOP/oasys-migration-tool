SELECT A.BOKG_APL_DH,
    A.LOUNGE_ID,
    B.LOUNGE_NM,
    A.BOKG_PEN_ID AS USER_ID,
    C.USER_NM /* 사용자명 */,
    D.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    TO_CHAR(TO_DATE(A.BOKG_DT), 'YYYY-MM-DD') AS TO_CHAR,
    A.RSCH_ASMT_TTL /* 연구과제제목 */,
    A.CRU_TELNO /* 부패전화번호 */,
    (SELECT AS CD_NM
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD ='1000291' AND CD = A.COFM_CD) AS COFM_NM, A.ATRZ_DOC_NO, A.HNDL_STEP, (SELECT CD_NM
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD ='1000881' AND CD = A.HNDL_STEP) AS HNDL_STEP_NM, A.COFM_CD FROM TBCSPOEP012M A, TBCSPOEP011M B, TBDCMACM001M C, TBDCMACM002M D WHERE 1=1 AND A.LOUNGE_ID = #LOUNGE_ID# AND A.LOUNGE_ID = B.LOUNGE_ID AND A.BOKG_PEN_ID = C.USER_ID AND C.SRECS_DEPT_CD = D.SRECS_DEPT_CD AND BOKG_DT = #YYYYMMDD# /* 예약일 */ ORDER BY BOKG_APL_DH