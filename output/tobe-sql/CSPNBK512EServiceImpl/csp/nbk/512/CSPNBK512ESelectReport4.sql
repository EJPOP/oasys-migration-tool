SELECT FLOOR(A.WTDW_ACM * B.WTDW_DVD_RTS / 10 / 365) AS FLOOR,
    B.WTDW_DVD_RTS,
    FLOOR(A.NWD_ACM * B.NWD_DVD_RTS / 10 / 365) AS FLOOR,
    B.NWD_DVD_RTS,
    FLOOR(A.EX_ACM * B.EX_DVD_RTS / 10 / 365) AS FLOOR,
    B.EX_DVD_RTS
FROM ( SELECT SUM(WTDW_ACM) AS WTDW_ACM,
    SUM(NWD_ACM) AS NWD_ACM,
    SUM(EX_ACM) AS EX_ACM
FROM TBCSPNBK402M WHERE CSH_DVD_YN = 'N' AND DVD_YR = #dvdYr# ) A , TBCSPNBK401M B WHERE B.DVD_YR = #dvdYr#