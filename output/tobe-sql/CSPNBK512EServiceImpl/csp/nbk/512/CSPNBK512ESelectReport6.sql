/* * 예상이익금배당지급자 * 중간 리스트부분 */ SELECT '개인' AS MOUI_DVSN,
    A.MOUI_NO,
    NVL(C.USER_NM,'') AS USER_NM /* 사용자명 */,
    NVL(B.TRGT_NOP_ENO, '') AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    D.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    E.CD_NM,
    A.WTDW_ACM,
    A.NWD_ACM,
    A.EX_ACM,
    A.DVD_AM
FROM TBCSPNBK402M A, TBCSPNBK001M B, TBDCMACM001M C, TBDCMACM002M D, TBDCMACM013C E WHERE A.DVD_YR = #dvdYr# AND A.MOUI_NO = B.MOUI_NO AND B.MOUI_DVSN_CD = '0' AND B.TRGT_NOP_ENO = C.TRGT_NOP_ENO AND C.SRECS_DEPT_CD = D.SRECS_DEPT_CD AND E.CMM_CD_DVSN_CD = '1000173' AND E.CD = C.JBGD_SECD UNION SELECT '단체' AS MOUI_DVSN,
    A.MOUI_NO,
    NVL(B.OGNZ_NM,'') AS USER_NM /* 사용자명 */,
    '' AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    '' AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    '' AS CD_NM,
    A.WTDW_ACM,
    A.NWD_ACM,
    A.EX_ACM,
    A.DVD_AM
FROM TBCSPNBK402M A, TBCSPNBK001M B WHERE A.DVD_YR = #dvdYr# AND A.MOUI_NO = B.MOUI_NO AND B.MOUI_DVSN_CD = '1'