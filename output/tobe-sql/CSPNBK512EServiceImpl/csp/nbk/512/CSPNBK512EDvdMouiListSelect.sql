SELECT /* CSPNBK512EDvdMouiListSelect */ D1.DVD_YR,
    F_CODE_NM( '1000153', D2.MOUI_DVSN_CD) AS F_CODE_NM,
    D1.MOUI_NO,
    DECODE( D2.MOUI_DVSN_CD, '0', D3.USER_NM, '1', D2.OGNZ_NM ) AS MOUI_DVSN_CD,
    D2.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    D1.WTDW_ACM,
    D1.NWD_ACM,
    D1.EX_ACM,
    D1.DVD_AM,
    D1.CSH_DVD_YN,
    D4.INV_BL
FROM TBCSPNBK402M D1 , TBCSPNBK001M D2 , TBDCMACM001M D3 , TBCSPNBK101M D4 WHERE D1.MOUI_NO = D2.MOUI_NO AND D2.TRGT_NOP_ENO = D3.TRGT_NOP_ENO AND D1.MOUI_NO = D4.MOUI_NO(+) AND D1.DVD_YR = #dvdYr# D1.MOUI_NO = #mouiNo# ( D3.USER_NM LIKE '%' || #mouiNm# || '%' OR D2.OGNZ_NM LIKE '%' || #mouiNm# || '%' ) D1.CSH_DVD_YN = #cshDvdYn# ORDER BY MOUI_NO