    B.EDU_DVSN_CD,
    B.EDU_CAT_CD,
    A.CRS_CD,
    A.CRS_CDN /* 과정기수 */,
    A.ASG_YN,
    D.LCT_RM_DVSN_CD,
    A.LCT_RM_NO,
    A.LCT_RM_NO,
    A.USE_STR_DT,
    A.USE_END_DT,
    A.USE_STR_HM,
    A.USE_END_HM
FROM TBCSPSAE203D A , TBCSPSAE103C B , TBDCMACM013C C , TBCSPSAE114C D WHERE B.CRS_CD = A.CRS_CD AND C.CMM_CD_DVSN_CD = '1000139' AND D.LCT_RM_DVSN_CD = C.CD AND D.LCT_RM_NO = A.LCT_RM_NO AND A.LINK_YR = #LINK_YR# AND B.EDU_DVSN_CD = #strEDU_DVSN_CD# AND B.EDU_CAT_CD = #strEDU_CAT_CD# AND A.CRS_CD = #strEDU_CRS_CD# AND A.CRS_CDN = #CRS_CDN# ORDER BY A.LINK_YR, B.EDU_DVSN_CD, B.EDU_CAT_CD, A.CRS_CD, A.CRS_CDN, A.ASG_YN DESC, D.LCT_RM_DVSN_CD, A.LCT_RM_NO, A.USE_STR_DT, A.USE_STR_HM