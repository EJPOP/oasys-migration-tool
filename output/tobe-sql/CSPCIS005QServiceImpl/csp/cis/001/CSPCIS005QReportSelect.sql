SELECT A.INSP_REGI_DT,
    A.INSP_REGI_NO,
    A.INSP_LVL_CNT,
    B.CD_NM,
    A.INSP_TIT_ENN,
    A.INSP_ECN,
    A.INSP_USR_EID,
    A.INSP_USR_NAM,
    #fromYmd# AS #FROMYMD#,
    #toYmd# AS #TOYMD#
FROM TBCSPCIS001M A, TBDCMACM013C B WHERE 1=1 AND A.INSP_DVSN_CD = B.CD AND B.CMM_CD_DVSN_CD = '1000308' AND A.INSP_LVL_CNT = 0 AND (INSP_USR_NAM like '%' || #name# || '%' or (INSP_REGI_DT , INSP_REGI_NO) IN (SELECT A.INSP_REGI_DT,
    A.INSP_REGI_NO
FROM TBCSPCIS001M A WHERE A.INSP_USR_NAM like '%' || #name# || '%') ) AND INSP_DVSN_CD = #dvsn# AND INSP_REGI_DT &gt;= #fromYmd# AND INSP_REGI_DT &lt;= #toYmd# AND (INSP_TIT_ENN like '%' || #title# || '%' or (INSP_REGI_DT , INSP_REGI_NO) IN (SELECT A.INSP_REGI_DT,
    A.INSP_REGI_NO
FROM TBCSPCIS001M A WHERE A.INSP_TIT_ENN like '%' || #title# || '%') ) AND SLCT_STTS_YN = #SLCT_STTS_YN# ORDER BY INSP_REGI_DT DESC, INSP_REGI_NO DESC, INSP_LVL_CNT