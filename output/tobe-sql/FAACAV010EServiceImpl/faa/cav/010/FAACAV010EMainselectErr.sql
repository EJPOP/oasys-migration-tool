    CASE WHEN BLL_KDCD = '28' AND SUM_SE_ID ='49' THEN '합계' ELSE ERR_SBJCT_NM END AS ERR_SBJCT_NM /* 오류과목명 */,
    DISAG_CD /* 불부합코드 */,
