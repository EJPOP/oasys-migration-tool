SELECT D1.QSTNAI_YR AS QSTNAI_YR /* 연도 */,
    D1.QSTNAI_NO AS QSTNAI_NO /* 질의서번호 */,
    D1.QSTNAI_YR || '-' || D1.QSTNAI_NO AS QSTNAI_YR /* 질의서연번호 */,
    D1.QSTNAI_TIT_NM AS QSTNAI_TIT_NM /* 질의서제목 */,
    D1.CMT_CD AS CMT_CD /* 위원회명 */,
    D1.RQT_YMD AS RQT_YMD /* 요구일자 */,
    D1.SMT_RQ_DT AS SMT_RQ_DT /* 제출일자 */,
    D1.SRECS_SRNG_MBCMT_NM AS SRECS_SRNG_MBCMT_NM /* 심사재심의심사위원명 */,
    F_CODE_NM ('1000374',D1.NTAS_RQ_MTRL_HNDL_STA_CD) AS F_CODE_NM /* 처리상태코드 */
FROM TBCSPFPL001M D1 where 1=1 D1.QSTNAI_YR = #strYear# D1.QSTNAI_NO = #strQstnai# D1.QSTNAI_TIT_NM LIKE '%'||#strQstnaiTitleNm#||'%' D1.SRECS_SRNG_MBCMT_NM = #SRECS_SRNG_MBCMT_NM# D1.RQT_YMD >= #strRqDtFrom# D1.RQT_YMD &lt;= #strRqDtTo# D1.SMT_RQ_DT >= #strSmtRqDtFrom# D1.SMT_RQ_DT &lt;= #strSmtRqDtTo# D1.SRECS_SRNG_MBCMT_NM = #SRECS_SRNG_MBCMT_NM# ORDER BY D1.QSTNAI_NO ASC