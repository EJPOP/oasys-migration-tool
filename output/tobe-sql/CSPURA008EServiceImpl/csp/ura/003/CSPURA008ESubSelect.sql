SELECT B.OTSD_EXPRT_SECD /* 외부전문가구분코드 */,
    B.CNSTN_MBCMT_REG_YR /* 자문위원등록년도 */,
    B.CNSTN_MBCMT_SQNO /* 자문위원순번 */,
    B.OTSD_EXPRT_SE_SQNO /* 외부전문가구분순번 */
FROM TB_CSPURA005M A, TB_CSPURA006D B WHERE 1=1 AND A.CNSTN_MBCMT_REG_YR = B.CNSTN_MBCMT_REG_YR AND A.CNSTN_MBCMT_SQNO = B.CNSTN_MBCMT_SQNO AND A.CNSTN_MBCMT_REG_YR = #CNSTN_MBCMT_REG_YR# AND A.CNSTN_MBCMT_SQNO = #CNSTN_MBCMT_SQNO# ORDER BY B.CNSTN_MBCMT_REG_YR DESC, TO_NUMBER(B.CNSTN_MBCMT_SQNO) DESC, OTSD_EXPRT_SECD DESC