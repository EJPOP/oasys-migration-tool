    D1.HLYR_DVSN_CD AS HLYR_DVSN_CD /* 반기구분코드 */,
    D1.PTCPT_CNB /* 피평가자 전산번호 */,
    '10001' AS CVLCPT_TASK_SECD /* 민원업무구분코드 */,
    D1.TASK_KDCD /* 업무종류코드 */,
    D1.TSK_NO AS TSK_NO /* 업무번호 */,
    #EVL_CMTR_CNB# AS #EVL_CMTR_CNB# /* 평가자전산번호 */,
