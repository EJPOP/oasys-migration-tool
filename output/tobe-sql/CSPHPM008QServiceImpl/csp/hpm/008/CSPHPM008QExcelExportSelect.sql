    /*자료구분코드*/ TO_CHAR(TO_DATE(A.REG_YMD),'YYYY-MM-DD') AS REG_YMD /* 등록일자 */,
    /*등록일*/ A.PBC_YR,
    /*발행연도*/ A.PBC_PLAC,
