SELECT '(' || SUBSTR(A.EXM_DT, 1, 4) || CASE WHEN LENGTH(A.EXM_DT) > 4 THEN '.' || SUBSTR(A.EXM_DT, 5, 2) ELSE '' END || CASE WHEN LENGTH(A.EXM_DT) > 6 THEN '.' || SUBSTR(A.EXM_DT, 7, 2) ELSE '' END || ')' AS SUBSTR /* 시험일 */,
    A.EXM_NM /* 시험명 */,
    A.APM_APT_RANK_NM /* 임용예정직급명 */,
    CASE WHEN A.ENF_ORG_NM IS NOT NULL THEN '[시행청:' || A.ENF_ORG_NM || ']' END AS ENF_ORG_NM /* 시행기관명 */
FROM TBCSPBII340L A WHERE A.CRU_RRNO_SCRTY = #CRU_RRNO_SCRTY# ORDER BY A.EXM_DT