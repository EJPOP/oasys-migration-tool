SELECT '(' || SUBSTR(A.OFOD_DT, 1, 4) || CASE WHEN LENGTH(A.OFOD_DT) > 4 THEN '.' || SUBSTR(A.OFOD_DT, 5, 2) ELSE '' END || CASE WHEN LENGTH(A.OFOD_DT) > 6 THEN '.' || SUBSTR(A.OFOD_DT, 7, 2) ELSE '' END || ')' AS SUBSTR /* 발령일 */,
    A.APM_DVSN_NM /* 임용구분명 */,
    CASE WHEN A.APM_DPT_NM IS NOT NULL THEN '(' || A.APM_DPT_NM || ')' END AS APM_DPT_NM /* 임용부서명 */,
    A.JBGD_NM /* 직급명 */,
    CASE WHEN A.JBPS_NM IS NOT NULL THEN '(' || A.JBPS_NM || ')' END AS JBPS_NM /* 직위명 */,
    CASE WHEN A.OFOD_OFI_NM IS NOT NULL THEN '[발령청:' || A.OFOD_OFI_NM || ']' END AS OFOD_OFI_NM /* 발령청명 */
FROM TBCSPBII341L A WHERE A.CRU_RRNO_SCRTY = #CRU_RRNO_SCRTY# ORDER BY A.OFOD_DT