SELECT A.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    A.APL_DT,
    A.BSC_SRNO,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    C.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    C.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    B.USER_NM /* 사용자명 */,
    A.USE_STR_DT,
    TO_CHAR(TO_DATE(A.USE_STR_DT, 'YYYYMMDD')+1, 'YYYYMMDD') AS TO_CHAR,
    TO_CHAR(ADD_MONTHS(TO_DATE(A.USE_STR_DT), 3), 'YYYYMMDD') AS TO_CHAR,
    '' AS LTD_RSN_TXT
FROM TBCSPSAE501D A , TBDCMACM001M B , TBDCMACM002M C , TBCSPSAE132B D WHERE B.TRGT_NOP_ENO = A.TRGT_NOP_ENO AND B.SRECS_DEPT_CD = C.SRECS_DEPT_CD AND A.USE_YN = 'N' AND A.COFM_STA_CD IN ('30','50') AND A.USE_STR_DT BETWEEN #strFromDt# AND #strToDt# ORDER BY A.USE_STR_DT, C.CNSTN_MBCMT_DEPT_NM, B.USER_NM