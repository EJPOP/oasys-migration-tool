SELECT A.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    A.APL_DT,
    A.BSC_SRNO,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    C.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    C.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    B.USER_NM /* 사용자명 */,
    A.UN_USE_DT,
    A.LTD_STR_DT,
    A.LTD_END_DT,
    A.LTD_RSN_TXT
FROM TBCSPSAE502D A , TBDCMACM001M B , TBDCMACM002M C WHERE B.TRGT_NOP_ENO = A.TRGT_NOP_ENO AND B.SRECS_DEPT_CD = C.SRECS_DEPT_CD AND A.UN_USE_DT BETWEEN #strFromDt# AND #strToDt# AND B.USER_NM LIKE '%' || #strUSR_NAM# || '%' AND C.SRECS_DEPT_CD = #SRECS_DEPT_CD# ORDER BY A.UN_USE_DT, C.CNSTN_MBCMT_DEPT_NM, B.USER_NM