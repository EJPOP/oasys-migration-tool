SELECT '1040000' AS SAL_DPT_CD,
    B.USER_NM /* 사용자명 */,
    B.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    A.LON_DDCT_DVSN_CD,
    '0' AS GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    #yyyymm# AS #YYYYMM#,
    'AA' AS SAL_DVSN_CD,
    SUM(A.MN_STT_AM) AS MN_STT_AM,
    '' AS DDCT_MM_DVSN_CD,
    '' AS BANK_CD,
    '' AS ACCOUNT,
    #yyyymm# AS #YYYYMM#,
    '' AS ETC,
    '' AS MDFCN_ID /* 수정아이디 */,
    '' AS DMF_DH
FROM TBCSPLFI001M A, TBDCMACM001M B, TBDCMACM002M C WHERE 1=1 AND A.LON_PEN_CNB = B.TRGT_NOP_ENO AND B.SRECS_DEPT_CD = C.SRECS_DEPT_CD AND A.STR_YM = #yyyymm# AND A.LON_DDCT_DVSN_CD = #lonDdctDvsnCd# AND A.LON_PEN_CNB = #cnb# AND B.USER_NM LIKE '%' || #name# || '%' GROUP BY B.USER_NM, B.CRU_RRNO_SCRTY, A.LON_DDCT_DVSN_CD ORDER BY B.CRU_RRNO_SCRTY, A.LON_DDCT_DVSN_CD