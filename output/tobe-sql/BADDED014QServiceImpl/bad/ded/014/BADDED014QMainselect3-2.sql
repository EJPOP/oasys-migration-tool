SELECT TE.RGN_SECD /* 지역구분코드 */,
    F_CODE_NM('1000023',TE.RGN_SECD) AS F_CODE_NM,
    SUM(TE.ADT_DCN) AS ADT_DCN /* 감사일수 */,
    SUM(NVL(TE.LEAD_CST, 0)) AS LEAD_CST /* 지휘비용 */,
    SUM(NVL(TE.ADT_ACTV_ACTC_AMT, 0)) AS ADT_ACTV_ACTC_AMT /* 감사활동수용금액 */,
    SUM(NVL(TE.SPM_CST, 0)) AS SPM_CST /* 보충비용 */,
    SUM(NVL(TE.LEAD_CST, 0)) + SUM(NVL(TE.SPM_CST, 0)) + SUM(NVL(TE.ADT_ACTV_ACTC_AMT, 0)) AS LEAD_CST /* 지휘비용 */
FROM TB_BADDED011L TE WHERE TE.BZTRP_YR = #BZTRP_YR# AND TE.BZTRP_SQNO = #BZTRP_SQNO# AND TE.CAL_YN = 'N' GROUP BY TE.RGN_SECD ORDER BY 1,2