SELECT M.FT_SQNO /* 출장조순번 */,
    MIN(M.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(M.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    SUM(M.ADT_DCN) AS ADT_DCN /* 감사일수 */,
    MIN(M.BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(M.BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    SUM(M.BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */
FROM (SELECT PTD.BZTRP_YR /* 출장년도 */,
    PTD.BZTRP_SQNO /* 출장순번 */,
    PTD.FT_SQNO /* 출장조순번 */,
    PTD.FT_TRGT_INST_SQNO /* 출장조대상기관순번 */,
    MIN(PTD.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(PTD.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(PTD.ADT_DCN) AS ADT_DCN /* 감사일수 */,
    MIN(PTD.BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(PTD.BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    MAX(PTD.BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */
FROM TBBADDED006L PTD, TBBADDED002M SITU WHERE PTD.BZTRP_YR = SITU.BZTRP_YR AND PTD.BZTRP_SQNO = SITU.BZTRP_SQNO AND SITU.BZTRP_SECD ='1' AND SITU.REL_ADT_YR = #REL_ADT_YR# AND SITU.REL_ADT_NO = #REL_ADT_NO# AND SITU.CVLCPT_ADT_STP_SQNO = #CVLCPT_ADT_STP_SQNO# AND PTD.FT_SQNO = #FT_SQNO# GROUP BY PTD.BZTRP_YR, PTD.BZTRP_SQNO, PTD.FT_SQNO, PTD.FT_TRGT_INST_SQNO ) M GROUP BY M.FT_SQNO