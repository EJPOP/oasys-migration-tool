SELECT TER.BZTRP_YR /* 출장년도 */,
    TER.BZTRP_SQNO /* 출장순번 */,
    TER.BZTRVR_ENO /* 출장자직원전산번호 */,
    /*전산번호*/ TER.BZTRVR_NM AS BZTRVR_NM /* 출장자명 */,
    /*성명*/ F_CODE_NM('1000173',TER.BZTRVR_JBGD_SECD) AS F_CODE_NM,
    /*직급*/ DEPT.ABR_DPT_NM_1 AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    /*소속*/ TE.SPM_CST AS SPM_CST /* 보충비용 */,
    /*보충경비*/ ( CASE WHEN F_LEAD_EXS_RCV_EXS_RCIT_CNB(TER.BZTRP_YR,TER.BZTRP_SQNO) = TER.BZTRVR_ENO THEN TER2.LEAD_CST ELSE 0 END) AS LEAD_CST /* 지휘비용 */,
    /*지휘비*/ ( CASE WHEN F_LEAD_EXS_RCV_EXS_RCIT_CNB(TER.BZTRP_YR,TER.BZTRP_SQNO) = TER.BZTRVR_ENO THEN TER2.ADT_ACTV_ACTC_AMT ELSE 0 END) AS ADT_ACTV_ACTC_AMT /* 감사활동수용금액 */,
    /*감사활동수용비*/ TE.SPM_CST+ ( CASE WHEN F_LEAD_EXS_RCV_EXS_RCIT_CNB(TER.BZTRP_YR,TER.BZTRP_SQNO) = TER.BZTRVR_ENO THEN TER2.LEAD_CST + TER2.ADT_ACTV_ACTC_AMT ELSE 0 END) AS SPM_CST /* 보충비용 */,
    /*지급액*/ TE.BZTRP_DCN AS BZTRP_DCN /* 출장일수 */,
    TE.BZTRP_BGNG_YMD /* 출장시작일자 */,
    /*출장기간 FROM */ TE.BZTRP_END_YMD AS BZTRP_END_YMD /* 출장종료일자 */,
    /*출장기간 TO*/ '' AS RECIPIENT /* 영수인 */
FROM TB_BADDED010L TER, TB_BADDED002M SITU, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    BZTRVR_ENO /* 출장자직원전산번호 */,
    SUM(TE0.SPM_CST) AS SPM_CST /* 보충비용 */,
    SUM(TE0.BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */,
    MIN(TE0.BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(TE0.BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */
FROM TBBADDED011L TE0 WHERE BZTRP_YR = #BZTRP_YR# AND BZTRP_SQNO = #BZTRP_SQNO# AND CAL_YN= 'N' GROUP BY BZTRP_YR, BZTRP_SQNO, BZTRVR_ENO) TE, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    SUM(NVL(LEAD_CST, 0)) AS LEAD_CST /* 지휘비용 */,
    SUM(NVL(ADT_ACTV_ACTC_AMT, 0)) AS ADT_ACTV_ACTC_AMT /* 감사활동수용금액 */
FROM TBBADDED011L WHERE BZTRP_YR = #BZTRP_YR# AND BZTRP_SQNO = #BZTRP_SQNO# AND CAL_YN= 'N' GROUP BY BZTRP_YR, BZTRP_SQNO) TER2, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    BZTRVR_ENO /* 출장자직원전산번호 */,
    SUM(RL_FARE_AMT) AS RL_FARE_AMT /* 철도운임금액 */
FROM TBBADDED017L WHERE BZTRP_YR = #BZTRP_YR# AND BZTRP_SQNO = #BZTRP_SQNO# AND CAL_YN= 'N' GROUP BY BZTRP_YR, BZTRP_SQNO, BZTRVR_ENO ) TFD, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    BZTRVR_ENO /* 출장자직원전산번호 */,
    MAX(OGDP_DEPT_CD) AS OGDP_DEPT_CD /* 소속부서코드 */
FROM TBBADDED005L WHERE BZTRP_YR = #BZTRP_YR# AND BZTRP_SQNO = #BZTRP_SQNO# GROUP BY BZTRP_YR, BZTRP_SQNO, BZTRVR_ENO) DBO_BLL_BLNC, TBDCMACM002M DEPT WHERE TER.BZTRP_YR = #BZTRP_YR# AND TER.BZTRP_SQNO = #BZTRP_SQNO# AND TE.BZTRP_YR(+) = TER.BZTRP_YR AND TE.BZTRP_SQNO(+) = TER.BZTRP_SQNO AND TE.BZTRVR_ENO(+) = TER.BZTRVR_ENO AND TFD.BZTRP_YR(+) = TER.BZTRP_YR AND TFD.BZTRP_SQNO(+) = TER.BZTRP_SQNO AND TFD.BZTRVR_ENO(+) = TER.BZTRVR_ENO AND TER2.BZTRP_YR = TER.BZTRP_YR AND TER2.BZTRP_SQNO = TER.BZTRP_SQNO AND SITU.BZTRP_YR = TER.BZTRP_YR AND SITU.BZTRP_SQNO = TER.BZTRP_SQNO AND BL.BZTRP_YR(+) = TER.BZTRP_YR AND BL.BZTRP_SQNO(+) = TER.BZTRP_SQNO AND BL.BZTRVR_ENO(+) = TER.BZTRVR_ENO AND DEPT.SRECS_DEPT_CD(+) = BL.OGDP_DEPT_CD