SELECT SITU.REL_ADT_YR /* 관계감사년도 */,
    SITU.REL_ADT_NO /* 관계감사번호 */,
    SITU.ADT_STP_SQNO /* 감사단계순번 */,
    CASE WHEN AUD.ADT_YR >= '2016' THEN F_CODE_NM('1000739',SUBSTR(AUD.ADT_KDCD,3,1)) ELSE '-' END AS ADT_YR /* 감사년도 */,
    F_CODE_NM('1000007',AUD.ADT_KDCD) AS F_CODE_NM
FROM TB_BADDED001M AUD, TB_BADDED002M SITU WHERE SITU.BZTRP_YR = #BZTRP_YR# AND SITU.BZTRP_SQNO = #BZTRP_SQNO# AND AUD.ADT_YR = SITU.REL_ADT_YR AND AUD.ADT_NO = SITU.REL_ADT_NO