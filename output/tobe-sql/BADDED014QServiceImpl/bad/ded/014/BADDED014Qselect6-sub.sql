SELECT TL.FT_SQNO /* 출장조순번 */,
    TL.BGNG_DPPL_SECD /* 시작출발지구분코드 */,
    NVL(TO_CHAR(TL.BZTRVR_ALTMNT_SQNO),'N') AS BZTRVR_ALTMNT_SQNO /* 출장자배정순번 */
FROM TB_BADDED009L TL WHERE TL.BZTRP_YR = #BZTRP_YR# AND TL.BZTRP_SQNO = #BZTRP_SQNO# GROUP BY TL.BZTRP_YR, TL.BZTRP_SQNO, TL.FT_SQNO, TL.BGNG_DPPL_SECD, TL.BZTRVR_ALTMNT_SQNO ORDER BY TL.FT_SQNO, TL.BGNG_DPPL_SECD, TL.BZTRVR_ALTMNT_SQNO