SELECT DISTINCT A.ACMP_SNO,
    F_MNG_KND_AUDYRNO(A.CVLCPT_ADT_YR, A.ADT_NO, C.ADT_KDCD, '-') ||'-'|| A.DSPS_RQT_SQNO AS F_MNG_KND_AUDYRNO,
    A.DSPS_RQT_ENFC_YMD AS DSPS_RQT_ENFC_YMD /* 처분요구시행일자 */,
    B.DSPS_RQT_KDCD /* 처분요구종류코드 */,
    F_CODE_NM('1000002', B.DSPS_RQT_KDCD) AS DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */,
    B.DSPS_RQT_NM /* 처분요구명 */,
    A.CVLCPT_ADT_YR /* 민원감사년도 */,
    A.ADT_NO /* 감사번호 */,
    A.ADT_MTTR_NM /* 감사사항명 */,
    B.DSPS_RQT_SQNO /* 처분요구순번 */,
    A.CVLCPT_ADT_YR /* 민원감사년도 */,
    A.ADT_NO /* 감사번호 */,
    B.DSPS_RQT_SQNO AS DSPS_RQT_SQNO /* 처분요구순번 */
FROM TBCSPIIT022M A /* BARON활용 처분요구 */ , TB_BADEAR001M B /* 처분요구사항기본 */ , TB_BADDED001M C , TBCSPIIT021M D WHERE A.CVLCPT_ADT_YR = B.ADT_YR AND A.ADT_NO = B.ADT_NO AND A.DSPS_RQT_SQNO = B.DSPS_RQT_SQNO AND B.ADT_YR = C.ADT_YR AND B.ADT_NO = C.ADT_NO AND A.ACMP_SNO = D.ACMP_SNO AND A.ACMP_SNO = #strACMP_SNO# AND D.ATRZ_DOC_NO = #ATRZ_DOC_NO#