SELECT A.BGNG_YMD /* 시작일자 */,
    A.END_YMD /* 종료일자 */,
    A.JUCHA,
    A.RES_STR_DT
FROM ( SELECT TO_CHAR(WEEK_START, 'YYYYMMDD') AS BGNG_YMD /* 시작일자 */,
    TO_CHAR(WEEK_END, 'YYYYMMDD') AS END_YMD /* 종료일자 */,
    TO_CHAR(TO_DATE(WEEK_END)-1, 'W') AS TO_CHAR,
    TO_CHAR(NEXT_DAY((LAST_DAY((TRUNC(TO_DATE(WEEK_END)-1, 'mm')-1))-7),'월'), 'YYYYMMDD') AS TO_CHAR
FROM ( SELECT TRUNC (START_DT + LEVEL, 'D') + 1 AS TRUNC,
    TRUNC (START_DT + LEVEL, 'D') + 7 AS TRUNC
FROM ( SELECT TO_DATE(#strYr# || #strM# || '01') - 1 AS TO_DATE,
    ADD_MONTHS(#strYr# || #strM# || '01', 1) AS END_YMD /* 종료일자 */
FROM DUAL ) CONNECT BY LEVEL <= END_YMD - START_DT ) GROUP BY WEEK_START, WEEK_END ORDER BY WEEK_START ) A WHERE TO_CHAR(TO_DATE(A.END_YMD)-1, 'MM') = #strM#