FROM TB_BADEAR001M MASTER ,TB_BADEAR002D DETAIL ,TB_BADDED001M AUD ,TBDCMACM002M DEPT WHERE 1=1 AND MASTER.ADT_YR = DETAIL.ADT_YR AND MASTER.ADT_NO = DETAIL.ADT_NO AND MASTER.DSPS_RQT_SQNO = DETAIL.DSPS_RQT_SQNO AND MASTER.ADT_YR = AUD.ADT_YR AND MASTER.ADT_NO = AUD.ADT_NO AND DETAIL.ENFC_DEPT_CD = DEPT.SRECS_DEPT_CD AND DETAIL.ADT_YR = #CVLCPT_ADT_YR# /*인자값1*/ AND DETAIL.ADT_NO = #ADT_NO# /*인자값2*/ AND DETAIL.DSPS_RQT_SQNO = #DSPS_RQT_SQNO# /*인자값3*/