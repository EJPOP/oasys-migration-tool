    MASTER.ADT_NO AS ADT_NO /* 감사번호 */,
    MASTER.DSPS_RQT_SQNO AS DSPS_RQT_SQNO /* 처분요구순번 */,
    MASTER.ADT_YR||'-'||MASTER.ADT_NO AS ADT_YR /* 감사년도 */,
    MASTER.ADT_YR||'-'||MASTER.ADT_NO||'-'||MASTER.DSPS_RQT_SQNO AS ADT_YR /* 감사년도 */,
    DETAIL.ENFC_YMD AS ENFC_YMD /* 시행일자 */,
