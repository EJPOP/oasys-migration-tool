SELECT DSPS_RQT_ACNTG_SECD /* 처분요구회계구분코드 */,
    DSPS_RQT_SQNO /* 처분요구순번 */,
    F_CODE_NM('1000004', DSPS_RQT_ACNTG_SECD) AS F_CODE_NM /* 회계명 */,
    SUM(PNRT_PRSRV_AMT) AS PNRT_PRSRV_AMT /* 추징회수보전금액 */,
    NVL(SUM(RMBR_AMT), 0) + NVL(SUM(SPLP_AMT), 0) AS RMBR_AMT /* 환급금액 */,
    NVL(SUM(EXPT_EFCT_AMT) ,0) AS EXPT_EFCT_AMT /* 기대효과금액 */,
    NVL(SUM(EPFC_EFCT_AMT) ,0) AS EPFC_EFCT_AMT /* 사후효과금액 */,
    NVL(SUM(CASE WHEN EXPT_EFCT_SECD ='1' THEN EXPT_EFCT_AMT ELSE 0 END),0) AS BGT_RTRC_AMT /* 예산절감금액 */,
    NVL(SUM(CASE WHEN EXPT_EFCT_SECD ='2' THEN EXPT_EFCT_AMT ELSE 0 END),0) AS INCM_INCRS_AMT /* 수입증가금액 */,
    NVL(SUM(CASE WHEN EXPT_EFCT_SECD ='3' THEN EXPT_EFCT_AMT ELSE 0 END),0) AS PPL_BRDN_RDT_AMT /* 국민부담경감금액 */,
    NVL(SUM(CASE WHEN EXPT_EFCT_SECD ='4' THEN EXPT_EFCT_AMT ELSE 0 END),0) AS PRPT_INCRS_AMT /* 재산증가금액 */,
    NVL(SUM(CASE WHEN EXPT_EFCT_SECD ='5' THEN EXPT_EFCT_AMT ELSE 0 END),0) AS EXPND_RATI_AMT /* 지출합리화금액 */
FROM TB_BADEAR003M WHERE 1=1 AND CVLCPT_ADT_YR = #CVLCPT_ADT_YR# /*인자값 1*/ AND ADT_NO = #ADT_NO# /*인자값 2*/ AND DSPS_RQT_SQNO = #DSPS_RQT_SQNO# /*인자값 3*/ GROUP BY DSPS_RQT_ACNTG_SECD, DSPS_RQT_SQNO;