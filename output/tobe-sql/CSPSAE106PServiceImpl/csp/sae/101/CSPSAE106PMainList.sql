FROM TBCSPSAE101D E WHERE E.LINK_YR = D.LINK_YR AND E.CRS_CDN = D.CRS_CDN AND E.CRS_CD = D.CRS_CD) ORDER BY D.EDU_BGNG_YMD, A.EDU_DVSN_CD, B.EDU_CAT_CD, C.CRS_CD, D.CRS_CDN