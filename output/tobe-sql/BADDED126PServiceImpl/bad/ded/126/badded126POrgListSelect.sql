SELECT T1.BZTRP_YR AS BZTRP_YR /* 출장년도 */,
    T1.BZTRP_SQNO AS BZTRP_SQNO /* 출장순번 */,
    T1.FT_SQNO AS FT_SQNO /* 출장조순번 */,
    T1.FT_TRGT_INST_SQNO AS FT_TRGT_INST_SQNO /* 출장조대상기관순번 */,
    F_ORG_INFO(T1.INST_CD,'1') || '(' || T1.INST_CD || ')' AS F_ORG_INFO,
    F_CODE_NM('1000001',T1.RGN_CD) || '(' || T1.RGN_CD || ')' AS F_CODE_NM,
    T1.ADT_BGNG_YMD AS ADT_BGNG_YMD /* 감사시작일자 */,
    T1.ADT_END_YMD AS ADT_END_YMD /* 감사종료일자 */,
    PSON.AUD_DAYS AS AUD_DAYS /* 일수 */,
    PSON.PSON_CNT AS PSON_CNT /* 인원수 */,
    PSON.MAN_DAY AS MAN_DAY /* 연인원 */
FROM TB_BADDED008L T1, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    FT_SQNO /* 출장조순번 */,
    FT_TRGT_INST_SQNO /* 출장조대상기관순번 */,
    MAX(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    COUNT(*) AS PSON_CNT,
    SUM(ADT_DCN) AS ADT_DCN /* 감사일수 */
FROM TBBADDED006L WHERE BZTRP_YR = #BZTRP_YR# AND BZTRP_SQNO = #BZTRP_SQNO# GROUP BY BZTRP_YR ,BZTRP_SQNO ,FT_SQNO ,FT_TRGT_INST_SQNO) PSON WHERE T1.BZTRP_YR = #BZTRP_YR# AND T1.BZTRP_SQNO = #BZTRP_SQNO# AND PSON.BZTRP_YR = T1.BZTRP_YR AND PSON.BZTRP_SQNO = T1.BZTRP_SQNO AND PSON.FT_SQNO = T1.FT_SQNO AND PSON.FT_TRGT_INST_SQNO = T1.FT_TRGT_INST_SQNO ORDER BY T1.FT_SQNO ,T1.FT_TRGT_INST_SQNO