/*쿼리명 : SQL_1*/ select SITU.BZTRP_YR /* 출장년도 */,
    /*출장년도*/ SITU.BZTRP_SQNO AS BZTRP_SQNO /* 출장순번 */,
    /*출장순번*/ SITU.BZTRP_YR||'-'||SITU.SPRT_NO AS SPRT_NO /* 지원번호 */,
    /*지원번호*/ F_CODE_NM('1000173',SITU.BZTRP_TL_JBGD_SECD) AS F_CODE_NM,
    /*감사반장 직급*/ F_USR_INFO(SITU.BZTRP_TL_ENO,'2') AS F_USR_INFO,
    /*감사반장 이름*/ F_CODE_NM('1000021',SITU.GNRL_BZTRP_KDCD) AS F_CODE_NM,
    /*일반출장종류명*/ SITU.BZTRP_NM AS BZTRP_NM /* 출장명 */,
    /*출장제목*/ PSON.BZTRP_BGNG_YMD AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    /*출장시작일*/ PSON.BZTRP_END_YMD AS BZTRP_END_YMD /* 출장종료일자 */,
    /*출장종료일*/ PSON.BZTRP_DCN AS BZTRP_DCN /* 출장일수 */,
    /*출장일수*/ pson.PSON_CNT,
    /*출장인원수*/ F_DPT_INFO(SITU.BZTRP_DEPT_CD,'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */
from TB_BADDED002M situ, (select BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    min(BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    max(BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    max(BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */,
    count(*) AS PSON_CNT
from (select TMA.BZTRP_YR /* 출장년도 */,
    TMA.BZTRP_SQNO /* 출장순번 */,
    TMA.BZTRVR_ENO /* 출장자직원전산번호 */,
    min(PTD.BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    max(PTD.BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    sum(PTD.BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */
from TBBADDED005L tma, TBBADDED006L ptd where PTD.BZTRP_YR = TMA.BZTRP_YR and PTD.BZTRP_SQNO = TMA.BZTRP_SQNO and PTD.FT_SQNO = TMA.FT_SQNO and PTD.BZTRVR_ALTMNT_SQNO = TMA.BZTRVR_ALTMNT_SQNO and PTD.BZTRP_YR = #BZTRP_YR# and PTD.BZTRP_SQNO = #BZTRP_SQNO# group by TMA.BZTRP_YR, TMA.BZTRP_SQNO, TMA.BZTRVR_ENO) ptd1 group by BZTRP_YR, BZTRP_SQNO ) pson where SITU.BZTRP_YR = #BZTRP_YR# and SITU.BZTRP_SQNO = #BZTRP_SQNO# and PSON.BZTRP_YR = SITU.BZTRP_YR and PSON.BZTRP_SQNO = SITU.BZTRP_SQNO