SELECT REGI_CHG_RQS_SNO,
    RQR_NM,
    PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    RQS_ORG_NM,
    DMND_YMD /* 요청일자 */,
    GPCD_PRCS_YMD /* 처리일자 */,
    RQS_DVSN_CD,
    F_CODE_NM('1000304', RQS_DVSN_CD) AS F_CODE_NM,
    PRCS_SECD /* 처리구분코드 */
FROM TBCSPIIT003M WHERE 1=1 AND DMND_YMD BETWEEN #strFrom# AND #strTo# AND PRCS_SECD = #PRCS_SECD# AND (PROF_RBPRSN_INST_NM LIKE '%'||#strRqNm#||'%' OR RQR_NM LIKE '%'||#strRqNm#||'%') ORDER BY REGI_CHG_RQS_SNO DESC