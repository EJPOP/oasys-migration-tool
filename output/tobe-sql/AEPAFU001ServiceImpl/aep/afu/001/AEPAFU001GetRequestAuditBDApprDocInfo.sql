SELECT A.SRECS_SORT_SQNO /* 심사재심의정렬순번 */,
    NVL(C.SRECS_RCPT_STP_NM,'') AS ADT_STP_NM /* 감사단계명 */,
    B.SRECS_RPTP_NM /* 심사재심의보고서명 */,
    B.SRECS_RPTP_KDCD /* 심사재심의보고서종류코드 */,
    A.SRECS_ATRZ_RQSTR_ID /* 심사재심의결재요청자아이디 */,
    A.SRECS_RCPT_YR AS SRECS_RCPT_YR /* 심사재심의접수년도 */,
    A.SRECS_RCPT_SQNO AS SRECS_RCPT_SQNO /* 심사재심의접수순번 */,
    A.SRECS_RCPT_STP_SECD AS SRECS_RCPT_STP_SECD /* 심사재심의접수단계구분코드 */,
    A.SRECS_DOC_ID /* 심사재심의문서아이디 */,
    A.SRECS_ATRZ_SECD /* 심사재심의결재구분코드 */,
    A.SRECS_ATRZ_DOC_SECD /* 심사재심의결재문서구분코드 */,
    A.SRECS_RCPT_SECD AS SRECS_RCPT_SECD /* 심사재심의접수구분코드 */,
    F_OPEN_EDIT(B.SRECS_LNKG_URL_ADDR) AS F_OPEN_EDIT
FROM TB_BADFRE109M A INNER JOIN TB_BADFRE103M B ON A.SRECS_RCPT_YR = B.SRECS_RCPT_YR AND A.SRECS_RCPT_SECD = B.SRECS_RCPT_SECD AND A.SRECS_RCPT_SQNO = B.SRECS_RCPT_SQNO AND A.SRECS_RCPT_STP_SECD = B.SRECS_RCPT_STP_SECD AND A.SRECS_DOC_ID = B.SRECS_DOC_ID LEFT JOIN TB_BADFRE101M C ON A.SRECS_RCPT_YR = C.SRECS_RCPT_YR AND A.SRECS_RCPT_SECD = C.SRECS_RCPT_SECD AND A.SRECS_RCPT_SQNO = C.SRECS_RCPT_SQNO AND A.SRECS_RCPT_STP_SECD = C.SRECS_RCPT_STP_SECD WHERE A.SRECS_ATRZ_RQSTR_ID = #ATRZ_DMND_ID# AND A.SRECS_ATRZ_DOC_SECD = #CRU_ATRZ_DOC_SECD# ORDER BY A.SRECS_SORT_SQNO