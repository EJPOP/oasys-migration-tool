    T1.SRECS_RCPT_SECD /* 심사재심의접수구분코드 */,
    T1.SRECS_RCPT_SQNO /* 심사재심의접수순번 */,
    T1.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    CASE WHEN T1.SRECS_RCPT_YR >= '2016' THEN T1.SRECS_RCPT_YR ||'-'|| DECODE(T1.SRECS_RCPT_SECD,'1','심사','2','재심','3','재심판정') ||'-'|| T1.SRECS_RCPT_SQNO ELSE T1.SRECS_RCPT_YR ||'-'|| T1.SRECS_RCPT_SECD ||'-'|| T1.SRECS_RCPT_SQNO END AS NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    T1.SRECS_RCPT_YMD /* 심사재심의접수일자 */,
    T1.SRECS_PRCS_TERM_YMD /* 심사재심의처리기한일자 */,
    T1.SRECS_CLMT_NM /* 심사재심의청구인명 */,
    T1.SRECS_NM /* 심사재심의명 */,
    T2.DSPS_RQT_NM /* 처분요구명 */,
    T1.SRECS_PRCS_PIC_NM /* 심사재심의처리담당자명 */
FROM TB_BADFRE012M T1 , TB_BADEAR001M T2 WHERE 1=1 AND T1.SRECS_ADT_YR = T2.ADT_YR(+) AND T1.SRECS_ADT_NO = T2.ADT_NO(+) AND T1.SRECS_DSPS_RQT_SQNO = T2.DSPS_RQT_SQNO(+) AND T1.SRECS_MERG_RCPT_YR = #SRECS_MERG_RCPT_YR# AND T1.SRECS_MERG_RCPT_SECD = #SRECS_MERG_RCPT_SECD# AND T1.SRECS_MERG_RCPT_SQNO = #SRECS_MERG_RCPT_SQNO# AND T1.SRECS_MERG_DEPT_CD = #SRECS_MERG_DEPT_CD# ORDER BY T1.SRECS_RCPT_YR DESC, T1.SRECS_RCPT_SQNO DESC