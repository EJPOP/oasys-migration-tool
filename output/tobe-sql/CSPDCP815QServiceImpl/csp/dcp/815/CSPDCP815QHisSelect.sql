SELECT T1.CVLCPT_DCLR_SECD /* 민원신고구분코드 */,
    T1.CVLCPT_RCPT_YR /* 민원접수년도 */,
    T1.CVLCPT_DCLR_RCPT_NO /* 민원신고접수번호 */,
    T2.CVLCPT_SQNO /* 민원순번 */,
    DECODE(T1.SLCT_ACTL_RCPT_NO,NULL,'',T1.CVLCPT_RCPT_YR||'-청금-'||T1.SLCT_ACTL_RCPT_NO) AS NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    F_DPT_INFO(T2.SLCT_HNDT_DEPT_CD,'1') AS F_DPT_INFO,
    F_USR_INFO_BY_ID(T2.SLCT_HNDT_PIC_ID,'2') AS F_USR_INFO_BY_ID,
    F_DPT_INFO(T2.SLCT_PRCS_DEPT_CD,'1') AS PRCS_DEPT_NM /* 처리부서명 */,
    F_USR_INFO_BY_ID(T2.SLCT_PRCS_PIC_ID,'2') AS SRECS_PRCS_PIC_NM /* 심사재심의처리담당자명 */,
    T2.SLCT_PRCS_CN /* 청탁처리내용 */,
    TO_CHAR(T2.FRST_REG_DT,'YYYY-MM-DD HH24:MI') AS FRST_REG_DT /* 최초등록일시 */
FROM TB_CSPDCP811M T1 ,TB_CSPDCP814H T2 WHERE T1.CVLCPT_DCLR_SECD = #CVLCPT_DCLR_SECD# AND T1.CVLCPT_RCPT_YR = #CVLCPT_RCPT_YR# AND T1.CVLCPT_DCLR_RCPT_NO = #CVLCPT_DCLR_RCPT_NO# AND T1.CVLCPT_DCLR_SECD = T2.CVLCPT_DCLR_SECD AND T1.CVLCPT_RCPT_YR = T2.CVLCPT_RCPT_YR AND T1.CVLCPT_DCLR_RCPT_NO = T2.CVLCPT_DCLR_RCPT_NO ORDER BY FRS_MDM_SQNO DESC