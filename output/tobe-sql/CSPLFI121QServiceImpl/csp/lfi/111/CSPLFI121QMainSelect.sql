SELECT C.CNSTN_MBCMT_DEPT_NM AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    A.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    B.USER_NM /* 사용자명 */,
    NVL(SUM(A.SALE_TAM),0) AS SALE_TAM,
    A.DDCT_YM
FROM TBCSPLFI002L A, TBDCMACM001M B, TBDCMACM002M C WHERE A.TRGT_NOP_ENO = B.TRGT_NOP_ENO AND B.SRECS_DEPT_CD = C.SRECS_DEPT_CD AND A.DDCT_YM = #deduction_date# AND A.TRGT_NOP_ENO = #TRGT_NOP_ENO# AND B.USER_NM LIKE '%'||#name#||'%' GROUP BY C.CNSTN_MBCMT_DEPT_NM, A.TRGT_NOP_ENO, B.USER_NM, A.DDCT_YM ORDER BY A.TRGT_NOP_ENO asc