SELECT '1040000' AS SALDEPTCD,
    B.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    B.USER_NM /* 사용자명 */,
    'Z03' AS DEDUCTIONCD,
    '0' AS ADT_RPTP_EDT_HSTRY_SQNO /* 감사보고서편집이력순번 */,
    A.DDCT_YM,
    'AA' AS SALDIVCD,
    NVL(SUM(A.SALE_TAM),0) AS SALE_TAM,
    '' AS DEDUCTIONDATECD,
    '' AS BANKCD,
    '' AS BANKNUMBER,
    A.DDCT_YM,
    '' AS ETC,
    '' AS MODIFYID,
    '' AS MODIFYDATE
FROM TBCSPLFI002L A, TBDCMACM001M B WHERE A.TRGT_NOP_ENO = B.TRGT_NOP_ENO AND DDCT_YM = #deduction_date# AND A.TRGT_NOP_ENO LIKE #emplNum# ||'%' AND B.USER_NM LIKE '%'||#name#||'%' GROUP BY B.CRU_RRNO_SCRTY,B.USER_NM, A.DDCT_YM ORDER BY B.CRU_RRNO_SCRTY ASC