SELECT D1.TPOF_HMPG_APLY_NO /* 제보누리집신청번호 */,
    D1.TPOF_RCPT_YR /* 제보접수년도 */,
    D1.TPOF_ACTL_RCPT_NO /* 제보실제접수번호 */,
    '60' AS E_RECV_CD,
    F_CODE_NM('1000361', '60') AS F_CODE_NM,
    TO_CHAR(D1.LAST_MDFCN_DT, 'YYYYMMDDHH24MISS') AS TO_CHAR,
    D2.TPOF_BKMK_ANS_CN /* 제보즐겨찾기답변내용 */,
    TO_CHAR(D2.FRST_REG_DT, 'YYYYMMDD') AS TO_CHAR
FROM TB_CSPDCP101M D1 ,TB_CSPDCP401H D2 WHERE 1=1 AND D1.CVLCPT_CLM_SECD = D2.CVLCPT_CLM_SECD AND D1.TPOF_RCPT_YR = D2.TPOF_RCPT_YR AND D1.TPOF_RCPT_NO = D2.TPOF_RCPT_NO AND D1.CVLCPT_CLM_SECD = #CLM_SECD# AND D1.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND D1.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# AND D2.TPOF_CYCL = #TPOF_CYCL#