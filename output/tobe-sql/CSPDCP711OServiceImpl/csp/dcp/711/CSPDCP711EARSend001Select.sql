SELECT A.CVLCPT_CLM_SECD /* 민원청구구분코드 */,
    A.TPOF_RCPT_YR /* 제보접수년도 */,
    A.TPOF_RCPT_NO /* 제보접수번호 */,
    DECODE(A.CVLCPT_CLM_SECD,'2','국민감사청구','3','공익감사청구') AS CLM_SE_NM /* 청구구분명 */,
    A.TPOF_NM /* 제보명 */,
    DECODE(A.TPOF_CLMT_KDCD,'1',A.TPOF_CVLPR_NM,'2',A.TPOF_CVLPR_RPRSV_NM,'3',A.TPOF_CVLPR_RPRSV_NM,'4',A.TPOF_CVLPR_NM) AS TPOF_CVLPR_NM /* 제보민원인명 */,
    B.CLMT_KDCD /* 청구인종류코드 */,
    F_CODE_NM('1000080',B.CLMT_KDCD) AS CLMT_TYPE_NM /* 청구인유형명 */,
    DECODE(A.CVLCPT_CLM_SECD,'2',A.TPOF_RCPT_YR||'-국민-'||A.TPOF_RCPT_NO,'3',A.TPOF_RCPT_YR||'-공익-'||A.TPOF_RCPT_NO) AS CLM_NO /* 청구번호 */,
    DECODE(A.TPOF_CLMT_KDCD,'1',NULL,A.TPOF_CVLPR_NM) AS SLCT_CVLPR_CONM_NM /* 청탁민원인상호명 */,
    A.TPOF_CVLPR_TELNO /* 제보민원인전화번호 */,
    A.TPOF_CVLPR_MPNO /* 제보민원인휴대전화번호 */,
    DECODE(A.TPOF_CVLPR_EML_ID,NULL,'',A.TPOF_CVLPR_EML_ID||'@'||A.TPOF_CVLPR_EML_DMN_NM) AS CLMT_EML_ADDR /* 청구인이메일주소 */,
    TO_CHAR(A.TPOF_RCPT_DT,'YYYY-MM-DD') AS NTFCTN_RCPT_YMD /* 통보접수일자 */,
    DECODE(#RSLT_CD#,'08',TO_CHAR(A.TPOF_DISCON_DT,'YYYY-MM-DD'),TO_CHAR(TO_DATE(B.ADT_RSLT_RPLY_YMD),'YYYY-MM-DD')) AS ADT_RSLT_RPLY_YMD /* 감사결과회신일자 */,
    #HOME_STA_CD# AS CRU_PRCS_PRCS_STTS /* 부패처리상태구분코드 */,
    #HOME_STA_NM# AS PRCS_NM /* 처리명 */,
    #DVSN_CD# AS USER_HSTRY_SECD /* 사용자이력구분코드 */
FROM TB_CSPDCP101M A, TB_CSPEAR001M B WHERE A.CVLCPT_CLM_SECD = B.CLM_SECD AND A.TPOF_RCPT_YR = B.CLM_YR AND A.TPOF_RCPT_NO = B.CLM_RCPT_NO AND A.CVLCPT_CLM_SECD = #CLM_SECD# AND A.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND A.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT#