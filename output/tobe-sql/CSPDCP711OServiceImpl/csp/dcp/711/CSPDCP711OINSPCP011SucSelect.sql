SELECT D1.TPOF_HMPG_APLY_NO /* 제보누리집신청번호 */,
    D1.TPOF_RCPT_YR /* 제보접수년도 */,
    D1.TPOF_ACTL_RCPT_NO /* 제보실제접수번호 */,
    D1.TPOF_STTS_SECD /* 제보상태구분코드 */,
    F_CODE_NM('1000361', D1.TPOF_STTS_SECD) AS F_CODE_NM,
    TO_CHAR(SYSDATE, 'YYYYMMDDHH24MISS') AS TO_CHAR,
    D2.TPOF_PRCS_DEPT_CD /* 제보처리부서코드 */,
    F_DPT_INFO(D2.TPOF_PRCS_DEPT_CD, '1') AS F_DPT_INFO,
    TO_CHAR(SYSDATE, 'YYYYMMDD') AS TO_CHAR
FROM TB_CSPDCP101M D1 ,TB_CSPDCP201M D2 WHERE 1=1 AND D1.CVLCPT_CLM_SECD = #CLM_SECD# AND D1.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND D1.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# AND D1.CVLCPT_CLM_SECD = D2.CVLCPT_CLM_SECD AND D1.TPOF_RCPT_YR = D2.TPOF_RCPT_YR AND D1.TPOF_RCPT_NO = D2.TPOF_RCPT_NO