SELECT AUD.ADT_KDCD /* 감사종류코드 */,
    F_CODE_NM('1000007',AUD.ADT_KDCD) AS F_CODE_NM,
    /*감사종류명*/ AUD.ADT_YR AS ADT_YR /* 감사년도 */,
    AUD.ADT_NO /* 감사번호 */,
    #ADT_BGNG_YMD# AS #ADT_BGNG_YMD#,
    SITU.ADT_STP_SQNO /* 감사단계순번 */,
    F_MNG_KND_AUDYRNO(AUD.ADT_YR, AUD.ADT_NO, AUD.ADT_KDCD, '-') ||'('|| SITU.ADT_STP_SQNO ||')' AS F_MNG_KND_AUDYRNO,
    /*감사년번호*/ SITU.BZTRP_NM AS BZTRP_NM /* 출장명 */,
    /*출장명*/ SITU.BZTRP_DEPT_CD AS BZTRP_DEPT_CD /* 출장부서코드 */,
    F_DPT_INFO(SITU.BZTRP_DEPT_CD, '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    PSON.TPOF_NOPE /* 제보인원수 */,
    /*실인원*/ PSON.ADT_BGNG_YMD AS ADT_BGNG_YMD /* 감사시작일자 */,
    /*감사시작일*/ PSON.ADT_END_YMD AS ADT_END_YMD /* 감사종료일자 */,
    /*감사종료일*/ PSON.ADT_DCN AS ADT_DCN /* 감사일수 */,
    /*일수*/ PSON.MAN_DAY,
    /*연인원*/ TTORG.INST_CONT AS INST_CONT /* 기관개수 */,
    /*기관수*/ AUD.ADT_MTTR_NM AS ADT_MTTR_NM /* 감사사항명 */
FROM TB_BADDED002M SITU, TB_BADDED001M AUD, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    MIN(ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    SUM(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    COUNT(*) AS TPOF_NOPE /* 제보인원수 */
FROM (SELECT TMA.BZTRP_YR /* 출장년도 */,
    TMA.BZTRP_SQNO /* 출장순번 */,
    TMA.BZTRVR_ENO /* 출장자직원전산번호 */,
    MIN(PTD.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(PTD.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    SUM(PTD.ADT_DCN) AS ADT_DCN /* 감사일수 */
FROM TBBADDED005L TMA, TBBADDED006L PTD WHERE PTD.BZTRP_YR = TMA.BZTRP_YR AND PTD.BZTRP_SQNO = TMA.BZTRP_SQNO AND PTD.FT_SQNO = TMA.FT_SQNO AND PTD.BZTRVR_ALTMNT_SQNO = TMA.BZTRVR_ALTMNT_SQNO GROUP BY TMA.BZTRP_YR, TMA.BZTRP_SQNO, TMA.BZTRVR_ENO) PTD1 GROUP BY BZTRP_YR, BZTRP_SQNO) PSON, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    COUNT(DISTINCT INST_CD) AS INST_CONT /* 기관개수 */
FROM TBBADDED008L GROUP BY BZTRP_YR, BZTRP_SQNO) AS TTORG WHERE AUD.ADT_YR = SITU.REL_ADT_YR AND AUD.ADT_NO = SITU.REL_ADT_NO AND TTORG.BZTRP_YR = SITU.BZTRP_YR AND TTORG.BZTRP_SQNO = SITU.BZTRP_SQNO AND PSON.BZTRP_YR = SITU.BZTRP_YR AND PSON.BZTRP_SQNO = SITU.BZTRP_SQNO AND SITU.BZTRP_SECD ='1' AND SITU.BZTRP_DEPT_CD LIKE #strDptCd#||'%' /*화면 부서코드*/ AND PSON.ADT_BGNG_YMD = #ADT_BGNG_YMD# /*출장일*/ /* 감찰담당관실은 조회시 제외하며, 로그인 한 담당자가 감찰담당관일때만 포함하여 모두 보임 - 2016.01.04 yyh */ AND AUD.SPRVSN_DEPT_CD != '100100'