    SITU.BZTRP_DEPT_CD /* 출장부서코드 */,
    TTO.INST_CD /* 기관코드 */,
    F_ORG_INFO(TTO.INST_CD,'1') AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    /*대상기관*/ TTO.RGN_CD AS RGN_CD /* 지역코드 */,
    F_CODE_NM('1000001',TTO.RGN_CD) AS F_CODE_NM,
    /*지역명*/ TTO.ADT_BGNG_YMD AS ADT_BGNG_YMD /* 감사시작일자 */,
    /*감사기간 FROM*/ TTO.ADT_END_YMD AS ADT_END_YMD /* 감사종료일자 */,
    /*감사기간 TO*/ TTO.AUDT_DEPT_NM AS AUDT_DEPT_NM /* 수감부서명 */,
    TTO.LDG_YN /* 숙박여부 */,
    TTO.STGT_MVMN_RGN_YN /* 연속이동지역여부 */,
    PSON.TPOF_NOPE /* 제보인원수 */,
    /*실인원*/ PSON.ADT_DCN AS ADT_DCN /* 감사일수 */,
    /*일수*/ PSON.MAN_DAY,
    /*연인원*/ F_MNG_KND_AUDYRNO(AUD.ADT_YR, AUD.ADT_NO, AUD.ADT_KDCD, '-') ||'('||SITU.ADT_STP_SQNO ||')' AS F_MNG_KND_AUDYRNO /* 감사년번호 */
FROM TB_BADDED008L TTO, TB_BADDED001M AUD, TB_BADDED002M SITU, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    FT_SQNO /* 출장조순번 */,
    FT_TRGT_INST_SQNO /* 출장조대상기관순번 */,
    COUNT(*) AS TPOF_NOPE /* 제보인원수 */,
    SUM(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    MAX(ADT_DCN) AS ADT_DCN /* 감사일수 */
FROM TBBADDED006L GROUP BY BZTRP_YR, BZTRP_SQNO, FT_SQNO, FT_TRGT_INST_SQNO) PSON WHERE TTO.ADT_BGNG_YMD = #ADT_BGNG_YMD# AND PSON.BZTRP_YR = TTO.BZTRP_YR AND PSON.BZTRP_SQNO = TTO.BZTRP_SQNO AND PSON.FT_SQNO = TTO.FT_SQNO AND PSON.FT_TRGT_INST_SQNO = TTO.FT_TRGT_INST_SQNO AND SITU.BZTRP_YR = TTO.BZTRP_YR AND SITU.BZTRP_SQNO = TTO.BZTRP_SQNO /*AND SITU.BZT_BRDV_CD = @DPT_CD_SEARCH 상위 쿼리의 (SQL_MAIN_QUERY) SITU.BZT_BRDV_CD*/ AND AUD.ADT_YR = SITU.REL_ADT_YR AND AUD.ADT_NO = SITU.REL_ADT_NO /* 감찰담당관실은 조회시 제외하며, 로그인 한 담당자가 감찰담당관일때만 포함하여 모두 보임 - 2016.01.04 yyh */ AND AUD.SPRVSN_DEPT_CD != '100100'