SELECT SITU.BZTRP_YR /* 출장년도 */,
    SITU.BZTRP_SQNO /* 출장순번 */,
    F_MNG_KND_AUDYRNO(AUD.ADT_YR, AUD.ADT_NO, AUD.ADT_KDCD, '-') ||'('|| SITU.ADT_STP_SQNO || ')' AS ADT_NO /* 감사번호 */,
    /*감사연번호*/ F_CODE_NM('1000007',AUD.ADT_KDCD) AS F_CODE_NM,
    /*감사종류명*/ F_CODE_NM('1000173',SITU.BZTRP_TL_JBGD_SECD) AS F_CODE_NM,
    /*감사반장 직급*/ F_USR_INFO(SITU.BZTRP_TL_ENO,'2') AS F_USR_INFO,
    /*감사반장 이름*/ SITU.BZTRP_NM AS BZTRP_NM /* 출장명 */,
    /*감사사항*/ PSON.BZTRP_BGNG_YMD AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    /*출장기간 FROM*/ PSON.BZTRP_END_YMD AS BZTRP_END_YMD /* 출장종료일자 */,
    /*출장기간 TO*/ PSON.BZTRP_DCN AS BZTRP_DCN /* 출장일수 */,
    /*출장일수*/ PSON.PSON_CNT,
    /*출장인원*/ F_DPT_INFO(SITU.BZTRP_DEPT_CD,'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    /*주관국과*/ F_CODE_NM('1000751',AUD.BZTRP_BGT_SECD) AS F_CODE_NM /* 예산구분명 추가 - 2016.06.22 yyh */
FROM TB_BADDED001M AUD, TB_BADDED002M SITU, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    MIN(BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    MAX(BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */,
    COUNT(*) AS PSON_CNT
FROM (SELECT TMA.BZTRP_YR /* 출장년도 */,
    TMA.BZTRP_SQNO /* 출장순번 */,
    TMA.BZTRVR_ENO /* 출장자직원전산번호 */,
    MIN(PTD.BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(PTD.BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    SUM(PTD.BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */
FROM TBBADDED005L TMA, TBBADDED006L PTD WHERE PTD.BZTRP_YR = TMA.BZTRP_YR AND PTD.BZTRP_SQNO = TMA.BZTRP_SQNO AND PTD.FT_SQNO = TMA.FT_SQNO AND PTD.BZTRVR_ALTMNT_SQNO = TMA.BZTRVR_ALTMNT_SQNO AND PTD.BZTRP_YR = #BZTRP_YR# AND PTD.BZTRP_SQNO = #BZTRP_SQNO# GROUP BY TMA.BZTRP_YR, TMA.BZTRP_SQNO, TMA.BZTRVR_ENO) PTD1 GROUP BY BZTRP_YR, BZTRP_SQNO ) PSON WHERE SITU.BZTRP_YR = #BZTRP_YR# AND SITU.BZTRP_SQNO = #BZTRP_SQNO# AND AUD.ADT_YR = SITU.REL_ADT_YR AND AUD.ADT_NO = SITU.REL_ADT_NO AND PSON.BZTRP_YR = SITU.BZTRP_YR AND PSON.BZTRP_SQNO = SITU.BZTRP_SQNO