SELECT SITU.BZTRP_YR /* 출장년도 */,
    SITU.BZTRP_SQNO /* 출장순번 */,
    F_MNG_KND_AUDYRNO(AUD.ADT_YR, AUD.ADT_NO, AUD.ADT_KDCD, '-') ||'('||SITU.ADT_STP_SQNO || ')' AS F_MNG_KND_AUDYRNO,
    /*감사연번호*/ F_CODE_NM('1000007',AUD.ADT_KDCD) AS F_CODE_NM,
    /*감사종류명*/ SITU.BZTRP_NM AS BZTRP_NM /* 출장명 */,
    /*감사사항*/ PSON.ADT_BGNG_YMD AS ADT_BGNG_YMD /* 감사시작일자 */,
    /*감사기간 FROM*/ PSON.ADT_END_YMD AS ADT_END_YMD /* 감사종료일자 */,
    /*감사기간 TO*/ PSON.ADT_DCN AS ADT_DCN /* 감사일수 */,
    /*감사일수*/ PSON.MAN_DAY,
    /*연인원*/ PSON.PSON_CNT,
    /*감사인원*/ F_CODE_NM('1000751',AUD.BZTRP_BGT_SECD) AS F_CODE_NM,
    /* 예산구분명 추가 - 2016.06.22 yyh */ F_CODE_NM('1000030',SITU.ADT_BZTRP_KDCD) AS F_CODE_NM,
    /*감사출장종류명 - 2018.01.17 yyh */ F_CODE_NM('1000027',AUD.ADT_SCL_SECD) AS F_CODE_NM /* 감사규모 - 2018.01.17 yyh */
FROM TB_BADDED001M AUD, TB_BADDED002M SITU, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    MIN(ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    SUM(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    COUNT(*) AS PSON_CNT
FROM (SELECT TMA.BZTRP_YR /* 출장년도 */,
    TMA.BZTRP_SQNO /* 출장순번 */,
    TMA.BZTRVR_ENO /* 출장자직원전산번호 */,
    MIN(PTD.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(PTD.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    SUM(PTD.ADT_DCN) AS ADT_DCN /* 감사일수 */
FROM TBBADDED005L TMA, TBBADDED006L PTD WHERE PTD.BZTRP_YR = TMA.BZTRP_YR AND PTD.BZTRP_SQNO = TMA.BZTRP_SQNO AND PTD.FT_SQNO = TMA.FT_SQNO AND PTD.BZTRVR_ALTMNT_SQNO = TMA.BZTRVR_ALTMNT_SQNO AND PTD.BZTRP_YR = #BZTRP_YR# AND PTD.BZTRP_SQNO = #BZTRP_SQNO# GROUP BY TMA.BZTRP_YR, TMA.BZTRP_SQNO, TMA.BZTRVR_ENO) PTD1 GROUP BY BZTRP_YR, BZTRP_SQNO ) PSON WHERE SITU.BZTRP_YR = #BZTRP_YR# AND SITU.BZTRP_SQNO = #BZTRP_SQNO# AND AUD.ADT_YR = SITU.REL_ADT_YR AND AUD.ADT_NO = SITU.REL_ADT_NO AND PSON.BZTRP_YR = SITU.BZTRP_YR AND PSON.BZTRP_SQNO = SITU.BZTRP_SQNO