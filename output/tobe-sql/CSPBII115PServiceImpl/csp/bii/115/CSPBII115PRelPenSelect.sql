SELECT A.INFO_RLPR_NM /* 정보관계자명 */,
    A.RRNO_SCRTY /* 주민등록번호보안 */,
    A.OGDP_INST_NM /* 소속기관명 */,
    A.JBGD_NM /* 직급명 */,
    A.JBPS_NM /* 직위명 */,
    A.INFO_YR /* 정보년도 */,
    A.INFO_SQNO /* 정보순번 */,
    A.INFO_RLPR_SQNO /* 정보관계자순번 */
FROM TB_CSPBII130L A /* 감사정보관련자내역 */ WHERE 1=1 AND A.INFO_YR = #INFO_YR# AND A.FRS_MDM_SQNO = #FRS_MDM_SQNO# AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII100M B WHERE B.OVRL_PIC_ENO = #OVRL_PIC_ENO#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT REGEXP_SUBSTR(#strYeList#, '[^||]+', 1, LEVEL),
    REGEXP_SUBSTR(#strSrnoList#, '[^||]+', 1, LEVEL)
FROM DUAL CONNECT BY LEVEL &lt;= REGEXP_COUNT(#strYeList#, '[^|]+')) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII100M B WHERE B.ATRZ_DOC_NO = #ATRZ_DOC_NO#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII100M B WHERE B.NTFCTN_RCPT_YMD BETWEEN #strStrAcpDt# AND #strEndAcpDt#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII100M B WHERE B.CRU_RCPT_DT BETWEEN #strStrAcpDh# AND #strEndAcpDh#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII100M B WHERE B.HNDT_YMD BETWEEN #strStrAcpDt# AND #strEndAcpDt#) UNION ALL SELECT A.INFO_RLPR_NM /* 정보관계자명 */,
    A.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    A.TPOF_OGDP_INST_NM /* 제보소속기관명 */,
    A.JBGD_NM /* 직급명 */,
    A.JBPS_NM /* 직위명 */,
    A.INFO_YR /* 정보년도 */,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    A.INFO_RLPR_SQNO /* 정보관계자순번 */
FROM TB_CSPBII175L A /* 감사정보관련자내역 */ WHERE 1=1 AND A.INFO_YR = #INFO_YR# AND A.FRS_MDM_SQNO = #FRS_MDM_SQNO# AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII170M B WHERE B.OVRL_PIC_ENO = #OVRL_PIC_ENO#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT REGEXP_SUBSTR(#strYeList2#, '[^||]+', 1, LEVEL),
    REGEXP_SUBSTR(#strSrnoList2#, '[^||]+', 1, LEVEL)
FROM DUAL CONNECT BY LEVEL &lt;= REGEXP_COUNT(#strYeList2#, '[^|]+')) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII170M B WHERE B.ATRZ_DOC_NO = #ATRZ_DOC_NO#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII170M B WHERE B.NTFCTN_RCPT_YMD BETWEEN #strStrAcpDt# AND #strEndAcpDt#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII170M B WHERE B.CRU_RCPT_DT BETWEEN #strStrAcpDh# AND #strEndAcpDh#) AND (A.INFO_YR, A.FRS_MDM_SQNO) IN (SELECT B.INFO_YR /* 정보년도 */,
    B.INFO_SQNO /* 정보순번 */
FROM TB_CSPBII170M B WHERE B.HNDT_YMD BETWEEN #strStrAcpDt# AND #strEndAcpDt#) ORDER BY A.INFO_RLPR_SQNO