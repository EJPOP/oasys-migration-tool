SELECT EDU_DVSN_CD,
    EDU_CAT_CD,
    CRS_CD,
    CRS_CDN /* 과정기수 */
FROM (SELECT T1.EDU_DVSN_CD,
    T2.EDU_CAT_CD,
    D1.CRS_CD,
    D1.CRS_CDN /* 과정기수 */
FROM TBCSPSAE201D D1 , TBCSPSAE101C T1 , TBCSPSAE102C T2 , TBCSPSAE103C T3 WHERE 1 = 1 AND D1.EDU_BGNG_YMD = #EDU_BGNG_YMD# AND T3.CRS_CD = D1.CRS_CD AND T2.EDU_CAT_CD = T3.EDU_CAT_CD AND T2.EDU_DVSN_CD = T3.EDU_DVSN_CD AND T1.EDU_DVSN_CD = T2.EDU_DVSN_CD ORDER BY D1.CRS_CD, D1.CRS_CDN) WHERE ROWNUM = 1;