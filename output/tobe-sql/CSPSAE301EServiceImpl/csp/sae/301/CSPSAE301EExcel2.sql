SELECT '환불' AS SE_NM /* 구분명 */,
    A.GRNDS_EXMN_AEX_SQNO || ' ' || A.INFO_RLPR_NM AS INFO_RLPR_NM /* 정보관계자명 */,
    (A.EDU_EXS + A.DMT_EXS + A.BZTRP_TRVEXP_FDEXP) AS EDU_EXS,
    A.PD_AMT,
    (A.UNPAID_AMT) AS UNPAID_AMT /* 미납금액 */,
    '환불 ' AS RMRK_CN /* 비고내용 */
FROM TBCSPSAE301D A WHERE A.LINK_YR = SUBSTR(#strFromYm#, 0, 4) AND A.CRS_CD = #strEDU_CRS_CD# AND A.CRS_CDN = #CRS_CDN# AND A.UNPAID_AMT > 0 ORDER BY A.GRNDS_EXMN_AEX_SQNO