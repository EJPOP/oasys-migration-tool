    A.LEND_BL,
    A.LAG_WDL_RMK AS LAG_WDL_RMK
FROM TBCSPNBK205L A, TBCSPNBK001M B, TBDCMACM001M C, TBDCMACM002M D, TBDCMACM013C E WHERE A.LEND_KND_CD = #lendKndCd# AND A.LEND_DEAL_DVSN_CD = '1' AND A.CNL_YN = 'N' AND SUBSTR(A.LEND_DEAL_DT,0,6) = #hndlYm# AND A.MOUI_NO = B.MOUI_NO AND B.TRGT_NOP_ENO = C.TRGT_NOP_ENO AND C.SRECS_DEPT_CD = D.SRECS_DEPT_CD AND E.CMM_CD_DVSN_CD = '1000173' AND E.CD = C.JBGD_SECD