SELECT DT AS DT,
    TO_CHAR(TO_DATE(DT), 'YYYY-MM-DD') || '(' || TO_CHAR(TO_DATE(DT), 'DY', 'NLS_DATE_LANGUAGE=korean') || ')' AS TO_CHAR,
    CASE WHEN (SELECT COUNT(1)
FROM TB_BADDED013B WHERE INFO_YR || HLDY_CRTR_MND = T1.DT) > 0 THEN 'Y' ELSE 'N' END AS HOLIDAY_YN FROM ( SELECT TO_CHAR(SYSDATE + (LEVEL-1), 'YYYYMMDD') AS TO_CHAR
FROM DUAL CONNECT BY LEVEL <= 14 ) T1 WHERE TO_CHAR(TO_DATE(DT), 'DY', 'NLS_DATE_LANGUAGE=korean') NOT IN ('토','일') ORDER BY DT