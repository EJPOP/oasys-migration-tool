SELECT D1.ATRZ_DOC_NO /* 결재문서번호 */,
    D1.TPOF_ATRZ_SECD /* 제보결재구분코드 */,
    D2.APV_SNO,
    D2.APRVR_ENO /* 결재자직원전산번호 */,
    D2.APVR_STA_CD,
    D2.APRVR_KDCD /* 결재자종류코드 */,
    D2.APVR_SEQ,
    ( SELECT USR.USER_ID AS USER_ID /* 사용자아이디 */
FROM TBDCMACM001M USR WHERE USR.TRGT_NOP_ENO = D2.APRVR_ENO) AS USER_ID FROM TBDCMACM022L D1, TBDCMACM023L D2 WHERE D1.ATRZ_DOC_NO = D2.ATRZ_DOC_NO AND D1.TPOF_ATRZ_SECD = D2.TPOF_ATRZ_SECD AND D1.ATRZ_DOC_NO = #ATRZ_DOC_NO# AND D1.TPOF_ATRZ_SECD = #TPOF_ATRZ_SECD# AND D2.APV_SNO <> #APV_SNO#