SELECT DECODE(A.APVR_STA_CD,'201','[상신완료]','202','[검토완료]','203','[결재완료]','204','[협조완료]','205','[전결완료]','206','[대결완료]','207','[제출완료]','401','[결재반려]') AS APVR_STA_CD,
    TO_CHAR(A.APV_DH,'yyyymmddhh24miss') AS TO_CHAR,
    F_USR_INFO(A.APRVR_ENO,'1') AS USER_ID /* 사용자아이디 */,
    A.APVR_SEQ,
    MAX(B.ADT_RPTP_EDT_HSTRY_SQNO) AS ADT_RPTP_EDT_HSTRY_SQNO /* 감사보고서편집이력순번 */
FROM TBDCMACM023L A , PTL_ORDER_LIST B WHERE A.ATRZ_DOC_NO = B.DOC_NO AND A.ATRZ_DOC_NO = #ATRZ_DOC_NO# AND A.APV_SNO = #APV_SNO# AND A.APV_DH IS NOT NULL GROUP BY A.APVR_STA_CD, A.APV_DH, A.APRVR_ENO , A.APVR_SEQ