SELECT A.LINK_YR /* 연계년도 */,
    A.CRS_CD,
    A.CRS_CDN /* 과정기수 */,
    A.ASG_YN,
    B.LCT_RM_DVSN_CD,
    B.LCT_RM_NO,
    A.USE_STR_DT,
    A.USE_END_DT,
    A.USE_STR_HM,
    A.USE_END_HM
FROM TBCSPSAE203D A , TBCSPSAE114C B WHERE A.LCT_RM_NO = B.LCT_RM_NO AND A.LINK_YR = #LINK_YR# AND A.CRS_CD = #strEDU_CRS_CD# AND A.CRS_CDN = #CRS_CDN# ORDER BY A.ASG_YN DESC, A.USE_STR_DT, A.USE_END_DT, A.USE_STR_HM, A.USE_END_HM