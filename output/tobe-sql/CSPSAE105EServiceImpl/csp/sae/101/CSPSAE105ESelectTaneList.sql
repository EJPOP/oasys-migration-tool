SELECT A.LINK_YR /* 연계년도 */,
    A.CRS_CD,
    A.CRS_CDN /* 과정기수 */,
    A.TANE_ID,
    A.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    A.INFO_RLPR_NM /* 정보관계자명 */,
    NVL(A.RAL_BLT_NM, (SELECT AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */
FROM TBDCMACM015M WHERE INST_CD = A.INST_CD)) AS RAL_BLT_NM , A.NPL_BLT_NM , A.JBGD_NM , TRIM(REPLACE(A.SLCT_CVLPR_MPNO, '-')) AS SLCT_CVLPR_MPNO , A.TRGT_NOP_ENO FROM TBCSPSAE301D A WHERE A.LINK_YR = #LINK_YR# AND A.CRS_CD = #strEDU_CRS_CD# AND A.CRS_CDN = #CRS_CDN# ORDER BY A.GRNDS_EXMN_AEX_SQNO, A.TANE_ID