SELECT B.LINK_YR /* 연계년도 */,
    B.CRS_CD,
    B.CRS_CDN /* 과정기수 */,
    B.SUBJ_SNO,
    B.SUBJ_CD,
    A.BLL_SBJCT_NM /* 계산서과목명 */,
    B.LCT_TM_HM,
    B.JON_TM_HM,
    B.ETC_TM_HM,
    SUM(B.LCT_TM_HM + B.JON_TM_HM + B.ETC_TM_HM) AS LCT_TM_HM,
    'U' AS REG_SECD /* 등록구분코드 */,
    B.SUBJ_YN,
    B.PFSR_ID
FROM TBCSPSAE101M A , TBCSPSAE101D B WHERE A.SUBJ_CD = B.SUBJ_CD AND A.USE_YN = 'Y' AND B.SUBJ_YN = 'N' AND B.LINK_YR = #LINK_YR# AND B.CRS_CD = #strEDU_CRS_CD# AND B.CRS_CDN = #CRS_CDN# GROUP BY B.LINK_YR , B.CRS_CD , B.CRS_CDN , B.SUBJ_SNO , B.SUBJ_CD , A.BLL_SBJCT_NM , B.LCT_TM_HM , B.JON_TM_HM , B.ETC_TM_HM , B.SUBJ_YN , B.PFSR_ID ORDER BY TO_NUMBER(B.SUBJ_SNO)