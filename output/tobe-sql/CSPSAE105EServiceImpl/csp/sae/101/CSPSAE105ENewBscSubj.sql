SELECT A.LINK_YR /* 연계년도 */,
    A.CRS_CD,
    A.CRS_CDN /* 과정기수 */,
    A.SUBJ_SNO,
    A.SUBJ_CD,
    A.BLL_SBJCT_NM /* 계산서과목명 */,
    A.LCT_TM_HM,
    A.JON_TM_HM,
    A.ETC_TM_HM,
    A.TOT_TM_HM,
    A.REG_SECD /* 등록구분코드 */,
    A.SUBJ_YN,
    A.PFSR_ID
FROM ( SELECT #strYR_CD# AS LINK_YR /* 연계년도 */,
    #strEDU_CRS_CD# AS #STREDU_CRS_CD#,
    #strCRS_CDN# AS CRS_CDN /* 과정기수 */,
    rownum,
    SUBJ_CD,
    BLL_SBJCT_NM /* 계산서과목명 */,
    '' AS LCT_TM_HM,
    '' AS JON_TM_HM,
    '' AS ETC_TM_HM,
    '' AS TOT_TM_HM,
    'I' AS REG_SECD /* 등록구분코드 */,
    'N' AS SUBJ_YN,
    NULL
FROM TBCSPSAE101M WHERE BSC_OP_SUBJ_YN = 'Y' AND USE_YN = 'Y' ORDER BY SUBJ_CD ) A ORDER BY A.SUBJ_SNO