FROM TBDCMACM001M WHERE TRGT_NOP_ENO = MEAL_PEN_CNB) OR (SELECT ACTL_OGDP_DEPT_CD /* 실제소속부서코드 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = #TRGT_NOP_ENO#) = (SELECT ACTL_OGDP_DEPT_CD /* 실제소속부서코드 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = BOKG_PEN_CNB) ) ORDER BY MEAL_DT