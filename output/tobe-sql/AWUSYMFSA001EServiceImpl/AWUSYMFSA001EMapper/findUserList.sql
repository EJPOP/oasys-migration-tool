SELECT FRS_MDM_SQNO /* 포렌식매체순번 */,
    AUTHRT_SECD /* 권한구분코드 */,
    AUTH_ID,
    (CASE WHEN A.AUTHRT_SECD = 'U' THEN B.USER_NM ELSE C.CNSTN_MBCMT_DEPT_NM END ) AS AUTHRT_SECD /* 권한구분코드 */,
    COUNT(1) OVER () AS 1
FROM TBAWUSYMFSA001M A, TBDCMACM001M B, TBDCMACM002M C WHERE 1=1 AND A.AUTH_ID = B.TRGT_NOP_ENO (+) AND A.AUTH_ID = C.SRECS_DEPT_CD(+) ORDER BY FRS_MDM_SQNO ASC