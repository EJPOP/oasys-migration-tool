    0
FROM DUAL UNION ALL SELECT A.DATE_NAME,
    ROWNUM
FROM ( SELECT DISTINCT( SUBSTRING(B.DT, 5, 2) || '. ' || SUBSTRING(B.DT, 7, 2) || '(' || SUBSTRING(UPPER(TO_CHAR(TO_DATE(B.DT), 'DAY', 'NLS_DATE_LANGUAGE=KOREAN')), 0, 1) || ')') AS SUBSTRING
FROM ( SELECT TO_CHAR(TO_DATE(A.EDU_BGNG_YMD, 'YYYYMMDD') + LEVEL-1, 'YYYYMMDD') AS TO_CHAR
FROM ( SELECT EDU_BGNG_YMD /* 교육시작일자 */,
    EDU_END_YMD /* 교육종료일자 */
FROM TBCSPSAE201D WHERE LINK_YR = #LINK_YR# AND CRS_CD = #strEDU_CRS_CD# AND CRS_CDN = #CRS_CDN#) A CONNECT BY LEVEL <= TO_DATE(A.EDU_END_YMD, 'YYYYMMDD') - TO_DATE(A.EDU_BGNG_YMD, 'YYYYMMDD') + 1 AND ROWNUM <= 7 ) B WHERE SUBSTRING(UPPER(TO_CHAR(TO_DATE(B.DT), 'DAY', 'NLS_DATE_LANGUAGE=KOREAN')), 0, 1) NOT IN ('일', '토') ORDER BY DATE_NAME ) A ORDER BY NUM