SELECT MAN.EVL_GP_NM /* 평가군명 */,
    F_USR_INFO(MAN.EVLT_CNB, '2') AS INFO_RLPR_NM /* 정보관계자명 */,
    SUM(DECODE( NVL(EVL.EVL_STA_CD, 0), 0, 1, '100', 1, '200', 1, 0)) AS EVL_STA_CD /* 미확인 건수 */,
    SUM(DECODE(EVL.EVL_STA_CD, '300', 1, 0)) AS EVL_STA_CD /* 확인완료건수 */,
    SUM(DECODE( NVL(EVL.EVL_STA_CD, 0), 0, 1, '100', 1, '200', 1, '300', 1, 0)) AS EVL_STA_CD /* 합계건수 */,
    MAX(MAN.EVL_YR) AS EVL_YR /* 평가년도 */,
    MAX(MAN.HLYR_DVSN_CD) AS HLYR_DVSN_CD /* 반기구분코드 */,
    MAN.EVL_GP_CD /* 평가군코드 */,
    MAN.EVLT_CNB AS PIC_ENO /* 담당자 전산번호 */
FROM ( SELECT D6.EVL_YR,
    D6.HLYR_DVSN_CD,
    D6.EVL_GP_CD,
    D6.EVL_GP_NM,
    D6.EVLT_CNB /* 평가자 전산번호 */,
    D7.NEVLT_CNB /* 피평가자 전산번호 */
FROM ( SELECT D4.EVL_YR,
    D4.HLYR_DVSN_CD,
    D4.EVL_GP_CD,
    D4.EVL_GP_NM,
    D4.EVLT_CNB /* 평가자 전산번호 */,
    D4.BLT_DPT_CD /* 평가자 소속부서코드 */
FROM ( SELECT D2.EVL_YR,
    D2.HLYR_DVSN_CD,
    D2.EVL_GP_CD,
    D1.EVL_GP_NM,
    D2.EVLT_CNB /* 평가자 전산번호 */,
    D2.BLT_DPT_CD /* 평가자 소속부서코드 */
FROM TBCSPAOE018M D1 , TBCSPAOE019D D2 WHERE D1.EVL_YR = D2.EVL_YR AND D1.HLYR_DVSN_CD = D2.HLYR_DVSN_CD AND D1.EVL_GP_CD = D2.EVL_GP_CD AND D2.EVL_YR = #strEvlYr# AND D2.HLYR_DVSN_CD = #strHlyrDvsnCd# ) D4 , TBDCMACM002M D5 WHERE D4.BLT_DPT_CD = D5.SRECS_DEPT_CD AND D5.USE_YN = 'Y' ) D6 , TBCSPAOE020D D7 WHERE D6.EVL_YR = D7.EVL_YR AND D6.HLYR_DVSN_CD = D7.HLYR_DVSN_CD AND D6.EVL_GP_CD = D7.EVL_GP_CD ) MAN /*운영직업무 평가자 리스트*/ , ( SELECT EVL_YR,
    HLYR_DVSN_CD,
    NEVLT_CNB,
    EVL_CMTR_CNB,
    MIN(EVL_STA_CD) AS EVL_STA_CD,
    MIN(EVL_CMTR_DVSN_CD) AS EVL_CMTR_DVSN_CD,
    MIN(CVLCPT_TASK_SECD) AS CVLCPT_TASK_SECD /* 민원업무구분코드 */
FROM TBCSPAOE032M WHERE EVL_YR = #strEvlYr# AND HLYR_DVSN_CD = #strHlyrDvsnCd# GROUP BY EVL_YR, HLYR_DVSN_CD, NEVLT_CNB, EVL_CMTR_CNB ) EVL WHERE MAN.EVL_YR = EVL.EVL_YR (+) AND MAN.HLYR_DVSN_CD = EVL.HLYR_DVSN_CD (+) AND MAN.NEVLT_CNB = EVL.NEVLT_CNB (+) AND MAN.EVLT_CNB = EVL.EVL_CMTR_CNB (+) AND MAN.EVL_GP_CD = EVL.EVL_CMTR_DVSN_CD (+) AND EVL.CVLCPT_TASK_SECD (+) = '10003' /*운영직업무*/ GROUP BY MAN.EVL_GP_CD, MAN.EVL_GP_NM, MAN.EVLT_CNB