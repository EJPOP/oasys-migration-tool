SELECT F_DPT_INFO(MAN.BLT_DPT_CD, '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    F_CODE_NM('1000173', MAN.JBGD_SECD) AS JBGD_NM /* 직급명 */,
    F_USR_INFO(MAN.NEVLT_CNB, '2') AS F_USR_INFO /* 피평가자 성명 */,
    F_CODE_NM('1000280', NVL(EVL.EVL_STA_CD, '100')) AS F_CODE_NM /* 평가상태 */
FROM ( SELECT D6.EVL_YR,
    D6.HLYR_DVSN_CD,
    D6.EVL_GP_CD,
    D6.EVL_GP_NM,
    D6.EVLT_CNB /* 평가자 전산번호 */,
    D7.NEVLT_CNB /* 피평가자 전산번호 */,
    (SELECT AS SRECS_DEPT_CD /* 심사재심의부서코드 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = D7.NEVLT_CNB) AS BLT_DPT_CD , D7.JBGD_SECD /*피평가자 직급*/ FROM ( SELECT D4.EVL_YR,
    D4.HLYR_DVSN_CD,
    D4.EVL_GP_CD,
    D4.EVL_GP_NM,
    D4.EVLT_CNB /* 평가자 전산번호 */,
    D4.BLT_DPT_CD /* 평가자 소속부서코드 */
FROM ( SELECT D2.EVL_YR,
    D2.HLYR_DVSN_CD,
    D2.EVL_GP_CD,
    D1.EVL_GP_NM,
    D2.EVLT_CNB /* 평가자 전산번호 */,
    D2.BLT_DPT_CD /* 평가자 소속부서코드 */
FROM TBCSPAOE018M D1 , TBCSPAOE019D D2 WHERE D1.EVL_YR = D2.EVL_YR AND D1.HLYR_DVSN_CD = D2.HLYR_DVSN_CD AND D1.EVL_GP_CD = D2.EVL_GP_CD AND D2.EVL_YR = #strEvlYr# AND D2.HLYR_DVSN_CD = #strHlyrDvsnCd# ) D4 , TBDCMACM002M D5 WHERE D4.BLT_DPT_CD = D5.SRECS_DEPT_CD AND D5.USE_YN = 'Y' ) D6 , TBCSPAOE020D D7 WHERE D6.EVL_YR = D7.EVL_YR AND D6.HLYR_DVSN_CD = D7.HLYR_DVSN_CD AND D6.EVL_GP_CD = D7.EVL_GP_CD ) MAN , ( SELECT EVL_YR,
    HLYR_DVSN_CD,
    NEVLT_CNB,
    EVL_CMTR_CNB,
    MIN(EVL_STA_CD) AS EVL_STA_CD,
    MIN(EVL_CMTR_DVSN_CD) AS EVL_CMTR_DVSN_CD,
    MIN(CVLCPT_TASK_SECD) AS CVLCPT_TASK_SECD /* 민원업무구분코드 */
FROM TBCSPAOE032M WHERE EVL_YR = #strEvlYr# AND HLYR_DVSN_CD = #strHlyrDvsnCd# GROUP BY EVL_YR, HLYR_DVSN_CD, NEVLT_CNB, EVL_CMTR_CNB ) EVL WHERE MAN.EVL_YR = EVL.EVL_YR (+) AND MAN.HLYR_DVSN_CD = EVL.HLYR_DVSN_CD (+) AND MAN.NEVLT_CNB = EVL.NEVLT_CNB (+) AND MAN.EVLT_CNB = EVL.EVL_CMTR_CNB (+) AND MAN.EVL_GP_CD = EVL.EVL_CMTR_DVSN_CD (+) AND MAN.EVL_GP_CD = #strEvlDvsnCd# /*평가군코드*/ AND MAN.EVLT_CNB = #strEvlCmtrCnb# /*평가자 전산번호*/ AND EVL.CVLCPT_TASK_SECD (+) = '10003' /*운영직업무*/ AND NVL(EVL.EVL_STA_CD, '100') IN ('100', '200') /*평가상태코드가 100인 미확인인것만 조회한다.*/ ORDER BY MAN.BLT_DPT_CD /*2015.02.04 최원형 감사관 요청*/