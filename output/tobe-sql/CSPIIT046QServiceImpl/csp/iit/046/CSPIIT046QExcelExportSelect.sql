SELECT DISTINCT A.CVLCPT_ADT_YR AS CVLCPT_ADT_YR /* 민원감사년도 */,
    A.ADT_NO /* 감사번호 */,
    A.DSPS_RQT_KDCD /* 처분요구종류코드 */,
    A.DSPS_RQT_ENFC_YMD /* 처분요구시행일자 */,
    A.RQS_TIT_NM,
    /*활용내역명*/ A.ADT_MTTR_NM AS ADT_MTTR_NM /* 감사사항명 */,
    B.USER_NM /* 사용자명 */,
    /*제출자명*/ A.TRGT_NOP_ENO AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    /*제출자전산번호*/ A.SRECS_DEPT_CD AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    A.DSPS_RQT_NM /* 처분요구명 */,
    D.CNSTN_MBCMT_DEPT_NM AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    A.CVLCPT_DOC_ID /* 민원문서아이디 */,
    A.ADT_GRNT_NO /* 감사부여번호 */,
    F_CODE_NM('1000002',A.DSPS_RQT_KDCD) AS DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */,
    CASE WHEN C.ADT_YR >= '2016' THEN C.ADT_KDCD ELSE '' END AS ADT_YR /* 감사년도 */,
    D.HIRK_DPT_CD,
    F_CODE_NM('1000978',A.APP_KND_CD) AS F_CODE_NM
FROM TBCSPIIT026M A, TBDCMACM001M B, /*사용자테이블(제출자)*/ TB_BADDED001M C, TBDCMACM002M D /*부서코드(요청자)*/ WHERE A.TRGT_NOP_ENO = B.TRGT_NOP_ENO(+) AND A.SRECS_DEPT_CD = D.SRECS_DEPT_CD(+) AND A.CVLCPT_ADT_YR = C.ADT_YR(+) AND A.ADT_NO = C.ADT_NO(+) AND A.DSPS_RQT_ENFC_YMD &gt;= #strDSP_RQ_ENF_DTS# AND A.DSPS_RQT_ENFC_YMD &lt;= #strDSP_RQ_ENF_DTE# AND (D.CNSTN_MBCMT_DEPT_NM LIKE '%'||#strSEARCH_NM#||'%' OR A.ADT_MTTR_NM LIKE '%'||#strSEARCH_NM#||'%' OR A.RQS_TIT_NM LIKE '%'||#strSEARCH_NM#||'%' OR A.DSPS_RQT_NM LIKE '%'||#strSEARCH_NM#||'%') AND B.USER_NM LIKE '%'||#strRQR_NAM#||'%' AND A.SRECS_DEPT_CD = #SRECS_DEPT_CD# AND A.DSPS_RQT_KDCD = #DSPS_RQT_KDCD# ORDER BY A.CVLCPT_ADT_YR, A.ADT_NO DESC