SELECT A.CVLCPT_CLM_SECD /* 민원청구구분코드 */,
    A.TPOF_RCPT_YR /* 제보접수년도 */,
    A.TPOF_RCPT_NO /* 제보접수번호 */,
    TO_CHAR(B.FRST_REG_DT,'YYYYMMDD') AS HNDT_YMD /* 배부일자 */,
    B.TPOF_HNDT_DEPT_CD /* 제보배부부서코드 */,
    F_DPT_INFO(B.TPOF_HNDT_DEPT_CD, '1') AS F_DPT_INFO,
    B.TPOF_HNDT_PIC_ID /* 제보배부담당자아이디 */,
    F_USR_INFO_BY_ID(B.TPOF_HNDT_PIC_ID,'2') AS F_USR_INFO_BY_ID,
    B.TPOF_PRCS_STTS_SECD /* 제보처리상태구분코드 */,
    B.TPOF_PRCS_DEPT_CD /* 제보처리부서코드 */,
    F_DPT_INFO(B.TPOF_PRCS_DEPT_CD, '1') AS PRCS_DEPT_NM /* 처리부서명 */,
    B.TPOF_PRCS_PIC_ID /* 제보처리담당자아이디 */,
    F_USR_INFO_BY_ID(B.TPOF_PRCS_PIC_ID,'2') AS SRECS_PRCS_PIC_NM /* 심사재심의처리담당자명 */,
    B.TPOF_RDTB_DMND_RSN_CN /* 제보재배부요청사유내용 */,
    B.TPOF_HNDT_HSTRY_SQNO /* 제보배부이력순번 */,
    B.TPOF_BAI_PIC_ID /* 제보감사원담당자아이디 */,
    F_USR_INFO_BY_ID(B.TPOF_BAI_PIC_ID,'2') AS F_USR_INFO_BY_ID
FROM TB_CSPDCP101M A ,TB_CSPDCP202H B WHERE 1=1 AND A.CVLCPT_CLM_SECD = '1' AND A.CVLCPT_CLM_SECD = B.CVLCPT_CLM_SECD AND A.TPOF_RCPT_YR = B.TPOF_RCPT_YR AND A.TPOF_RCPT_NO = B.TPOF_RCPT_NO AND A.CVLCPT_CLM_SECD = #CLM_SECD# AND A.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND A.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# ORDER BY CVLCPT_HNDT_HNDT_HSTRY ASC