SELECT DECODE(A.PRPS_ACP_NO,NULL,'',PRPS_YE||'-'||A.PRPS_ACP_NO) AS PRPS_ACP_NO /* 접수번호 */,
    A.PRPS_TIT_NM /* 제안제목 */,
    F_USR_INFO(A.PRPS_PEN_CNB,'2') AS F_USR_INFO /* 제안자명 */,
    A.PRPS_PEN_CNB /* 제안자전산번호 */,
    A.NTFCTN_RCPT_YMD /* 통보접수일자 */,
    A.EXEC_DPT_CD /* 실시부서코드 */,
    F_DPT_INFO(A.EXEC_DPT_CD,'1') AS F_DPT_INFO /* 실시부서명 */,
    A.EXEC_DT /* 실시일자 */,
    A.EXEC_DVSN_CD /* 실시구분코드 */,
    A.PRPS_YE /* 제안연도 */,
    A.PRPS_SLN /* 제안연번 */
FROM TBCSPGSU001M A /*제안기본*/ WHERE 1=1 AND A.PRPS_STA_CD = 'N' AND EXEC_DVSN_CD = #strEXEC_DVSN_CD# AND A.NTFCTN_RCPT_YMD &gt;= #strACP_DTS# AND A.NTFCTN_RCPT_YMD &lt;= #strACP_DTE# AND A.EXEC_DT &gt;= #strEXEC_DTS# AND A.EXEC_DT &lt;= #strEXEC_DTE# AND A.PRPS_TIT_NM LIKE '%'||#strPRPS_TIT_NM#||'%' AND A.PRPS_PEN_CNB IN (SELECT TRGT_NOP_ENO /* 대상인원직원전산번호 */
FROM TBDCMACM001M Z WHERE Z.USER_NM LIKE '%'||#strPRPS_PEN_NAM#||'%') ORDER BY TO_NUMBER(A.PRPS_YE) DESC , TO_NUMBER(A.PRPS_ACP_NO) DESC