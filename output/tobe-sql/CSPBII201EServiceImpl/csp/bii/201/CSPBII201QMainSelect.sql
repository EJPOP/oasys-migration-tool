SELECT A.SPCF_INFO_CLCT_NO /* 특정정보수집번호 */,
    A.BGNG_YMD /* 시작일자 */,
    A.END_YMD /* 종료일자 */,
    A.SPCF_ADT_MTTR_NM /* 특정감사사항명 */,
    A.SPRVSN_DEPT_CD /* 주관부서코드 */,
    F_DPT_INFO(A.SPRVSN_DEPT_CD, '1') AS F_DPT_INFO
FROM TB_CSPBII140M A WHERE #strYe# BETWEEN SUBSTR(A.BGNG_YMD, 1, 4) AND SUBSTR(A.END_YMD, 1, 4) ORDER BY DECODE(A.SPCF_INFO_CLCT_NO,'4','2',A.SPCF_INFO_CLCT_NO)