SELECT A.REG_YMD /* 등록일자 */,
    A.REG_NO /* 등록번호 */,
    A.ENAIS_PST_ANS_NMTM /* ENAIS게시물답변횟수 */,
    A.USER_HSTRY_SECD /* 사용자이력구분코드 */,
    F_CODE_NM('1000308', A.USER_HSTRY_SECD) AS F_CODE_NM,
    A.TIT_ENN,
    A.ENTX,
    A.ECN,
    A.USR_EID,
    A.USER_NM /* 사용자명 */,
    A.ATT_FL_EID,
    A.ADT_PLAN_DOC_TPCD /* 감사계획문서유형코드 */,
    A.SLCT_STTS_YN /* 청탁상태여부 */,
    DECODE(B.REG_YMD,NULL,'N','Y') AS REG_YMD /* 등록일자 */,
    B.REG_YMD /* 등록일자 */,
    B.REG_NO /* 등록번호 */,
    B.ENAIS_PST_ANS_NMTM /* ENAIS게시물답변횟수 */,
    A.USER_HSTRY_SECD /* 사용자이력구분코드 */,
    B.TIT_ENN,
    B.ENTX,
    B.ECN,
    B.USR_EID,
    B.USER_NM /* 사용자명 */,
    B.SLCT_STTS_YN /* 청탁상태여부 */
FROM (SELECT *
FROM TBCSPCIS003M A WHERE ENAIS_PST_ANS_NMTM = 0) A, (SELECT *
FROM TBCSPCIS003M A WHERE ENAIS_PST_ANS_NMTM <> 0 ) B WHERE A.REG_YMD = B.REG_YMD(+) AND A.REG_NO = B.REG_NO(+) AND A.SLCT_STTS_YN = #SLCT_STTS_YN# AND A.REG_YMD = #REG_YMD# AND A.REG_NO = #REG_NO# ORDER BY A.REG_YMD DESC, A.REG_NO DESC