    CD_NM,
    CMM_CD_DVSN_CD
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000270' AND NVL(USE_YN, 'N') = 'Y' AND CD <> '000000000' AND CD <> '10000' UNION ALL SELECT CD,
    CD_NM,
    CMM_CD_DVSN_CD
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000272' AND NVL(USE_YN, 'N') = 'Y' AND CD <> '000000000' AND CD <> '10000' UNION ALL SELECT CD,
    CD_NM,
    '1000277' AS CD_KEY
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000270' AND NVL(USE_YN, 'N') = 'Y' AND CD = '10000'