SELECT T1.CVLCPT_SECD /* 민원구분코드 */,
    T1.CVLCPT_RCPT_YR /* 민원접수년도 */,
    T1.CVLCPT_RCPT_NO /* 민원접수번호 */,
    T1.CVLCPT_SQNO /* 민원순번 */,
    F_USR_INFO_BY_ID(T1.CRU_PRCR_ID,'2') AS F_USR_INFO_BY_ID,
    F_DPT_INFO(T1.CRU_PRCS_DEPT_CD,'1') AS F_DPT_INFO,
    T1.CRU_PRCS_CN /* 부패처리내용 */,
    T2.CVLCPT_RCPT_YR||'-부감-'||T2.CRU_ACTL_RCPT_NO AS NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    TO_CHAR(T1.FRST_REG_DT,'YYYY-MM-DD HH24:MI') AS FRST_REG_DT /* 최초등록일시 */
FROM TB_CSPCOR103H T1 ,TB_CSPCOR101M T2 WHERE T1.CVLCPT_SECD = #CVLCPT_SECD# AND T1.CVLCPT_RCPT_YR = #CVLCPT_RCPT_YR# AND T1.CVLCPT_RCPT_NO = #CVLCPT_RCPT_NO# AND T1.CVLCPT_SECD = T2.CVLCPT_SECD AND T1.CVLCPT_RCPT_NO = T2.CVLCPT_RCPT_NO AND T1.CVLCPT_RCPT_YR = T2.CVLCPT_RCPT_YR ORDER BY T1.CVLCPT_SQNO ASC