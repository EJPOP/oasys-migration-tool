SELECT T1.FRS_MDM_SQNO AS FRS_MDM_SQNO /* 포렌식매체순번 */,
    T1.CHR_TSK_CD AS CHR_TSK_CD /* 담당업무코드 */,
    F_CODE_NM('1000904', T1.CHR_TSK_CD) AS F_CODE_NM /* 담당업무코드명 */,
    T1.TRGT_NOP_ENO AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    T2.USER_NM AS USER_NM /* 사용자명 */,
    F_DPT_INFO(T2.SRECS_DEPT_CD, '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    TO_CHAR(T1.FRST_REG_DT,'YYYY-MM-DD') AS REG_YMD /* 등록일자 */
FROM TBCSPRGA003M T1 ,TBDCMACM001M T2 WHERE T1.TRGT_NOP_ENO = T2.TRGT_NOP_ENO ORDER BY T1.CHR_TSK_CD ,T1.TRGT_NOP_ENO