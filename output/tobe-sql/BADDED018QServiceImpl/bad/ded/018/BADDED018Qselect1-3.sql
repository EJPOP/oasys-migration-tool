    /*감사연번호*/ SITU.BZTRP_NM AS BZTRP_NM /* 출장명 */,
    /*감사출장명*/ F_CODE_NM('1000001',TE.RGN_CD) AS F_CODE_NM,
    /*지역명*/ TE.BZTRP_BGNG_YMD AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    /*출장시작일*/ TE.BZTRP_END_YMD AS BZTRP_END_YMD /* 출장종료일자 */,
    /*출장종료일*/ TE.BZTRP_DCN AS BZTRP_DCN /* 출장일수 */,
    /*출장일수*/ TE.LDG_DCN AS LDG_DCN /* 숙박일수 */,
    /*숙박일수*/ TE.LDG_CST AS LDG_CST /* 숙박비용 */,
    /*숙박비*/ TE.BZTRP_TRVEXP_FDEXP AS BZTRP_TRVEXP_FDEXP /* 출장여비식비 */,
