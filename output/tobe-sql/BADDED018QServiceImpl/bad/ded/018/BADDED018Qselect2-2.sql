SELECT '1' AS NO,
    SITU.BZTRP_YR /* 출장년도 */,
    /*출장년도*/ SITU.BZTRP_SQNO AS BZTRP_SQNO /* 출장순번 */,
    /*출장순번*/ F_CODE_NM('1000739',AUD.ADT_KDCD) AS F_CODE_NM,
    /*감사종류명*/ CASE WHEN AUD.ADT_YR >= '2016' THEN F_CODE_NM('1000739',AUD.ADT_KDCD) ELSE '-' END AS ADT_YR /* 감사년도 */,
    SITU.REL_ADT_YR /* 관계감사년도 */,
    /*감사년도*/ SITU.REL_ADT_NO AS REL_ADT_NO /* 관계감사번호 */,
    /*감사순번*/ SITU.ADT_STP_SQNO AS ADT_STP_SQNO /* 감사단계순번 */,
    /*감사단계번호*/ SITU.BZTRP_NM AS BZTRP_NM /* 출장명 */,
    /*출장명*/ TE.BZTRP_BGNG_YMD AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    /*출장시작일*/ TE.BZTRP_END_YMD AS BZTRP_END_YMD /* 출장종료일자 */,
    /*출장종료일*/ TE.BZTRP_DCN AS BZTRP_DCN /* 출장일수 */,
    /*출장일수*/ TE.LDG_DCN AS LDG_DCN /* 숙박일수 */,
    /*숙박일수*/ TE.SPM_CST AS SPM_CST /* 보충비용 */,
    /*보충경비*/ TE.BZTRVR_ENO AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    ( CASE WHEN SITU.BZTRP_TL_ENO = TE.BZTRVR_ENO THEN TE.LEAD_CST ELSE 0 END) AS LEAD_CST /* 지휘비용 */,
    /*지휘비*/ NVL(TFD.RL_FARE_AMT, 0) + NVL(TE.CPRG_FARE_CST,0) AS RL_FARE_AMT /* 철도운임금액 */,
    /*교통비*/ TE.SPM_CST + (CASE WHEN SITU.BZTRP_TL_ENO = TE.BZTRVR_ENO THEN TE.LEAD_CST ELSE 0 END) + NVL(TFD.RL_FARE_AMT, 0) + NVL(TE.CPRG_FARE_CST,0) AS SPM_CST /* 보충비용 */
FROM TB_BADDED002M SITU, TB_BADDED001M AUD, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    BZTRVR_ENO /* 출장자직원전산번호 */,
    MIN(BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    SUM(BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */,
    SUM(LDG_DCN) AS LDG_DCN /* 숙박일수 */,
    SUM(SPM_CST) AS SPM_CST /* 보충비용 */,
    SUM(CPRG_FARE_CST) AS CPRG_FARE_CST /* 수도권운임비용 */,
    /*수도권운임비 추가 - 2015.05.11 yyh*/ SUM(SUM(LEAD_CST)) OVER (PARTITION BY BZTRP_YR, BZTRP_SQNO) AS LEAD_CST /* 지휘비용 */
FROM TBBADDED011L WHERE 1=1 AND BZTRVR_ENO = #BZTRVR_ENO# AND BZTRP_BGNG_YMD >= #strBztStrDt# AND BZTRP_END_YMD <= #strBztEndDt# AND CMPTN_YN = 'Y' GROUP BY BZTRP_YR, BZTRP_SQNO, BZTRVR_ENO) TE, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    BZTRVR_ENO /* 출장자직원전산번호 */,
    SUM(RL_FARE_AMT) AS RL_FARE_AMT /* 철도운임금액 */
FROM TBBADDED017L WHERE 1=1 AND BZTRVR_ENO = #BZTRVR_ENO# AND CMPTN_YN = 'Y' GROUP BY BZTRP_YR, BZTRP_SQNO, BZTRVR_ENO) TFD WHERE 1=1 AND TE.BZTRVR_ENO = #BZTRVR_ENO# AND TFD.BZTRP_YR(+) = TE.BZTRP_YR AND TFD.BZTRP_SQNO(+) = TE.BZTRP_SQNO AND TFD.BZTRVR_ENO (+) = TE.BZTRVR_ENO AND SITU.BZTRP_YR = TE.BZTRP_YR AND SITU.BZTRP_SQNO = TE.BZTRP_SQNO AND AUD.ADT_YR = SITU.REL_ADT_YR AND AUD.ADT_NO = SITU.REL_ADT_NO AND SITU.BZTRP_SECD = '1'