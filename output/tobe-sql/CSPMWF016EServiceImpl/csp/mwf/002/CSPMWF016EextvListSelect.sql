SELECT TO_CHAR(TO_DATE(A.MEAL_DT), 'YYYY-MM-DD') AS TO_CHAR,
    A.BOKG_ID,
    A.BOKG_PEN_CNB,
    (SELECT AS USER_NM /* 사용자명 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = A.BOKG_PEN_CNB) AS BOKG_PEN_NAM , A.BOKG_DT, A.MEAL_PEN_CNB, (SELECT USER_NM /* 사용자명 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = A.MEAL_PEN_CNB) AS MEAL_PEN_NAM , (SELECT CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */
FROM TBDCMACM002M WHERE SRECS_DEPT_CD = (SELECT SRECS_DEPT_CD /* 심사재심의부서코드 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = MEAL_PEN_CNB)) AS CNSTN_MBCMT_DEPT_NM, (SELECT CD_NM
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000176' AND CD = (SELECT JBPS_CD /* 직위코드 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = MEAL_PEN_CNB)) AS JBGD_NM, A.BOKG_YN, A.MEAL_YN, A.BOKG_CNL_YN, A.MEAL_AM, A.BOKG_REGI_DH, A.MEAL_YN AS OLD_MEAL_YN, B.SRCH_SNO FROM TBCSPMWF003M A, TBCSPMWF005M B WHERE A.MEAL_DT = #ymd# AND A.MEAL_PEN_CNB = B.TRGT_NOP_ENO AND A.BOKG_CNL_YN = 'N' UNION SELECT TO_CHAR(TO_DATE(#ymd#), 'YYYY-MM-DD') AS TO_CHAR,
    '' AS BOKG_ID,
    '' AS BOKG_PEN_CNB,
    '' AS BOKG_PEN_NAM,
    '' AS BOKG_DT,
    B.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    F_USR_INFO(B.TRGT_NOP_ENO,'2') AS F_USR_INFO,
    F_DPT_INFO(F_USR_INFO(B.TRGT_NOP_ENO,'5'),'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    F_CODE_NM('1000176',F_USR_INFO(B.TRGT_NOP_ENO,'4')) AS JBGD_NM /* 직급명 */,
    'N' AS BOKG_YN,
    '' AS MEAL_YN,
    'N' AS BOKG_CNL_YN,
    0,
    '' AS BOKG_REGI_DH,
    '' AS OLD_MEAL_YN,
    B.SRCH_SNO
FROM TBCSPMWF005M B WHERE B.TRGT_NOP_ENO NOT IN (SELECT MEAL_PEN_CNB
FROM TBCSPMWF003M WHERE MEAL_DT = #ymd#) ORDER BY B.SRCH_SNO