SELECT A.WSH_BOO_SLN,
    A.BOO_TIT_NM,
    A.PBC_PLAC_NM,
    A.RSCH_RPTP_AUT_NM /* 연구보고서저자명 */,
    NVL(A.BOO_PUR_PR,0) AS BOO_PUR_PR,
    A.PRCHS_QTY /* 구매수량 */,
    F_CODE_NM('1000164',A.WSH_BOO_HNDL_STA_CD) AS F_CODE_NM,
    TO_CHAR(TO_DATE(A.APL_DT),'YYYY-MM-DD') AS TO_CHAR,
    F_USR_INFO(A.APN_CNB,'2') AS F_USR_INFO,
    F_DPT_INFO(F_USR_INFO(A.APN_CNB,'5'),'1') AS F_DPT_INFO
FROM TBCSPQBO004M A /*도서자료기본*/ WHERE 1=1 AND A.COPT_CAT_CD = #strCOPT_CAT_CD# AND A.WSH_BOO_HNDL_STA_CD = #strWSH_BOO_HNDL_STA_CD# AND A.APL_DT &gt;= #strAPL_DTS# AND A.APL_DT &lt;= #strAPL_DTE# AND A.APN_CNB IN (SELECT TRGT_NOP_ENO /* 대상인원직원전산번호 */
FROM TBDCMACM001M WHERE USER_NM LIKE '%'||#strAPN_NAM#||'%') AND A.PBC_PLAC_NM LIKE '%'||#strPBC_PLAC_NM#||'%' AND A.RSCH_RPTP_AUT_NM LIKE '%'||#strATR_NM#||'%' AND A.BOO_TIT_NM LIKE '%'||#strBOO_TIT_NM#||'%' ORDER BY TO_NUMBER(A.WSH_BOO_SLN) DESC