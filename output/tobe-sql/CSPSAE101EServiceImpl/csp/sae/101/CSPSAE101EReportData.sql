SELECT A.EDU_DVSN_NM,
    B.EDU_CAT_NM,
    C.CRS_NM /* 과정명 */,
    C.CRS_CD,
    D.LINK_YR /* 연계년도 */,
    D.EDU_PRD_TXT,
    D.EDU_NBC,
    D.ONTM_NOP_CNT,
    D.TOT_NOP_CNT,
    SUBSTR(E.EDU_BGNG_YMD, 0, 4) || '.' || SUBSTR(E.EDU_BGNG_YMD, 5, 2) || '.' || SUBSTR(E.EDU_BGNG_YMD, 7, 2) || ' ~ ' || SUBSTR(E.EDU_END_YMD, 0, 4) || '.' || SUBSTR(E.EDU_END_YMD, 5, 2) || '.' || SUBSTR(E.EDU_END_YMD, 7, 2) AS SUBSTR
FROM TBCSPSAE101C A , TBCSPSAE102C B , TBCSPSAE103C C , TBCSPSAE201M D , TBCSPSAE201D E WHERE B.EDU_DVSN_CD = A.EDU_DVSN_CD AND C.EDU_DVSN_CD = B.EDU_DVSN_CD AND C.EDU_CAT_CD = B.EDU_CAT_CD AND D.CRS_CD = C.CRS_CD AND E.LINK_YR = D.LINK_YR AND E.CRS_CD = D.CRS_CD AND D.LINK_YR = #LINK_YR# AND A.EDU_DVSN_CD = #strEDU_DVSN_CD# AND B.EDU_CAT_CD = #strEDU_CAT_CD# AND C.CRS_CD = #strEDU_CRS_CD# ORDER BY A.EDU_DVSN_CD ASC, B.EDU_CAT_CD ASC, C.CRS_CD ASC, D.LINK_YR ASC, EDU_DT