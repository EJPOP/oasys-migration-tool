    D.LINK_YR /* 연계년도 */,
    A.EDU_DVSN_CD /* 교육구분코드 */,
    A.EDU_DVSN_NM /* 교육구분명 */,
    B.EDU_CAT_CD /* 교육분류코드 */,
    B.EDU_CAT_NM /* 교육분류명 */,
    C.CRS_CD /* 과정코드 */,
    C.CRS_NM /* 과정명 */,
    E.CRS_CDN /* 과정기수 */,
    D.EDU_GL_TXT /* 교육목표내용 */,
    D.EDU_TG_TXT /* 교육대상내용 */,
