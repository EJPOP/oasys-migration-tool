SELECT T1.MONTH AS MONTH,
    T1.BRU_CD AS BRU_CD,
    T1.BRU_NM AS BRU_NM,
    T1.POS_CLASS_NM AS POS_CLASS_NM,
    T1.AREA_KIND_NM AS AREA_KIND_NM,
    SUM(T1.MAN_DAY) AS MAN_DAY,
    SUM(T1.RL_FARE_AMT) AS RL_FARE_AMT /* 철도운임금액 */,
    SUM(T1.IN_EXP) AS IN_EXP,
    SUM( CASE WHEN T1.BZTRVR_ENO = T1.BZTRP_TL_ENO AND MONTH = T2.MIN_MONTH THEN T2.LEAD_CST ELSE 0 END) AS LEAD_CST /* 지휘비용 */,
    SUM(T1.SPM_CST) AS SPM_CST /* 보충비용 */
FROM TBBADDED027L T1, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    AREA_KIND_CD,
    SUM(NVL(LEAD_CST, 0)) AS LEAD_CST /* 지휘비용 */,
    MIN(SUBSTR(BZTRP_BGNG_YMD, 5, 2)) AS SUBSTR
FROM TBBADDED027L WHERE 1=1 AND BZTRP_BGNG_YMD >= #strBztStrDt# AND BZTRP_END_YMD AND BRU_CD = #strBruCd# GROUP BY BZTRP_YR, BZTRP_SQNO, AREA_KIND_CD) T2 WHERE 1=1 AND T1.BZTRP_YR = T2.BZTRP_YR AND T1.BZTRP_SQNO = T2.BZTRP_SQNO AND T1.AREA_KIND_CD = T2.AREA_KIND_CD AND T1.BZTRP_BGNG_YMD >= #strBztStrDt# AND T1.BZTRP_END_YMD AND T1.BRU_CD = #strBruCd# GROUP BY MONTH, BRU_CD, BRU_NM, POS_CLASS_NM, AREA_KIND_NM /* ORDER BY MONTH, BRU_CD, POS_CLASS_NM, AREA_KIND_NM */