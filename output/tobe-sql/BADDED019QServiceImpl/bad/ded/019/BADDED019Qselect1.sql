SELECT T1.MONTH AS MONTH,
    T1.BRU_CD,
    T1.BRU_NM,
    T1.BRU_NM,
    T1.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    T1.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    T1.ADT_KDCD /* 감사종류코드 */,
    T1.AUD_KND_NM,
    T1.POS_CLASS_NM AS POS_CLASS_NM,
    T1.AREA_KIND_NM AS AREA_KIND_NM,
    T1.MONTH,
    SUM(T1.MAN_DAY) AS MAN_DAY,
    SUM(T1.RL_FARE_AMT) AS RL_FARE_AMT /* 철도운임금액 */,
    SUM(T1.IN_EXP) AS IN_EXP,
    SUM( CASE WHEN T1.BZTRVR_ENO = T1.BZTRP_TL_ENO AND MONTH = T2.MIN_MONTH THEN T2.LEAD_CST ELSE 0 END) AS LEAD_CST /* 지휘비용 */,
    SUM(T1.SPM_CST) AS SPM_CST /* 보충비용 */
FROM TBBADDED027L T1, (SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    AREA_KIND_CD,
    SUM(NVL(LEAD_CST, 0)) AS LEAD_CST /* 지휘비용 */,
    MIN(SUBSTR(BZTRP_BGNG_YMD, 5, 2)) AS SUBSTR
FROM TBBADDED027L WHERE 1=1 AND BZTRP_BGNG_YMD >= #BZTRP_END_YMD# AND BZTRP_END_YMD = #BZTRP_END_YMD# AND T1.BZTRP_END_YMD AND SUBSTR(T1.ADT_KDCD, -1) = SUBSTR(#strAudKndCd#, -1) /* 끝자리로만 조회 - 2016.07.12 yyh */ AND T1.BRU_CD = #strBruCd# AND T1.SRECS_DEPT_CD = #SRECS_DEPT_CD# GROUP BY MONTH, BRU_CD, BRU_NM, SRECS_DEPT_CD, CNSTN_MBCMT_DEPT_NM, ADT_KDCD, AUD_KND_NM, POS_CLASS_NM, AREA_KIND_NM ORDER BY MONTH, BRU_CD, SRECS_DEPT_CD, ADT_KDCD, POS_CLASS_NM, AREA_KIND_NM