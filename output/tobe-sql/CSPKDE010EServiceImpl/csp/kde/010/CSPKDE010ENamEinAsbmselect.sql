    D1.JBGD_NM AS JBGD_NM /* 직급명 */,
    D1.INFO_RLPR_NM AS INFO_RLPR_NM /* 정보관계자명 */,
    SUBSTR(D1.CRU_RRNO_SCRTY,0,6) AS CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    D1.CFM_DDLN_DT AS CFM_DDLN_DT /* 확인기한일 */,
    D1.IDNTY_YN AS IDNTY_YN /* 확인여부 */
