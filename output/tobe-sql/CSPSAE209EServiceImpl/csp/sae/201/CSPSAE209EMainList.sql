SELECT A.SV_TIT_CD,
    A.SV_QEST_NO,
    DECODE(A.SV_CLU_NO, 0, A.GRNDS_EXMN_AEX_SQNO, '') AS GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    DECODE(A.SV_CLU_NO, 0, A.SUB_QEST_NO, '') AS SV_CLU_NO,
    A.SV_CLU_NO,
    A.SV_QEST_TXT
FROM (SELECT A.SV_TIT_CD,
    B.SV_QEST_NO,
    B.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    B.SUB_QEST_NO,
    0,
    B.SV_QEST_TXT
FROM TBCSPSAE121C A , TBCSPSAE122C B WHERE B.SV_TIT_CD = A.SV_TIT_CD UNION ALL SELECT A.SV_TIT_CD,
    B.SV_QEST_NO,
    B.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    B.SUB_QEST_NO,
    C.SV_CLU_NO,
    C.SV_CLU_TXT
FROM TBCSPSAE121C A , TBCSPSAE122C B , TBCSPSAE123C C WHERE B.SV_TIT_CD = A.SV_TIT_CD AND C.SV_QEST_NO = B.SV_QEST_NO AND C.SV_TIT_CD = B.SV_TIT_CD ) A WHERE A.SV_TIT_CD = #strSvTit# ORDER BY A.GRNDS_EXMN_AEX_SQNO, A.SV_QEST_NO, A.SV_CLU_NO