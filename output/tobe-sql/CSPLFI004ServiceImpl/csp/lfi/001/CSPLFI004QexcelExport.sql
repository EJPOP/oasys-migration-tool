SELECT '1040000' AS SALDEPTCD,
    /*급여부처코드*/ B.RCN AS RCN,
    /*개인ID*/ 'Z03' AS DEDUCTIONCD,
    /*공제코드*/ '1' AS ADT_RPTP_EDT_HSTRY_SQNO /* 감사보고서편집이력순번 */,
    /*순번*/ A.DDCTYM AS DDCTYM,
    /*시작년월*/ 'AA' AS SALDIVCD,
    /*급여구분코드*/ NVL(SUM(A.TAM),0) AS TAM,
    /*공제금액*/ '' AS DEDUCTIONDATECD,
    /*공제월구분코드*/ '' AS BANKCD,
    /*은행코드*/ '' AS BANKNUMBER,
    /*계좌번호*/ A.DDCTYM AS DDCTYM,
    /*종료년월*/ '' AS ETC,
    /*비고사항*/ '' AS MODIFYID,
    /*수정자ID*/ '' AS MODIFYDATE /* 수정일시 */
FROM TBCSPLFI004L A, CM_USRINFO B WHERE A.TRGT_NOP_ENO = B.EMPLNUM AND DDCTYM = #deduction_date# AND A.TRGT_NOP_ENO = #TRGT_NOP_ENO# AND B.NAME LIKE '%'||#name#||'%' GROUP BY B.RCN,NAME,DDCTYM ORDER BY B.RCN asc;