SELECT C.DEPT_NM AS DEPT_NM /* 부서명 */,
    /*부서명*/ A.TRGT_NOP_ENO AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    /*전산번호*/ B.NAME AS NAME,
    /*성명*/ NVL(sum(A.TAM),0) AS TAM,
    /*총액*/ A.DDCTYM AS DDCTYM /* 공재월 */
FROM TBCSPLFI004L A, CM_USRINFO B, CM_DEPT C WHERE A.TRGT_NOP_ENO = B.EMPLNUM AND B.DEPT_CD = C.DEPT_CD AND A.DDCTYM = #deduction_date# AND A.TRGT_NOP_ENO = #TRGT_NOP_ENO# AND B.NAME LIKE '%'||#name#||'%' GROUP BY C.DEPT_NM, A.TRGT_NOP_ENO, B.NAME, A.DDCTYM ORDER BY A.TRGT_NOP_ENO asc