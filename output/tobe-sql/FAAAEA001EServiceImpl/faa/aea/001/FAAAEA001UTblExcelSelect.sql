SELECT A.*
FROM ( SELECT SYS_NM AS SYS_NM,
    UNT_SYS_NM AS UNT_SYS_NM,
    TBL_ID AS TBL_ID /* 테이블아이디 */,
    TBL_NM AS TBL_NM,
    TBL_DES AS TBL_DES,
    TO_CHAR(COL_CNT) AS TO_CHAR,
    TO_CHAR(DATA_NOC) AS TO_CHAR,
    TBL_ID /* 테이블아이디 */,
    '1' AS SNO2,
    ROWNUM
FROM TBFAAAEA003M WHERE JRSD_INST_CD = #JRSD_INST_CD# AND SYS_NM = #strSysNm# AND UNT_SYS_NM = #strUntSysNm# AND TBL_NM LIKE '%' || #strTblNm# || '%' UNION ALL SELECT '' AS SYS_NM,
    '[컬럼]' AS UNT_SYS_NM,
    T1.COL_ID AS TBL_ID,
    T1.COL_NM,
    T1.COL_DES,
    '' AS COL_CNT,
    '' AS DATA_NOC,
    T1.TBL_ID /* 테이블아이디 */,
    '2' AS SNO2,
    ROWNUM
FROM TBFAAAEA004M T1 ,TBFAAAEA003M T2 WHERE T1.JRSD_INST_CD = T2.JRSD_INST_CD AND T1.SYS_NM = T2.SYS_NM AND T1.UNT_SYS_NM = T2.UNT_SYS_NM AND T1.TBL_ID = T2.TBL_ID AND T1.JRSD_INST_CD = #JRSD_INST_CD# AND T1.SYS_NM = #strSysNm# AND T1.UNT_SYS_NM = #strUntSysNm# AND T2.TBL_NM LIKE '%' || #strTblNm# || '%' ) A ORDER BY A.SNO1 ,A.SNO2 ,A.SNO3