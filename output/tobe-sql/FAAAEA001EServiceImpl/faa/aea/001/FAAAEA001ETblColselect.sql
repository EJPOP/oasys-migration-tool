SELECT D1.TBL_NM AS TBL_NM,
    D1.TBL_DES AS TBL_DES,
    D1.TBL_ID AS TBL_ID /* 테이블아이디 */,
    (SELECT AGGR_CONCAT(Z.COL_NM, ', ')
FROM TBFAAAEA004M Z WHERE Z.JRSD_INST_CD = D1.JRSD_INST_CD AND Z.SYS_NM = D1.SYS_NM AND Z.UNT_SYS_NM = D1.UNT_SYS_NM AND Z.TBL_ID = D1.TBL_ID AND ROWNUM AND D1.TBL_NM LIKE '%' || #strTblNm# || '%' ORDER BY D1.TBL_NM