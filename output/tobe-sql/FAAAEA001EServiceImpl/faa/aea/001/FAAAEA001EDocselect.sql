SELECT D2.JRSD_INST_CD /* 소관기관코드 */,
    D1.SYS_NM,
    D1.FRS_MDM_SQNO /* 포렌식매체순번 */,
    D1.DOC_NM /* 문서명 */,
    D1.DOC_DES,
    D1.SRECS_FILE_NM /* 파일명 */,
    D1.CVLCPT_DOC_ID /* 민원문서아이디 */,
    D1.ADT_PLAN_DOC_TPCD /* 감사계획문서유형코드 */,
    D1.FL_EXT_NM,
    D1.FL_SZ_TXT AS FL_SZ_TXT
FROM TBFAAAEA009M D1 LEFT OUTER JOIN TBFAAAEA001M D2 ON D1.SYS_NM = D2.SYS_NM AND D1.JRSD_INST_CD = D2.JRSD_INST_CD WHERE 1=1 AND D2.USE_YN = 'Y' AND D2.BAI_ORG_CD = #strCoptOrgCd# AND D1.SYS_NM = #strSysNm# ORDER BY D1.FRS_MDM_SQNO