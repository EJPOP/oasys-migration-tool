    D1.FRS_MDM_SQNO /* 포렌식매체순번 */,
    D1.CON_ORG_NM,
    D1.CON_INF_SYS_NM,
    D1.INN_EXTL_CON_DVSN_CD,
    D1.MAIN_CON_TXT,
    D1.SNDRCV_DVSN_NM,
    D1.CON_MEAN_NM,
    D1.CON_DVSN_NM,
    D2.JRSD_INST_CD /* 소관기관코드 */
FROM TBFAAAEA030M D1 LEFT OUTER JOIN TBFAAAEA001M D2 ON D1.SYS_NM = D2.SYS_NM AND D1.JRSD_INST_CD = D2.JRSD_INST_CD WHERE 1=1 AND D2.USE_YN = 'Y' AND D1.SYS_NM = #strSysNm# AND D2.BAI_ORG_CD = #strCoptOrgCd# ORDER BY D1.FRS_MDM_SQNO