SELECT A.*
FROM ( SELECT CMO_INST_CD AS CMO_INST_CD /* 소관청기관코드 */,
    COPT_ORG_NM AS COPT_ORG_NM,
    SYS_NM AS SYS_NM,
    SYS_DES AS SYS_DES,
    URL_AD AS URL_AD,
    SYS_BGN_DT AS SYS_BGN_DT,
    OP_DPT_NM AS OP_DPT_NM,
    OP_CHGR_NM AS OP_CHGR_NM,
    OP_CHGR_CNIF_NM AS OP_CHGR_CNIF_NM,
    F_ORG_INFO(BAI_ORG_CD, '1') AS F_ORG_INFO,
    SYS_NM,
    '1' AS SNO2,
    ROWNUM
FROM TBFAAAEA001M WHERE JRSD_INST_CD = #JRSD_INST_CD# AND SYS_NM = #strSysNm# UNION ALL SELECT '' AS CMO_INST_CD /* 소관청기관코드 */,
    '[단위시스템]' AS COPT_ORG_NM,
    UNT_SYS_NM,
    UNT_SYS_DES,
    DBMS_KND_NM,
    UNT_SYS_BGN_DT,
    TKCG_DEPT_NM /* 담당부서명 */,
    PIC_NM /* 담당자명 */,
    CHGR_CNIF_NM,
    '' AS BAI_ORG_CD,
    SYS_NM,
    '1' AS SNO2,
    ROWNUM
FROM TBFAAAEA002M WHERE JRSD_INST_CD = #JRSD_INST_CD# AND SYS_NM = #strSysNm# ) A ORDER BY A.SNO1 ,A.SNO2 ,A.SNO3