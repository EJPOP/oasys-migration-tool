SELECT D1.UNT_SYS_NM,
    D1.UNT_SYS_DES,
    D1.TKCG_DEPT_NM /* 담당부서명 */,
    D1.PIC_NM /* 담당자명 */,
    D1.CHGR_CNIF_NM,
    D2.JRSD_INST_CD /* 소관기관코드 */
FROM TBFAAAEA002M D1 LEFT OUTER JOIN TBFAAAEA001M D2 ON D1.SYS_NM = D2.SYS_NM AND D1.JRSD_INST_CD = D2.JRSD_INST_CD WHERE 1=1 AND D2.USE_YN = 'Y' AND D1.SYS_NM = #strSysNm# AND D2.BAI_ORG_CD = #strCoptOrgCd# ORDER BY D1.UNT_SYS_NM