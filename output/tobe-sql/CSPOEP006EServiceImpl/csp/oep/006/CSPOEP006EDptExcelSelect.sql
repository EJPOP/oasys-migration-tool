SELECT T1.SRECS_DEPT_CD AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    T2.CNSTN_MBCMT_DEPT_NM AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    T2.DPT_SNO,
    T1.TOT_CNT AS TOT_CNT,
    T1.CNT1 AS CNT1,
    T1.CNT2 AS CNT2,
    T1.CNT3 AS CNT3,
    T1.CNT4 AS CNT4,
    T1.CNT5 AS CNT5,
    T1.CNT6 AS CNT6,
    T1.TOT_CNT - (T1.CNT1 + T1.CNT2 + T1.CNT3 + T1.CNT4 + T1.CNT5 + T1.CNT6) AS TOT_CNT,
    T1.TOT_CNT - (T1.CNT1 + T1.CNT2 + T1.CNT3 + T1.CNT4 + T1.CNT5) AS TOT_CNT
FROM ( SELECT D1.SRECS_DEPT_CD AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    COUNT(D1.TRGT_NOP_ENO) AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    SUM(CASE WHEN D1.APOLS_DH IS NULL AND D1.NO_APOLS_DVSN_CD = '1' THEN 1 ELSE 0 END) AS APOLS_DH,
    SUM(CASE WHEN D1.APOLS_DH IS NULL AND D1.NO_APOLS_DVSN_CD = '2' THEN 1 ELSE 0 END) AS APOLS_DH,
    SUM(CASE WHEN D1.APOLS_DH IS NULL AND D1.NO_APOLS_DVSN_CD = '3' THEN 1 ELSE 0 END) AS APOLS_DH,
    SUM(CASE WHEN D1.APOLS_DH IS NULL AND D1.NO_APOLS_DVSN_CD = '5' THEN 1 ELSE 0 END) AS APOLS_DH,
    SUM(CASE WHEN D1.APOLS_DH IS NULL AND D1.NO_APOLS_DVSN_CD = '4' THEN 1 ELSE 0 END) AS APOLS_DH,
    SUM(CASE WHEN D1.APOLS_DH IS NOT NULL THEN 1 ELSE 0 END) AS APOLS_DH
FROM TBCSPOEP003L D1 WHERE D1.CLMETG_SRNO = #strClmetgSrno# GROUP BY D1.SRECS_DEPT_CD ) T1 ,TBDCMACM002M T2 WHERE T1.SRECS_DEPT_CD = T2.SRECS_DEPT_CD ORDER BY T2.DPT_SNO