SELECT D1.ADT_YR AS ADT_YR /* 감사년도 */,
    D1.ADT_NO AS ADT_NO /* 감사번호 */,
    D1.DSPS_RQT_SQNO AS DSPS_RQT_SQNO /* 처분요구순번 */,
    F_MNG_KND_AUDYRNO(D1.ADT_YR, D1.ADT_NO, '', '-') ||'-'|| D1.DSPS_RQT_SQNO AS F_MNG_KND_AUDYRNO,
    D2.DSPS_RQT_KDCD AS DSPS_RQT_KDCD /* 처분요구종류코드 */,
    F_CODE_NM('1000002', D2.DSPS_RQT_KDCD) AS DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */,
    D2.DSPS_RQT_NM AS DSPS_RQT_NM /* 처분요구명 */,
    DSPS_RQT_NM_1 AS DSPS_RQT_NM_1 /* 처분요구명1 */,
    DSPS_RQT_NM_2 AS DSPS_RQT_NM_2 /* 처분요구명2 */,
    DSPS_RQT_NM_3 AS DSPS_RQT_NM_3 /* 처분요구명3 */
FROM TB_BADEAR035L D1 ,TB_BADEAR001M D2 WHERE 1=1 AND D1.ADT_YR = D2.ADT_YR AND D1.ADT_NO = D2.ADT_NO AND D1.DSPS_RQT_SQNO = D2.DSPS_RQT_SQNO AND D1.DSPS_RQT_DSCTN_SECD = '8' /* 손망실 */ AND D1.DSPS_RQT_NM_1 = #DSPS_RQT_NM_1# AND D1.DSPS_RQT_NM_2 = #DSPS_RQT_NM_2# AND D1.DSPS_RQT_NM_3 = #DSPS_RQT_NM_3# ORDER BY D1.ADT_YR ,D1.ADT_NO ,D1.DSPS_RQT_SQNO