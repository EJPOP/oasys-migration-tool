SELECT D1.CD,
    D1.CD_NM,
    CASE WHEN D2.AUD_TSK_KND_CD IS NOT NULL THEN 'N' ELSE 'Y' END AS AUD_TSK_KND_CD
FROM (SELECT CD,
    CD_NM,
    OTPT_SEQ
FROM TBDCMACM013C d1 WHERE CMM_CD_DVSN_CD = '1000272' AND CD NOT IN ('000000000', '10000') AND USE_YN = 'Y' ) D1 ,(SELECT AUD_TSK_KND_CD
FROM TBCSPAOE029M WHERE EVL_YR = #strEvlYr# AND HLYR_DVSN_CD = #strHlyrDvsnCd# AND CVLCPT_TASK_SECD = '10001' AND NVL(EVL_STA_CD, '100') != '100' GROUP BY AUD_TSK_KND_CD ) D2 WHERE D1.CD = D2.AUD_TSK_KND_CD (+) ORDER BY D1.OTPT_SEQ, D1.CD