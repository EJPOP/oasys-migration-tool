SELECT DECODE(A.PRPS_ACP_NO,NULL,'',A.PRPS_YE||'-'||A.PRPS_ACP_NO) AS PRPS_ACP_NO /* 접수번호 */,
    A.PRPS_TIT_NM /* 제안제목 */,
    A.NTFCTN_RCPT_YMD /* 통보접수일자 */,
    A.PRPS_STA_CD /* 제안상태코드 */,
    F_CODE_NM('1000075',A.PRPS_STA_CD) AS F_CODE_NM /* 제안상태코드 */,
    A.PRPS_YE /* 제안연도 */,
    A.PRPS_SLN /* 제안연번 */,
    A.PRPS_ACP_NO AS NTFCTN_RCPT_SQNO,
    A.EXAM_APV_DOC_NO /* 검토결재문서번호 */,
    A.EXAM_RSLT_CD
FROM TBCSPGSU001M A /*도서자료기본*/ WHERE 1=1 AND EXAM_DPT_CD = #strDPT_CD# AND A.PRPS_STA_CD &gt;= 'G' AND A.PRPS_STA_CD = #strPRPS_STA_CD# AND A.NTFCTN_RCPT_YMD &gt;= #strACP_DTS# AND A.NTFCTN_RCPT_YMD &lt;= #strACP_DTE# AND A.PRPS_TIT_NM LIKE '%'||#strPRPS_TIT_NM#||'%' ORDER BY TO_NUMBER(A.PRPS_YE) DESC , TO_NUMBER(A.PRPS_ACP_NO) DESC