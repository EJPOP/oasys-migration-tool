/* 방문횟수별 인원목록 */ SELECT '1회' AS NM,
    NVL(TO_CHAR(SUM(CASE WHEN CNT = 1 THEN 1 ELSE 0 END), 'FM999,999'), 0) AS TO_CHAR,
    TO_CHAR(TO_DATE(#fromYmd#), 'YYYY-MM-DD') AS TO_CHAR,
    TO_CHAR(TO_DATE(#toYmd#), 'YYYY-MM-DD') AS TO_CHAR
FROM( SELECT TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    OGDP_DEPT_CD /* 소속부서코드 */,
    INFO_RLPR_NM /* 정보관계자명 */,
    F_CODE_NM('1000173', JBGD_SECD) AS JBGD_NM /* 직급명 */,
    NVL(COUNT(VI_PPS_CD), 0) AS VI_PPS_CD
FROM TBCSPOEP001M WHERE 1=1 AND TRGT_NOP_ENO IS NOT NULL AND VI_DT >= #fromYmd# AND VI_DT = #fromYmd# AND VI_DT = #fromYmd# AND VI_DT = #fromYmd# AND VI_DT = 5 THEN 1 ELSE 0 END), 'FM999,999'), 0) AS CNT1, TO_CHAR(TO_DATE(#fromYmd#), 'YYYY-MM-DD') AS FROMYMD, TO_CHAR(TO_DATE(#toYmd#), 'YYYY-MM-DD') AS TOYMD FROM( SELECT TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    OGDP_DEPT_CD /* 소속부서코드 */,
    INFO_RLPR_NM /* 정보관계자명 */,
    F_CODE_NM('1000173', JBGD_SECD) AS JBGD_NM /* 직급명 */,
    NVL(COUNT(VI_PPS_CD), 0) AS VI_PPS_CD
FROM TBCSPOEP001M WHERE 1=1 AND TRGT_NOP_ENO IS NOT NULL AND VI_DT >= #fromYmd# AND VI_DT = #fromYmd# AND VI_DT <= #toYmd# GROUP BY TRGT_NOP_ENO, OGDP_DEPT_CD,INFO_RLPR_NM,JBGD_SECD )