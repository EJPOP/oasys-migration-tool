SELECT A.GCC_ORG_CD,
    A.LOWT_ORG_NM,
    A.ALL_ORG_NM,
    CASE WHEN A.GCC_SNCL_ORG_CD = #strGccOrgCd# THEN NULL ELSE A.GCC_SNCL_ORG_CD END AS GCC_SNCL_ORG_CD
FROM TBCSPIIT006M A START WITH A.GCC_SNCL_ORG_CD = #strGccOrgCd# CONNECT BY PRIOR A.GCC_ORG_CD = A.GCC_SNCL_ORG_CD ORDER SIBLINGS BY A.ORG_RAK_NO