SELECT A.DATA_DMND_SQNO /* 자료요청순번 */,
    A.DMND_YMD /* 요청일자 */,
    A.RQSTR_ENO /* 요청자직원전산번호 */,
    A.RQS_TIT_NM,
    B.USER_NM /* 사용자명 */,
    C.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    B.SRECS_DEPT_CD AS SRECS_DEPT_CD /* 심사재심의부서코드 */
FROM TBCSPIIT018M A ,TBDCMACM001M B ,TBDCMACM002M C WHERE 1=1 AND A.RQSTR_ENO = B.TRGT_NOP_ENO(+) AND B.SRECS_DEPT_CD = C.SRECS_DEPT_CD(+) AND A.DMND_DEPT_CD = #DMND_DEPT_CD# AND A.PRCS_YMD IS NULL AND A.PRCS_YMD IS NOT NULL ORDER BY DATA_DMND_SQNO DESC