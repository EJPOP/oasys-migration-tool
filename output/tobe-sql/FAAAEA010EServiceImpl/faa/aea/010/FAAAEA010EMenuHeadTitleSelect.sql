SELECT MAX(CASE WHEN CD = '1000' THEN CD ELSE '' END) AS CD,
    MAX(CASE WHEN CD = '1000' THEN $strTxtColNm$ ELSE '' END) AS CD,
    MAX(CASE WHEN CD = '2000' THEN CD ELSE '' END) AS CD,
    MAX(CASE WHEN CD = '2000' THEN $strTxtColNm$ ELSE '' END) AS CD,
    MAX(CASE WHEN CD = '3000' THEN CD ELSE '' END) AS CD,
    MAX(CASE WHEN CD = '3000' THEN $strTxtColNm$ ELSE '' END) AS CD,
    MAX(CASE WHEN CD = '9000' THEN CD ELSE '' END) AS CD,
    MAX(CASE WHEN CD = '9000' THEN $strTxtColNm$ ELSE '' END) AS CD
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000864' AND CD IN ('1000','2000','3000','9000') AND USE_YN = 'Y'