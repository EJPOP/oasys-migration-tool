SELECT A.PRPS_ACP_NO /* 접수번호 */,
    A.PRPS_TIT_NM /* 제안제목 */,
    A.PRPS_PEN_NAM /* 제안자명 */,
    A.PRPS_PEN_CNB /* 제안자전산번호 */,
    A.NTFCTN_RCPT_YMD /* 통보접수일자 */,
    A.PRPS_STA_CD /* 제안상태코드 */,
    A.PRPS_YE /* 제안연도 */,
    A.PRPS_SLN /* 제안연번 */,
    A.ORDER_NO,
    A.EXAM_DT,
    A.EXAM_DPT_NM
FROM ( SELECT DECODE(A.PRPS_ACP_NO,NULL,'',A.PRPS_YE||'-'||A.PRPS_ACP_NO) AS PRPS_ACP_NO /* 접수번호 */,
    A.PRPS_TIT_NM /* 제안제목 */,
    F_USR_INFO(A.PRPS_PEN_CNB,'2') AS F_USR_INFO /* 제안자명 */,
    A.PRPS_PEN_CNB /* 제안자전산번호 */,
    A.NTFCTN_RCPT_YMD /* 통보접수일자 */,
    A.PRPS_STA_CD /* 제안상태코드 */,
    A.PRPS_YE /* 제안연도 */,
    A.PRPS_SLN /* 제안연번 */,
    '1' AS ORDER_NO,
    A.PRPS_ACP_NO AS NTFCTN_RCPT_SQNO,
    A.EXAM_DT,
    F_DPT_INFO(A.EXAM_DPT_CD,'1') AS F_DPT_INFO
FROM TBCSPGSU001M A /*도서자료기본*/ WHERE 1=1 AND PRPS_PEN_CNB = #strCNB# AND A.PRPS_STA_CD = #strPRPS_STA_CD# AND A.NTFCTN_RCPT_YMD &gt;= #strACP_DTS# AND A.NTFCTN_RCPT_YMD &lt;= #strACP_DTE# AND A.PRPS_TIT_NM LIKE '%'||#strPRPS_TIT_NM#||'%' UNION SELECT DECODE(A.PRPS_ACP_NO,NULL,'',A.PRPS_YE||'-'||A.PRPS_ACP_NO) AS PRPS_ACP_NO /* 접수번호 */,
    A.PRPS_TIT_NM /* 제안제목 */,
    SUBSTR(F_USR_INFO(A.PRPS_PEN_CNB,'2'),1,1)||'OO' AS SUBSTR /* 제안자명 */,
    A.PRPS_PEN_CNB /* 제안자전산번호 */,
    A.NTFCTN_RCPT_YMD /* 통보접수일자 */,
    A.PRPS_STA_CD /* 제안상태코드 */,
    A.PRPS_YE /* 제안연도 */,
    A.PRPS_SLN /* 제안연번 */,
    '2' AS ORDER_NO,
    A.PRPS_ACP_NO AS NTFCTN_RCPT_SQNO,
    A.EXAM_DT,
    F_DPT_INFO(A.EXAM_DPT_CD,'1') AS F_DPT_INFO
FROM TBCSPGSU001M A /*도서자료기본*/ WHERE 1=1 AND A.PRPS_PEN_CNB <> #strCNB# AND A.PRPS_STA_CD IN ('N','M') AND #strSEARCH_CD# IS NULL AND A.PRPS_STA_CD = #strPRPS_STA_CD# AND A.NTFCTN_RCPT_YMD &gt;= #strACP_DTS# AND A.NTFCTN_RCPT_YMD &lt;= #strACP_DTE# AND A.PRPS_TIT_NM LIKE '%'||#strPRPS_TIT_NM#||'%' ) A ORDER BY A.ORDER_NO ASC, TO_NUMBER(A.PRPS_YE) DESC, DECODE(A.ORDER_NO,'1',TO_NUMBER(A.PRPS_SLN),TO_NUMBER(A.NTFCTN_RCPT_SQNO)) DESC