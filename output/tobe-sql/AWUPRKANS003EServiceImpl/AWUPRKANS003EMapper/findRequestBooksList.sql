    T1.RSCH_ASMT_TTL /* 연구과제제목 */,
    T1.IMG_DOC_ID,
    T1.LINK_YR /* 연계년도 */,
    TO_CHAR(TO_DATE(T1.CVLCPT_RCPT_YM, 'YYYYMM'), 'YYYY-MM') AS CVLCPT_RCPT_YM /* 민원접수연월 */
FROM TBAWUPRKANS001M T1 WHERE T1.RSCH_RPTP_BBS_ID = 'BBSMSTR_900000000002' AND T1.LINK_YR= #{selYr} AND T1.TIT_NM LIKE '%'||#{titNm}||'%' ORDER BY 1 DESC