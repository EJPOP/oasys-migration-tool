SELECT F_USR_INFO(A.PRPS_PEN_CNB, '1') AS USER_ID /* 사용자아이디 */,
    F_USR_INFO(A.PRPS_PEN_CNB, '2') AS USER_NM /* 사용자명 */,
    F_USR_INFO(A.PRPS_PEN_CNB, '5') AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    F_DPT_INFO(F_USR_INFO(A.PRPS_PEN_CNB, '5'),'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    PRPS_PEN_CNB,
    CTB_RT /* 기여비율 */,
    PRPS_YE,
    PRPS_SLN
FROM TBCSPGSU002L A WHERE 1=1 AND PRPS_YE = #strPRPS_YE# AND PRPS_SLN = #strPRPS_SLN# ORDER BY GRNDS_EXMN_AEX_SQNO ASC