SELECT SRECS_DEPT_CD /* 심사재심의부서코드 */,
    CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    'S' AS HIRK_CTG_ID
FROM TBDCMACM002M WHERE SRECS_DEPT_CD IN ( SELECT HIRK_DPT_CD
FROM TBDCMACM002M WHERE SRECS_DEPT_CD = #SRECS_DEPT_CD# AND HIRK_DPT_CD IS NOT NULL UNION ALL SELECT #SRECS_DEPT_CD#
FROM DUAL ) ORDER BY SRECS_DEPT_CD