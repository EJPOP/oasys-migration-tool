    D2.POST_SEQ,
    D2.CMMT_SEQ,
    D2.CONTENT /* 내용 */,
    SUBSTR(D2.PROF_RBPRSN_CRT_YMD, 1, 4) || '.' || SUBSTR(D2.PROF_RBPRSN_CRT_YMD, 5, 2) || '.' || SUBSTR(D2.PROF_RBPRSN_CRT_YMD, 7, 2) AS PROF_RBPRSN_CRT_YMD /* 증명책임자생성일자 */,
    D2.CRE_USER_NAME /* 등록자 */,
    D2.CRE_USER_ID /* 등록자ID */,
