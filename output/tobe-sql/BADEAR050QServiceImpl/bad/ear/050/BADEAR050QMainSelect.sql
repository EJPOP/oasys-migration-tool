SELECT TO_CHAR(TO_DATE(LINK_REG_DT),'YYYY-MM-DD') AS LINK_REG_DT /* 연계등록일시 */,
    SEND_SERVER_NM,
    REV_SERVER_NM,
    SUM(SUCCESS_CNT) AS SUCCESS_CNT,
    SUM(FAIL_CNT) AS FAIL_CNT
FROM ( SELECT SUBSTR(LINK_REG_DT,0,8) AS LINK_REG_DT /* 연계등록일시 */,
    '오아시스' AS SEND_SERVER_NM,
    '홈페이지' AS REV_SERVER_NM,
    CASE WHEN LINK_STTS_SECD = 'SC' THEN 1 ELSE 0 END AS LINK_STTS_SECD /* 연계상태구분코드 */,
    CASE WHEN LINK_STTS_SECD = 'SE' THEN 1 ELSE 0 END AS LINK_STTS_SECD /* 연계상태구분코드 */
FROM BAI_INF_DB_SND_001 D1 UNION ALL SELECT SUBSTR(LINK_REG_DT,0,8) AS LINK_REG_DT /* 연계등록일시 */,
    '홈페이지' AS SEND_SERVER_NM,
    '오아시스' AS REV_SERVER_NM,
    CASE WHEN LINK_STTS_SECD = 'RC' THEN 1 ELSE 0 END AS LINK_STTS_SECD /* 연계상태구분코드 */,
    CASE WHEN LINK_STTS_SECD = 'RE' THEN 1 ELSE 0 END AS LINK_STTS_SECD /* 연계상태구분코드 */
FROM BAI_INF_DB_RCV_001 ) A WHERE 1=1 AND A.LINK_REG_DT &gt;= #strIF_RAISE_DTS# AND A.LINK_REG_DT &lt;= #strIF_RAISE_DTE# AND SEND_SERVER_NM = '오아시스' AND SEND_SERVER_NM = '홈페이지' GROUP BY LINK_REG_DT,SEND_SERVER_NM,REV_SERVER_NM ORDER BY LINK_REG_DT DESC