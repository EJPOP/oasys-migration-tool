SELECT B.CVLCPT_CLM_SECD /* 민원청구구분코드 */,
    B.TPOF_RCPT_YR /* 제보접수년도 */,
    B.TPOF_RCPT_NO /* 제보접수번호 */,
    A.TPOF_ACTL_RCPT_NO /* 제보실제접수번호 */,
    A.TPOF_RCPT_YR||'-민원-'||A.TPOF_ACTL_RCPT_NO AS NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    A.TPOF_KDCD /* 제보종류코드 */,
    TO_CHAR(A.TPOF_RCPT_DT,'YYYY-MM-DD HH24:MI') AS CRU_RCPT_DT /* 부패접수일시 */,
    A.TPOF_NM /* 제보명 */,
    DECODE(A.TPOF_CLMT_KDCD,'1',A.TPOF_CVLPR_NM,'2',A.TPOF_CVLPR_RPRSV_NM,'3',A.TPOF_CVLPR_RPRSV_NM,'4',A.TPOF_CVLPR_NM) AS TPOF_CVLPR_NM /* 제보민원인명 */,
    B.TPOF_MCA_CLM_SECD /* 제보합건청구구분코드 */,
    B.TPOF_MCA_RCPT_YR /* 제보합건접수년도 */,
    B.TPOF_MCA_CVLCPT_RCPT_NO /* 제보합건민원접수번호 */,
    A.TPOF_STTS_SECD /* 제보상태구분코드 */
FROM TB_CSPDCP101M A, TB_CSPDCP105L B WHERE B.CVLCPT_CLM_SECD = #CLM_SECD# AND B.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND B.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# AND B.TPOF_MCA_CLM_SECD = A.CVLCPT_CLM_SECD AND B.TPOF_MCA_RCPT_YR = A.TPOF_RCPT_YR AND B.TPOF_MCA_CVLCPT_RCPT_NO = A.TPOF_RCPT_NO