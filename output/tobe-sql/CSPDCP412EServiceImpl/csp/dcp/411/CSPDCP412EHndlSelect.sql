SELECT B.CVLCPT_CLM_SECD /* 민원청구구분코드 */,
    B.TPOF_RCPT_YR /* 제보접수년도 */,
    B.TPOF_RCPT_NO /* 제보접수번호 */,
    B.TPOF_SQNO /* 제보순번 */,
    B.TPOF_PRCS_SECD /* 제보처리구분코드 */,
    B.TPOF_ADT_REF_DATA_TRNF_DEPT_CD /* 제보감사참고자료이송부서코드 */,
    DECODE(B.TPOF_ADT_REF_DATA_TRNF_DEPT_CD,NULL,'',SUBSTR(B.TPOF_ADT_REF_DATA_TRNF_DEPT_CD,1,3)||'000') AS TPOF_ADT_REF_DATA_TRNF_DEPT_CD /* 제보감사참고자료이송부서코드 */,
    B.TPOF_TRNF_DEPT_CD /* 제보이송부서코드 */,
    DECODE(B.TPOF_TRNF_DEPT_CD,NULL,'',SUBSTR(B.TPOF_TRNF_DEPT_CD,1,3)||'000') AS TPOF_TRNF_DEPT_CD /* 제보이송부서코드 */,
    B.TPOF_PRCS_SECD /* 제보처리구분코드 */,
    B.TPOF_DOC_ID AS TPOF_DOC_ID /* 제보문서아이디 */,
    '첨부파일보기' AS DOC_NM /* 문서명 */
FROM TB_CSPDCP201M A, TB_CSPDCP205L B WHERE A.CVLCPT_CLM_SECD = #CLM_SECD# AND A.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND A.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# AND A.CVLCPT_CLM_SECD = B.CVLCPT_CLM_SECD AND A.TPOF_RCPT_YR = B.TPOF_RCPT_YR AND A.TPOF_RCPT_NO = B.TPOF_RCPT_NO ORDER BY B.TPOF_SQNO ASC