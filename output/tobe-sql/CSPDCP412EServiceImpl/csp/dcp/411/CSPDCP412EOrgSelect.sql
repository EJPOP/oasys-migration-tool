SELECT B.CVLCPT_CLM_SECD /* 민원청구구분코드 */,
    B.TPOF_RCPT_YR /* 제보접수년도 */,
    B.TPOF_RCPT_NO /* 제보접수번호 */,
    B.TPOF_PRCS_INST_SQNO /* 제보처리기관순번 */,
    B.TPOF_INST_DSCTN_SECD /* 제보기관내역구분코드 */,
    B.TPOF_SQNO /* 제보순번 */,
    B.TPOF_INST_CD /* 제보기관코드 */,
    B.TPOF_PBNST_SECD /* 제보공공기관구분코드 */,
    B.TPOF_JRSD_SG_SECD /* 제보소관부호구분코드 */,
    F_ORG_HRK_NM(B.TPOF_JRSD_SG_SECD) AS F_ORG_HRK_NM,
