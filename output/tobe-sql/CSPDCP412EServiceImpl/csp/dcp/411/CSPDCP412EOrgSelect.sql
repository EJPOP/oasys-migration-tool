    B.TPOF_OCRN_INST_CD /* 제보발생기관코드 */
FROM TB_CSPDCP201M A, TB_CSPDCP206L B WHERE A.CVLCPT_CLM_SECD = #CLM_SECD# AND A.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND A.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# AND B.TPOF_INST_DSCTN_SECD ='1' AND A.CVLCPT_CLM_SECD = B.CVLCPT_CLM_SECD AND A.TPOF_RCPT_YR = B.TPOF_RCPT_YR AND A.TPOF_RCPT_NO = B.TPOF_RCPT_NO