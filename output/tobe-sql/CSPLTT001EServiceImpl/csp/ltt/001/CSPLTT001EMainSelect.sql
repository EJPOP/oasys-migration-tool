SELECT ROWNUM,
    A.NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    A.RSCH_ASMT_TTL /* 연구과제제목 */,
    A.USER_NM /* 사용자명 */,
    A.NAM_OPEN_YN,
    A.SBMSN_YMD /* 제출일자 */,
    A.RLS_YMD /* 공개일자 */,
    A.DEL_DT /* 남은공개일 */,
    A.PRGRS_STTS_SECD /* 진행상태구분코드 */,
    A.PRSS_STA_NM,
    A.TK_YE /* 생각연도 */,
    A.TK_SLN /* 생각연번 */,
    A.EXAM_DT,
    A.EXAM_DPT_NM,
    A.RCMD_CNT,
    A.ENAIS_PST_INQ_NMTM /* ENAIS게시물조회횟수 */,
    A.CMMT_CNT,
    A.DCMD_CNT,
    A.NEW_YN,
    A.CMT_NEW_YN,
    A.EXAM_NEW_YN
FROM ( SELECT DECODE(A.NTFCTN_RCPT_SQNO,NULL,'',A.ACP_YE||'-'||A.NTFCTN_RCPT_SQNO) AS NTFCTN_RCPT_SQNO /*접수번호*/ ,A.RSCH_ASMT_TTL /*제목*/ ,F_USR_INFO(DGUARD.DECRYPT('TB_ENC','EAN', A.TRGT_NOP_ENO),'2') AS USER_NM /*사용자명*/ ,A.NAM_OPEN_YN ,A.SBMSN_YMD /*제출일자*/ ,A.RLS_YMD /*공개일자*/ ,60 - (TO_DATE(TO_CHAR(SYSDATE,'YYYYMMDD')) - TO_DATE(A.RLS_YMD)) AS DEL_DT /*남은공개일*/ ,A.PRGRS_STTS_SECD /*진행상태코드*/ ,F_CODE_NM('1000992',A.PRGRS_STTS_SECD) AS PRSS_STA_NM ,A.TK_YE /*생각연도*/ ,A.TK_SLN /*생각연번*/ ,A.EXAM_DT ,CASE WHEN NVL(A.MDT_YN,'N') = 'Y' AND A.PRGRS_STTS_SECD IN ('M', 'N') THEN F_DPT_INFO(A.MDT_DPT_CD, 1) ELSE F_DPT_INFO(A.EXAM_DPT_CD, 1) END AS EXAM_DPT_NM ,NVL(A.ENAIS_PST_INQ_NMTM, 0) AS ENAIS_PST_INQ_NMTM ,NVL(B.CMMT_CNT, 0) AS CMMT_CNT ,NVL(C.RCMD_CNT, 0) AS RCMD_CNT ,NVL(C.DCMD_CNT, 0) AS DCMD_CNT , CASE WHEN TO_DATE(TO_CHAR(SYSDATE,'YYYYMMDD')) 0 THEN 'Y' ELSE 'N' END AS CMT_NEW_YN , CASE WHEN NVL(A.MDT_YN,'N') = 'Y' AND A.PRGRS_STTS_SECD IN ('M', 'N') AND TO_DATE(TO_CHAR(SYSDATE,'YYYYMMDD')) 'Y' AND A.PRGRS_STTS_SECD IN ('M', 'N') AND TO_DATE(TO_CHAR(SYSDATE,'YYYYMMDD')) AND A.PRGRS_STTS_SECD = #PRGRS_STTS_SECD# AND (A.SBMSN_YMD &gt;= #strSMT_DTS# OR A.SBMSN_YMD IS NULL) AND (A.SBMSN_YMD &lt;= #strSMT_DTE# OR A.SBMSN_YMD IS NULL) AND A.RSCH_ASMT_TTL LIKE '%'||#strTIT_NM#||'%' ORDER BY TO_NUMBER(A.TK_YE) ASC,TO_NUMBER(A.TK_SLN) ASC ) A ORDER BY TO_NUMBER(A.TK_YE) DESC, TO_NUMBER(A.TK_SLN) DESC