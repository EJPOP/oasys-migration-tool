SELECT A.SC_GRN_TG_CD,
    KLD_ACTV_NM,
    A.SET_DVSN_CD,
    A.EVL_CHGR_DVSN_CD,
    A.EVL_SC_DVSN_CD,
    F_CODE_NM('1000588', EVL_SC_DVSN_CD) AS F_CODE_NM,
    F_CODE_NM('1000356', SET_DVSN_CD) AS F_CODE_NM,
    F_CODE_NM('1000589', EVL_CHGR_DVSN_CD) AS F_CODE_NM,
    BSC_SC,
    CASE WHEN SET_DVSN_CD = 'C' THEN MIN_SC || '~' || MAX_SC ELSE '-' END AS SET_DVSN_CD,
    ADD_SC,
    CASE WHEN EVL_CHGR_DVSN_CD = '2' THEN TO_CHAR(BSC_SC) WHEN SET_DVSN_CD = 'B' THEN (MIN_SC + BSC_SC) || '~' || (TO_NUMBER(MAX_SC) + TO_NUMBER(BSC_SC) + TO_NUMBER(ADD_SC)) ELSE (MIN_SC + BSC_SC) || '~' || (TO_NUMBER(MAX_SC) + TO_NUMBER(BSC_SC)) || ' (' || (TO_NUMBER(MIN_SC) + TO_NUMBER(BSC_SC)) || '~' || (TO_NUMBER(MAX_SC) + TO_NUMBER(BSC_SC) + TO_NUMBER(ADD_SC)) || ')' END AS EVL_CHGR_DVSN_CD,
    RFC_BND_SC,
    USE_YN /* 사용여부 */
FROM TBAEPGKM006M AS A, TBAEPGKM012L AS B WHERE 1=1 AND A.SC_GRN_TG_CD = B.SC_GRN_TG_CD EVL_YR = #EVL_YR# EVL_DVSN_CD = #EVL_DVSN_CD#