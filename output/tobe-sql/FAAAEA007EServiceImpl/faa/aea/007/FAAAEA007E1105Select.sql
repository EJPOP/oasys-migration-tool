SELECT D1.ADT_YR /* 감사년도 */,
    D1.ADT_NO /* 감사번호 */,
    D1.DSPS_RQT_SQNO /* 처분요구순번 */,
    D3.ADT_MTTR_NM /* 감사사항명 */,
    F_MNG_KND_AUDYRNO(D1.ADT_YR, D1.ADT_NO, '', '-') ||'-'|| D1.DSPS_RQT_SQNO AS ADT_CLSF_NO /* 감사분류번호 */,
    F_CODE_NM('1000002',D1.DSPS_RQT_KDCD) AS DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */,
    D1.DSPS_RQT_NM /* 처분요구명 */,
    D2.ENFC_YMD /* 시행일자 */
FROM TB_BADEAR001M D1 ,TB_BADEAR002D D2 ,TB_BADDED001M D3 WHERE D1.ADT_YR = D2.ADT_YR AND D1.ADT_NO = D2.ADT_NO AND D1.DSPS_RQT_SQNO = D2.DSPS_RQT_SQNO AND D1.ADT_YR = D3.ADT_YR AND D1.ADT_NO = D3.ADT_NO AND D1.ADT_YR BETWEEN #strAudYrStr# AND #strAudYrEnd# AND D2.CMO_INST_CD = #CMO_INST_CD# AND D1.DSPS_RQT_KDCD = #DSPS_RQT_KDCD# AND D1.DSPS_RQT_NM LIKE '%'||#strTitTxt#||'%' ORDER BY D1.ADT_YR DESC ,D1.ADT_NO DESC ,D1.DSPS_RQT_SQNO