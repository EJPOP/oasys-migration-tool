SELECT D1.INST_CD AS INST_CD /* 기관코드 */,
    D1.FRS_MDM_SQNO AS FRS_MDM_SQNO /* 포렌식매체순번 */,
    D1.ORGA_NM AS ORGA_NM /* 기구명 */,
    D1.RPS_ORGA_NM AS RPS_ORGA_NM /* 전담기구명 */,
    D1.ORGA_OGDP_TYPE_NM AS ORGA_OGDP_TYPE_NM /* 기구소속유형명 */,
    D1.NOW_CONT||'/'||D1.PSCP_NOPE AS NOW_CONT /* 현재개수 */,
    REPLACE(TO_CHAR(D1.BGT_AMT,'999,999,999') ,'','') AS BGT_AMT /* 예산금액 */,
    D1.CNSTN_MBCMT_FAX_NO AS CNSTN_MBCMT_FAX_NO /* 자문위원팩스번호 */,
    D1.OGNZ_CPST_CN AS OGNZ_CPST_CN /* 조직구성내용 */,
    D1.DVDT_TASK_CN AS DVDT_TASK_CN /* 분장업무내용 */,
    D2.JBPS_CD AS JBPS_CD /* 직위코드 */,
    D2.JBGD_NM AS JBGD_NM /* 직급명 */,
    D2.INFO_RLPR_NM AS INFO_RLPR_NM /* 정보관계자명 */,
    CASE WHEN D2.DSPS_RQT_TRPR_BRDT IS NOT NULL THEN SUBSTRING(D2.DSPS_RQT_TRPR_BRDT,0,4)||'.'||SUBSTRING(D2.DSPS_RQT_TRPR_BRDT,5,2)||'.'||SUBSTRING(D2.DSPS_RQT_TRPR_BRDT,7,2) ELSE D2.DSPS_RQT_TRPR_BRDT END AS DSPS_RQT_TRPR_BRDT /* 처분요구대상자생년월일 */,
    D2.MAIN_ACBG_CN AS MAIN_ACBG_CN /* 주요학력내용 */,
    D2.MAIN_CRR_CN AS MAIN_CRR_CN /* 주요경력내용 */,
    D2.APNTMN_SHP_NM AS APNTMN_SHP_NM /* 임용형태명 */,
    (CASE WHEN D2.RPIM_BGNG_YMD IS NOT NULL THEN SUBSTRING(D2.RPIM_BGNG_YMD,0,4)||'.'||SUBSTRING(D2.RPIM_BGNG_YMD,5,2)||'.'||SUBSTRING(D2.RPIM_BGNG_YMD,7,2) ELSE D2.RPIM_BGNG_YMD END) ||' ~ '|| (CASE WHEN D2.RPIM_EXPRY_YMD IS NOT NULL THEN SUBSTRING(D2.RPIM_EXPRY_YMD,0,4)||'.'||SUBSTRING(D2.RPIM_EXPRY_YMD,5,2)||'.'||SUBSTRING(D2.RPIM_EXPRY_YMD,7,2) ELSE D2.RPIM_EXPRY_YMD END) AS RPIM_BGNG_YMD /* 재임시작일자 */,
    D2.QLFC_MTTR_CN AS QLFC_MTTR_CN /* 자격사항내용 */,
    TO_CHAR(D1.FRST_REG_DT, 'YYYY.MM.DD') AS TO_CHAR
FROM TBFAAAEA011M D1 ,TBFAAAEA012M D2 WHERE D1.INST_CD = D2.INST_CD(+) AND D1.FRS_MDM_SQNO = D2.FRS_MDM_SQNO(+) AND D1.DEL_YN = 'N' AND D1.INST_CD = #INST_CD#