SELECT B.OTSD_EXPRT_SECD /* 외부전문가구분코드 */,
    B.OTSD_EXPRT_REG_YR /* 외부전문가등록년도 */,
    B.OTSD_EXPRT_SQNO /* 외부전문가순번 */,
    B.OTSD_EXPRT_SQNO /* 외부전문가순번 */
FROM TB_CSPURA003M A, TB_CSPURA004D B WHERE 1=1 AND A.OTSD_EXPRT_REG_YR = B.OTSD_EXPRT_REG_YR AND A.OTSD_EXPRT_SQNO = B.OTSD_EXPRT_SQNO AND A.OTSD_EXPRT_REG_YR = #OTSD_EXPRT_REG_YR# AND A.OTSD_EXPRT_SQNO = #OTSD_EXPRT_SQNO# ORDER BY B.OTSD_EXPRT_REG_YR DESC, TO_NUMBER(B.OTSD_EXPRT_SQNO) DESC, OTSD_EXPRT_SECD DESC