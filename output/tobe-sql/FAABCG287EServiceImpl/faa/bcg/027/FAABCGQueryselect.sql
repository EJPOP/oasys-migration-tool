SELECT D3.FOLDER_NAME,
    D1.REPORT_CODE,
    D2.REPORT_NAME,
    D2.REPORT_PATH,
    TO_CHAR(SUBSTR(REPLACE(REPLACE(D1.SQL_TEXT,' ',' '),'\t',''),1,2000)) AS TO_CHAR,
    TO_CHAR(SUBSTR(REPLACE(REPLACE(D1.SQL_TEXT,' ',' '),'\t',''),2001,2000)) AS TO_CHAR,
    TO_CHAR(SUBSTR(REPLACE(REPLACE(D1.SQL_TEXT,' ',' '),'\t',''),4001,2000)) AS TO_CHAR
FROM MTX_PROPERTY D1 LEFT OUTER JOIN MTX_REPORT D2 ON D1.REPORT_CODE = D2.REPORT_CODE LEFT OUTER JOIN MTX_FOLDER D3 ON D2.FOLDER_CODE = D3.FOLDER_CODE WHERE FOLDER_NAME IS NOT NULL AND D1.SHEET_NAME LIKE '%DataSource%' AND D3.FOLDER_CODE = #strFolderCode# AND D1.REPORT_CODE LIKE '%'||#strReportCode#||'%' AND D2.REPORT_NAME LIKE '%'||#strReportName#||'%' ORDER BY D3.FOLDER_NAME, D2.REPORT_SEQ