SELECT C.BOARD_ID,
    C.BOARD_TITLE,
    C.BOARD_SUB_TITLE,
    C.USE_YN /* 사용여부 */
FROM CTT_BOARD_SKIN AS A, CTT_BOARD_TPL AS B, CTT_BULLETIN_BOARD AS C WHERE 1=1 AND A.SKIN_ID = B.SKIN_ID AND A.SKIN_ID = C.SKIN_ID AND C.CAFE_ID IS NULL AND A.SKIN_ID IN ('5', '10') C.BOARD_TITLE LIKE '%' || #BOARD_TITLE# || '%' C.USE_YN = #USE_YN# GROUP BY C.BOARD_ID, C.BOARD_TITLE, C.USE_YN, C.BOARD_SUB_TITLE