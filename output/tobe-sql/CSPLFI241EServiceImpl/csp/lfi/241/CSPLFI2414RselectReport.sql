SELECT A.CD_NM,
    NVL(C.CNT,0) AS CNT,
    NVL(c.TOTAL,0) AS TOTAL,
    #toYm# AS #TOYM#
FROM ( SELECT SUBSTRING(A.CD,1,3) AS SUBSTRING,
    B.ABR_DPT_NM_2 /* 20181104 EUNOK CD_NM -> B.ABR_DPT_NM_2 */,
    B.DPT_SNO,
    B.CRU_SORT_SQNO /* 부패정렬순번 */
FROM TBDCMACM013C A,TBDCMACM002M B WHERE A.CMM_CD_DVSN_CD = '1000144' AND (A.CD LIKE '___000' OR A.CD ='770770') AND A.USE_YN !='N' AND A.CD = B.SRECS_DEPT_CD )A, ( SELECT SUBSTRING(B.SRECS_DEPT_CD,1,3) AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    SUM(CNT) AS CNT,
    SUM(TOTAL) AS TOTAL
FROM( SELECT SUBSTRING(A.SRECS_DEPT_CD,1,3) AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    COUNT(A.TRGT_NOP_ENO) AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    SUM(A.PMT_AM) AS PMT_AM
FROM TBCSPLFI104L A, TBDCMACM001M B WHERE A.TRGT_NOP_ENO = B.TRGT_NOP_ENO AND PMT_YM = #toYm# AND A.ALW_DVSN_CD = #AlwDvsnCd# AND A.PMT_AM != 0 GROUP BY SUBSTRING(A.SRECS_DEPT_CD,1,3) )B GROUP BY SUBSTRING(B.SRECS_DEPT_CD,1,3) )C WHERE A.CD= C.SRECS_DEPT_CD(+) ORDER BY A.DPT_SNO ASC, A.CRU_SORT_SQNO ASC, A.CD ASC