SELECT D2.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    D3.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    D4.CD_NM,
    D1.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    D2.USER_NM /* 사용자명 */,
    MAX(DECODE(D1.ALW_DVSN_CD, '9993', D1.ALW_AM, 0)) AS ALW_DVSN_CD,
    '모니터링활동비Ⅱ' AS ALW_DVSN_CD_NM,
    '1' AS CNT
FROM TBCSPLFI102M D1 , TBDCMACM001M D2 , ( SELECT DISTINCT D11.SRECS_DEPT_CD AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    D11.ABR_DPT_NM_2 AS CNSTN_MBCMT_DEPT_NM /* 20181104 EUNOK D11.DPT_NM -> D11.ABR_DPT_NM_2 변경 */,
    D11.DPT_SNO,
    D11.CRU_SORT_SQNO /* 부패정렬순번 */
FROM TBDCMACM002M D11 , TBDCMACM001M D12 WHERE D11.SRECS_DEPT_CD = D12.SRECS_DEPT_CD AND D12.SRECS_DEPT_CD != '880880' AND D12.BAI_USR_YN = 'Y' AND D11.USE_YN = 'Y' AND ( D12.RTRM_YMD IS NULL OR D12.RTRM_YMD = '') AND D12.TRGT_NOP_ENO IN ( SELECT TRGT_NOP_ENO /* 대상인원직원전산번호 */
FROM TBCSPLFI102M WHERE ALW_DVSN_CD IN ( '9992', '9993' ) AND ALW_AM > 0 ) ORDER BY SRECS_DEPT_CD ) D3 , ( SELECT CD,
    CD_NM
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000173' ) AS D4 WHERE D1.TRGT_NOP_ENO = D2.TRGT_NOP_ENO AND D2.SRECS_DEPT_CD = D3.SRECS_DEPT_CD AND D2.JBGD_SECD = D4.CD AND D1.ALW_DVSN_CD IN ( '9992', '9993' ) AND D2.SRECS_DEPT_CD != '880880' AND ( D2.RTRM_YMD IS NULL OR D2.RTRM_YMD = '') AND D2.BAI_USR_YN != 'N' AND D1.ALW_AM > 0 AND D3.SRECS_DEPT_CD = #SRECS_DEPT_CD# AND D2.TRGT_NOP_ENO = #TRGT_NOP_ENO# GROUP BY D2.SRECS_DEPT_CD , D3.CNSTN_MBCMT_DEPT_NM , D3.DPT_SNO , D3.CRU_SORT_SQNO , D2.JBGD_SECD , D4.CD_NM , D1.TRGT_NOP_ENO , D2.USER_NM ORDER BY D3.DPT_SNO, D3.CRU_SORT_SQNO, SRECS_DEPT_CD, JBGD_SECD, TRGT_NOP_ENO