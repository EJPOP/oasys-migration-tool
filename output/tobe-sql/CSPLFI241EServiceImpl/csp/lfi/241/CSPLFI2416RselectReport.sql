SELECT A.CD_NM,
    NVL(B.CNT1,0) AS CNT1,
    NVL(B.TOTAL1,0) AS TOTAL1,
    NVL(B.CNT2,0) AS CNT2,
    NVL(B.TOTAL2,0) AS TOTAL2,
    NVL(B.CNT3,0) AS CNT3,
    NVL(B.TOTAL3,0) AS TOTAL3,
    NVL(B.CNT4,0) AS CNT4,
    NVL(B.TOTAL4,0) AS TOTAL4,
    NVL(B.CNT1 + B.CNT2 + B.CNT3 + B.CNT4, 0) AS CNT1,
    NVL(B.TOTAL1 + B.TOTAL2 + B.TOTAL3 + B.TOTAL4, 0) AS TOTAL1,
    #toYm# AS #TOYM#
FROM ( SELECT SUBSTRING(A.CD,1,3) AS SUBSTRING,
    B.ABR_DPT_NM_2 /* 20181104 EUNOK CD_NM -> B.ABR_DPT_NM_2 */,
    B.DPT_SNO,
    B.CRU_SORT_SQNO /* 부패정렬순번 */
FROM TBDCMACM013C A,TBDCMACM002M B WHERE A.CMM_CD_DVSN_CD = '1000144' AND (A.CD LIKE '___000' OR A.CD ='770770') AND A.USE_YN !='N' AND A.CD = B.SRECS_DEPT_CD ) A, ( SELECT SUBSTRING(B.SRECS_DEPT_CD,1,3) AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    SUM(CNT1) AS CNT1,
    SUM(TOTAL1) AS TOTAL1,
    SUM(CNT2) AS CNT2,
    SUM(TOTAL2) AS TOTAL2,
    SUM(CNT3) AS CNT3,
    SUM(TOTAL3) AS TOTAL3,
    SUM(CNT4) AS CNT4,
    SUM(TOTAL4) AS TOTAL4
FROM( SELECT SUBSTRING(A.SRECS_DEPT_CD,1,3) AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    COUNT(CASE WHEN A.ALW_DVSN_CD = '9992' THEN A.TRGT_NOP_ENO END) AS ALW_DVSN_CD,
    SUM(CASE WHEN A.ALW_DVSN_CD = '9992' THEN A.PMT_AM ELSE 0 END) AS ALW_DVSN_CD,
    COUNT(CASE WHEN A.ALW_DVSN_CD = '9993' THEN A.TRGT_NOP_ENO END) AS ALW_DVSN_CD,
    SUM(CASE WHEN A.ALW_DVSN_CD = '9993' THEN A.PMT_AM ELSE 0 END) AS ALW_DVSN_CD,
    COUNT(CASE WHEN A.ALW_DVSN_CD = '9994' THEN A.TRGT_NOP_ENO END) AS ALW_DVSN_CD,
    SUM(CASE WHEN A.ALW_DVSN_CD = '9994' THEN A.PMT_AM ELSE 0 END) AS ALW_DVSN_CD,
    COUNT(CASE WHEN A.ALW_DVSN_CD = '9995' THEN A.TRGT_NOP_ENO END) AS ALW_DVSN_CD,
    SUM(CASE WHEN A.ALW_DVSN_CD = '9995' THEN A.PMT_AM ELSE 0 END) AS ALW_DVSN_CD
FROM TBCSPLFI104L A, TBDCMACM001M B WHERE A.TRGT_NOP_ENO = B.TRGT_NOP_ENO AND PMT_YM = #toYm# AND A.ALW_DVSN_CD IN ('9992', '9993', '9994', '9995') AND A.PMT_AM != 0 GROUP BY SUBSTRING(A.SRECS_DEPT_CD,1,3), A.TRGT_NOP_ENO )B GROUP BY SUBSTRING(B.SRECS_DEPT_CD,1,3) ) B WHERE A.CD= B.SRECS_DEPT_CD(+) ORDER BY A.DPT_SNO ASC, A.CRU_SORT_SQNO ASC, A.CD ASC