    D4.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    D3.CNT,
    D3.ALW_AM_SUM
FROM ( SELECT SUBSTR(D2.SRECS_DEPT_CD, 1, 3) || '000' AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    COUNT(D1.TRGT_NOP_ENO) AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    NVL(SUM(D1.ALW_AM),0) AS ALW_AM
FROM TBCSPLFI102M D1 , TBDCMACM001M D2 WHERE D1.TRGT_NOP_ENO = D2.TRGT_NOP_ENO AND D1.ALW_DVSN_CD = '9991' AND ( D2.RTRM_YMD IS NULL OR D2.RTRM_YMD = '') AND D2.SRECS_DEPT_CD != '880880' AND D2.BAI_USR_YN != 'N' AND D1.ALW_AM > 0 GROUP BY SUBSTR(D2.SRECS_DEPT_CD, 1, 3) ) D3 , ( SELECT SUBSTR(D1.SRECS_DEPT_CD, 1, 3) || '000' AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    D1.ABR_DPT_NM_2 AS CNSTN_MBCMT_DEPT_NM /* 20181104 EUNOK D1.DPT_NM -> D1.ABR_DPT_NM_2 변경 */,
    D1.DPT_SNO,
    D1.CRU_SORT_SQNO /* 부패정렬순번 */
FROM TBDCMACM002M D1 WHERE ( D1.SRECS_DEPT_CD LIKE '___000' OR D1.SRECS_DEPT_CD = '770770' ) ) D4 WHERE D3.SRECS_DEPT_CD = D4.SRECS_DEPT_CD ORDER BY D4.DPT_SNO, D4.CRU_SORT_SQNO, SRECS_DEPT_CD