    DECODE(D4.SRECS_DEPT_CD, '770000', '파견자', D4.CNSTN_MBCMT_DEPT_NM ) AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    NVL(D3.CNT_9994,0) AS CNT_9994,
    NVL(D3.ALW_AM_9994_SUM,0) AS ALW_AM_9994_SUM,
    NVL(D3.CNT_9995,0) AS CNT_9995,
    NVL(D3.ALW_AM_9995_SUM,0) AS ALW_AM_9995_SUM,
    NVL(D3.CNT_9994 + D3.CNT_9995,0) AS CNT_9994,
    NVL(D3.ALW_AM_9994_SUM + D3.ALW_AM_9995_SUM,0) AS ALW_AM_9994_SUM
FROM ( SELECT SUBSTR(D2.SRECS_DEPT_CD, 1, 3) || '000' AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    SUM(CASE WHEN D1.ALW_DVSN_CD = '9994' AND D1.ALW_AM > 0 THEN 1 ELSE 0 END) AS ALW_DVSN_CD,
    SUM(CASE WHEN D1.ALW_DVSN_CD = '9995' AND D1.ALW_AM > 0 THEN 1 ELSE 0 END) AS ALW_DVSN_CD,
    SUM(DECODE(D1.ALW_DVSN_CD, '9994', D1.ALW_AM, 0)) AS ALW_DVSN_CD,
    SUM(DECODE(D1.ALW_DVSN_CD, '9995', D1.ALW_AM, 0)) AS ALW_DVSN_CD
FROM TBCSPLFI102M D1 , TBDCMACM001M D2 WHERE D1.TRGT_NOP_ENO = D2.TRGT_NOP_ENO AND D1.ALW_DVSN_CD IN ( '9994', '9995' ) AND ( D2.RTRM_YMD IS NULL OR D2.RTRM_YMD = '') AND D2.SRECS_DEPT_CD != '880880' AND D2.BAI_USR_YN != 'N' GROUP BY SUBSTR(D2.SRECS_DEPT_CD, 1, 3) ) D3 , ( SELECT SUBSTR(D1.SRECS_DEPT_CD, 1, 3) || '000' AS SRECS_DEPT_CD /* 심사재심의부서코드 */,
    D1.ABR_DPT_NM_2 AS CNSTN_MBCMT_DEPT_NM /* 20181104 EUNOK D1.DPT_NM -> D1.ABR_DPT_NM_2 변경 */,
    D1.DPT_SNO,
    D1.CRU_SORT_SQNO /* 부패정렬순번 */
FROM TBDCMACM002M D1 WHERE ( D1.SRECS_DEPT_CD LIKE '___000' OR D1.SRECS_DEPT_CD = '770770' ) AND D1.USE_YN = 'Y' ) D4 WHERE D3.SRECS_DEPT_CD = D4.SRECS_DEPT_CD ORDER BY D4.DPT_SNO, D4.CRU_SORT_SQNO, SRECS_DEPT_CD