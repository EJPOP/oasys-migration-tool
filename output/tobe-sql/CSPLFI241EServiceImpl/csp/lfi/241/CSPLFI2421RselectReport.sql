SELECT PMT_YM,
    COUNT(B.TRGT_NOP_ENO) AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    NVL(SUM(A.PMT_AM), 0) AS PMT_AM,
    #fromYm# AS #FROMYM#,
    #toYm# AS #TOYM#
FROM TBCSPLFI104L A, TBDCMACM001M B WHERE A.TRGT_NOP_ENO = B.TRGT_NOP_ENO AND PMT_YM BETWEEN #fromYm# AND #toYm# AND A.ALW_DVSN_CD = #AlwDvsnCd# GROUP BY PMT_YM ORDER BY PMT_YM