SELECT TO_CHAR(WRT_DT, 'YYYY-MM-DD') AS WRT_DT /* 작성일시 */,
    SUBSTR(WRT_DT,1,4)||'-'||FRS_MDM_SQNO AS SUBSTR,
    GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    INFO_RLPR_NM /* 정보관계자명 */,
    CRU_TELNO /* 부패전화번호 */,
    CRPT_TYP_NM,
    INSP_TXT,
    CVLCPT_DOC_ID /* 민원문서아이디 */,
    ADT_PLAN_DOC_TPCD /* 감사계획문서유형코드 */,
    EXAM_TXT,
    RSCH_ASMT_TTL /* 연구과제제목 */,
    HNDL_STEP_CD,
    F_CODE_NM('1000658', HNDL_STEP_CD) AS F_CODE_NM,
    FIN_DH
FROM TBCSPCIS002M WHERE 1=1 AND TO_CHAR(WRT_DT, 'YYYYMMDD') BETWEEN #ACP_DH_START# AND #ACP_DH_END# AND FIN_DH >= #FIN_DH_ST# AND FIN_DH BETWEEN #FIN_DH_ST# AND #FIN_DH_END# AND HNDL_STEP_CD = #CD# AND INFO_RLPR_NM LIKE '%'|| #NAME# ||'%' AND RSCH_ASMT_TTL LIKE '%'|| #TITLE# ||'%' AND CRPT_TYP_NM LIKE '%'|| #CRPT_TYP_NM# ||'%' ORDER BY WRT_DT DESC