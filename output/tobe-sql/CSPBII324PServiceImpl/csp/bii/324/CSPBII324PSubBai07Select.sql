SELECT A.INFO_YR /* 정보년도 */,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    TO_CHAR(TO_DATE(A.EVL_DT, 'YYYYMMDD'), 'YYYY.MM.DD') AS TO_CHAR,
    F_CODE_NM('1000070', A.EVL_MTRL_DVSN_CD) AS F_CODE_NM,
    (SELECT C.CNSTN_MBCMT_DEPT_NM AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */
FROM TBDCMACM002M C WHERE C.SRECS_DEPT_CD = A.EVL_DPT_CD) AS EVL_DPT_NM , (SELECT C.PROF_RBPRSN_INST_NM /* 증명책임자기관명 */
FROM TBDCMACM015M C WHERE C.INST_CD = A.OGDP_INST_CD) AS TPOF_OGDP_INST_NM , A.JBPS_NM FROM TBCSPBII500L A WHERE A.CRU_RRNO_SCRTY = #CRU_RRNO_SCRTY# ORDER BY A.INFO_YR DESC, A.FRS_MDM_SQNO DESC