/*감사청구미처리*/ SELECT STTC_DVSN_CD,
    NVL(MAX(COL_1_1),0) + NVL(MAX(COL_2_1),0) AS COL_1_1,
    /*국민미처리*/ NVL(MAX(COL_3_1),0) + NVL(MAX(COL_4_1),0) AS COL_3_1,
    /*국민기한경과*/ NVL(MAX(COL_5_1),0) + NVL(MAX(COL_6_1),0) AS COL_5_1,
    /*공익미처리*/ NVL(MAX(COL_7_1),0) + NVL(MAX(COL_8_1),0) AS COL_7_1 /* 공익기한경과 */
FROM ( SELECT STTC_DVSN_CD,
    DECODE(STTC_SNO,2,STTC_NM_3,NULL) AS STTC_SNO,
    DECODE(STTC_SNO,2,STTC_NM_6,NULL) AS STTC_SNO,
    DECODE(STTC_SNO,2,STTC_NM_5,NULL) AS STTC_SNO,
    DECODE(STTC_SNO,2,STTC_NM_8,NULL) AS STTC_SNO,
    DECODE(STTC_SNO,3,STTC_NM_3,NULL) AS STTC_SNO,
    DECODE(STTC_SNO,3,STTC_NM_6,NULL) AS STTC_SNO,
    DECODE(STTC_SNO,3,STTC_NM_5,NULL) AS STTC_SNO,
    DECODE(STTC_SNO,3,STTC_NM_8,NULL) AS STTC_SNO
FROM TBDCMBST002M WHERE STATS_YR = #STATS_YR# AND STTC_MN = #strMn# AND STTC_WEK_NO = #strWekNo# AND STTC_DVSN_CD = #strSttcDvsnCd# ) GROUP BY STTC_DVSN_CD