SELECT T1.INFO_YR /* 정보년도 */,
    T1.TPOF_CYCL /* 제보차수 */,
    T1.CABT_GP_CD,
    T1.CABT_CD,
    T1.QST_NO,
    F_CODE_NM('1000996',T1.CABT_GP_CD) || chr(13)||chr(10) || '[' || F_CODE_NM('1000997',T1.CABT_CD) || ']' AS F_CODE_NM,
    T1.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    T1.QST,
    T2.CHO_ITM
FROM TBCSPSAE803M T1, TBCSPSAE803D T2 WHERE T1.INFO_YR = T2.INFO_YR(+) AND T1.TPOF_CYCL = T2.TPOF_CYCL(+) AND T1.CABT_GP_CD = T2.CABT_GP_CD(+) AND T1.CABT_CD = T2.CABT_CD(+) AND T1.QST_NO = T2.QST_NO(+) AND T1.INFO_YR = #INFO_YR# AND T1.TPOF_CYCL = #TPOF_CYCL# AND T2.TRGT_NOP_ENO(+) = #CNB# ORDER BY CABT_CD, GRNDS_EXMN_AEX_SQNO ASC