SELECT rownum,
    T1.EVL_YR /* 평가년도 */,
    T1.HLYR_DVSN_CD /* 반기구분코드 */,
    T1.EVL_DGE /* 평가차수 */,
    T1.EVL_TG_DPT_CD /* 평가대상부서코드 */,
    F_DPT_INFO(T1.EVL_TG_DPT_CD,'1') AS F_DPT_INFO,
    T1.UNT_TSK_SNO /* 단위업무순번 */,
    T1.UNT_TSK_NM /* 단위업무명 */,
    T1.UNT_TSK_TXT /* 단위업무내용 */,
    COUNT(1) OVER () AS 1
FROM TBRESRSP002M T1 WHERE 1=1 AND T1.EVL_YR = #EVL_YR# AND T1.HLYR_DVSN_CD = #HLYR_DVSN_CD# AND T1.EVL_DGE = #EVL_DGE# AND T1.EVL_TG_DPT_CD = #EVL_TG_DPT_CD# AND T1.UNT_TSK_SNO IN #UNT_TSK_SNO[]# AND T1.ATRZ_DOC_NO = #ATRZ_DOC_NO# ORDER BY T1.UNT_TSK_SNO ASC