FROM TBDCMACM001M WHERE USER_ID = ADM_ID_2) AS NAM2, DECODE(A.ADM_TNB_2, NULL, (SELECT CRU_TELNO /* 부패전화번호 */
FROM TBDCMACM001M WHERE USER_ID = A.ADM_ID_2), A.ADM_TNB_2) AS TNB2 FROM TBCSPOEP011M A, TBDCMACM002M B WHERE 1=1 AND A.MNG_DEPT_CD = B.SRECS_DEPT_CD(+) AND A.CFR_RM_INSPC_RM_DVSN_CD NOT IN ('1','2') ORDER BY A.CFR_RM_INSPC_RM_DVSN_CD,A.LOUNGE_SRCH_SEQ