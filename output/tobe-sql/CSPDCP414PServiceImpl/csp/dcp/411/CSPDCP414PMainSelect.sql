SELECT A.CVLCPT_CLM_SECD /* 민원청구구분코드 */,
    A.TPOF_RCPT_YR /* 제보접수년도 */,
    A.TPOF_RCPT_NO /* 제보접수번호 */,
    A.TPOF_RCPT_YR||'-민원-'||A.TPOF_ACTL_RCPT_NO AS NTFCTN_RCPT_SQNO /* 통보접수순번 */,
    TO_CHAR(A.TPOF_RCPT_DT,'YYYY-MM-DD HH24:MI') AS CRU_RCPT_DT /* 부패접수일시 */,
    F_CODE_NM('1000056',A.TPOF_RCPT_PATH_SECD) AS F_CODE_NM,
    DECODE(A.TPOF_CLMT_KDCD,'1',A.TPOF_CVLPR_NM,'2',A.TPOF_CVLPR_RPRSV_NM,'3',A.TPOF_CVLPR_RPRSV_NM,'4',A.TPOF_CVLPR_NM) AS TPOF_CVLPR_NM /* 제보민원인명 */,
    DECODE(A.TPOF_CVLPR_ZIP,NULL,'','(우)'||A.TPOF_CVLPR_ZIP||' '||A.TPOF_CVLPR_ADDR||' '||A.TPOF_CVLPR_DADDR) AS OTSD_EXPRT_ADDR /* 외부전문가주소 */,
    A.TPOF_NM /* 제보명 */,
    B.TPOF_CN /* 제보내용 */,
    A.TPOF_SMG_CVLCPT_APLY_NO /* 제보신문고민원신청번호 */,
    A.TPOF_CVLPR_TELNO /* 제보민원인전화번호 */,
    A.TPOF_CVLPR_MPNO /* 제보민원인휴대전화번호 */,
    CASE WHEN ( SELECT COUNT(FRS_MDM_ATCH_FILE_ID) AS FRS_MDM_ATCH_FILE_ID /* 포렌식매체첨부파일아이디 */
FROM ECM_FILE_LIST_VIEW2 X WHERE A.TPOF_DOC_ID = X.CVLCPT_DOC_ID ) > 0 THEN 'Y' ELSE 'N' END AS ATCH_YN FROM TB_CSPDCP101M A ,TB_CSPDCP102M B WHERE 1=1 AND A.CVLCPT_CLM_SECD = #CLM_SECD# AND A.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND A.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# AND A.CVLCPT_CLM_SECD = B.CVLCPT_CLM_SECD AND A.TPOF_RCPT_YR = B.TPOF_RCPT_YR AND A.TPOF_RCPT_NO = B.TPOF_RCPT_NO