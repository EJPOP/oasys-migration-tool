SELECT T1.TASK_KDCD /* 업무종류코드 */,
    F_CODE_NM('1000787', T1.TASK_KDCD) AS F_CODE_NM,
    T1.CVLCPT_TASK_STP_SECD /* 민원업무단계구분코드 */,
    F_CODE_NM('1000785', T1.CVLCPT_TASK_STP_SECD) AS SLCT_TASK_STP_NM /* 청탁업무단계명 */,
    T1.TASK_DOC_SECD /* 업무문서구분코드 */,
    F_CODE_NM('1000786', T1.TASK_DOC_SECD) AS F_CODE_NM,
    T1.MTIF_INFO_SQNO /* 메타정보정보순번 */,
    T1.MTIF_INFO_EXPLN_CN /* 메타정보정보설명내용 */,
    CASE WHEN T1.MNGR_YN = 'Y' THEN '표준' WHEN T1.MNGR_YN = 'N' THEN '비표준' END AS MNGR_YN /* 관리자여부 */,
    T1.MNGR_YN /* 관리자여부 */,
    T1.RGTR_ID /* 등록자아이디 */,
    T1.USE_YN /* 사용여부 */,
    T2.MTIF_INFO_CN /* 메타정보정보내용 */
FROM TB_FDMADM004M T1, TB_FDMADM005M T2 WHERE 1=1 AND T1.TASK_KDCD = T2.TASK_KDCD AND T1.CVLCPT_TASK_STP_SECD = T2.TASK_STP_SECD AND T1.TASK_DOC_SECD = T2.TASK_DOC_SECD AND T1.MTIF_INFO_SQNO = T2.MTIF_INFO_SQNO AND (T1.MNGR_YN = 'Y' OR T1.RGTR_ID = (SELECT USR.USER_ID /* 사용자아이디 */
FROM TBDCMACM001M USR WHERE USR.TRGT_NOP_ENO = #TRGT_NOP_ENO# ) ) AND T1.TASK_KDCD = #TASK_KDCD# AND T1.CVLCPT_TASK_STP_SECD = #CVLCPT_TASK_STP_SECD# AND T1.TASK_DOC_SECD = #TASK_DOC_SECD# ORDER BY MNGR_YN DESC ,T1.MTIF_INFO_SQNO DESC;