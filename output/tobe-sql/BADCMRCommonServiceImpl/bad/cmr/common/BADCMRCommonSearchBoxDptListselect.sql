SELECT D1.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    D1.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    NVL(D2.HIRK_DPT_CD, D1.SRECS_DEPT_CD) AS HIRK_DPT_CD
FROM ( SELECT SRECS_DEPT_CD /* 심사재심의부서코드 */,
    CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */
FROM TBDCMACM002M WHERE NVL(USE_YN, 'N') = 'Y' ORDER BY SRECS_DEPT_CD ) D1 , TBDCMACM002M D2 WHERE D1.SRECS_DEPT_CD = D2.SRECS_DEPT_CD