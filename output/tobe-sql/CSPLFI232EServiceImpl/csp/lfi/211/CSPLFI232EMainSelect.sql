SELECT A.CD,
    #TRGT_NOP_ENO# AS TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    NVL(B.ALW_AM, 0) AS ALW_AM,
    NVL(B.FIX_YN, 'N') AS FIX_YN
FROM ( SELECT CD,
    CD_NM,
    USR_DFN_TXT_2
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000145' AND CD!= '000000000' )A, ( SELECT ALW_DVSN_CD,
    TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    NVL(ALW_AM, 0) AS ALW_AM,
    FIX_YN
FROM TBCSPLFI102M WHERE TRGT_NOP_ENO = #TRGT_NOP_ENO# )B WHERE 1=1 AND A.CD = B.ALW_DVSN_CD(+) ORDER BY A.USR_DFN_TXT_2