SELECT A.ADT_SBMSN_DATA_ID /* 감사제출자료아이디 */,
    B.ATCH_FILE_IDNO /* 첨부파일식별번호 */,
    B.SRECS_FILE_NM /* 파일명 */,
    B.FILE_HASH_VL /* 파일해시값 */,
    B.FILE_DNV_VL /* 파일DN값 */,
    B.FILE_SIGN_VL /* 파일서명값 */,
    B.ENAIS_PST_ATCH_FILE_ID /* 첨부파일아이디 */,
    B.DOC_ID /* 문서아이디 */,
    B.EVI_DCMNT_SECD /* 증거서류구분코드 */,
    F_MNG_KND_AUDYRNO(B.ADT_YR, B.ADT_NO, NULL, '-') AS ADT_CLSF_NO /* 감사분류번호 */,
    DECODE(B.DATA_SBMSN_RCPT_YR,NULL,NULL,B.DATA_SBMSN_RCPT_YR||'-심사-'||B.DATA_SBMSN_RCPT_SQNO) AS DATA_SBMSN_RCPT_YR /* 자료제출접수년도 */,
    B.REG_YMD /* 등록일자 */,
    B.LAST_REG_YMD /* 최종등록일자 */,
    (SELECT AS ADT_MTTR_NM /* 감사사항명 */
FROM TB_BADDED001M S1 WHERE S1.CVLCPT_ADT_YR = B.ADT_YR AND S1.ADT_NO = B.ADT_NO ) AS TIT_NM2 ,(SELECT RSCH_ASMT_TTL /* 연구과제제목 */
FROM TB_BADFRE012M S1 WHERE S1.CVLCPT_RCPT_YR = B.DATA_SBMSN_RCPT_YR AND S1.SRECS_RCPT_SECD = B.DATA_SBMSN_RCPT_SECD AND S1.SRECS_RCPT_SQNO = B.DATA_SBMSN_RCPT_SQNO ) AS TIT_NM3 FROM TB_DCMIDM001L A ,TB_DCMIDM001D B WHERE A.ADT_SBMSN_DATA_ID = B.ADT_SBMSN_DATA_ID AND A.ADT_SBMSN_DATA_ID = #ADT_SBMSN_DATA_ID# AND LENGTH(B.FILE_HASH_VL) > 0 ORDER BY B.ATCH_FILE_IDNO