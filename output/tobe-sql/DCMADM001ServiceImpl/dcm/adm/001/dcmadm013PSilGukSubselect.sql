    A.PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    A.SLCT_JRSD_SG_SECD /* 청탁소관부호구분코드 */,
    B.PROF_RBPRSN_INST_NM /* 증명책임자기관명 */
FROM TBDCMACM015M A, (SELECT INST_CD /* 기관코드 */,
    PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    SLCT_JRSD_SG_SECD /* 청탁소관부호구분코드 */
FROM TBDCMACM015M WHERE SUBSTR(INST_CD , 4) = '000000000' AND USE_DVSN_NO = 'Y' AND ORG_DGE_CD = '1' AND SUBSTR(INST_CD, 0,3) = SLCT_JRSD_SG_SECD) B WHERE A.SLCT_JRSD_SG_SECD = B.SLCT_JRSD_SG_SECD(+) AND A.ORG_DGE_CD != '1' AND SUBSTR(A.INST_CD, 0,3) = A.SLCT_JRSD_SG_SECD AND A.USE_DVSN_NO = 'Y' A.PROF_RBPRSN_INST_NM LIKE '%' || #strSilGukNm# || '%' ORDER BY INST_CD