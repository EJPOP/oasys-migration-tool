SELECT D1.ADT_YR AS ADT_YR /* 감사년도 */,
    D1.ADT_NO AS ADT_NO /* 감사번호 */,
    D1.DSPS_RQT_SQNO AS DSPS_RQT_SQNO /* 처분요구순번 */,
    D1.REL_INST_CD AS REL_INST_CD /* 관계기관코드 */,
    F_ORG_INFO(D1.REL_INST_CD,'1') AS REL_INST_NM /* 관계기관명 */,
    D1.JRSD_INST_CD AS JRSD_INST_CD /* 소관기관코드 */,
    D1.DSPS_RQT_TRGT_KDCD AS DSPS_RQT_TRGT_KDCD /* 처분요구대상종류코드 */,
    D1.CPLT_YN AS CPLT_YN /* 완결여부 */
FROM TB_BADEAR008M D1 WHERE 1=1 AND D1.ADT_YR = #CVLCPT_ADT_YR# AND D1.ADT_NO = #ADT_NO# AND D1.DSPS_RQT_SQNO = #DSPS_RQT_SQNO# ORDER BY D1.FRST_REG_DT, D1.REL_INST_CD