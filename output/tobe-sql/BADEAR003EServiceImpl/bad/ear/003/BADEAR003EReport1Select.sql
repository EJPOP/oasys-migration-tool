SELECT Z.CVLCPT_ADT_YR /* 민원감사년도 */,
    /*감사년도*/ Z.ADT_NO AS ADT_NO /* 감사번호 */,
    /*감사순번*/ MAX(Z.SPRVSN_DEPT_CD) AS SPRVSN_DEPT_CD /* 주관부서코드 */,
    /*부서코드*/ MAX(Z.EXEC_DPT_NM) AS EXEC_DPT_NM,
    /*부서명*/ MAX(Z.ADT_KDCD) AS ADT_KDCD /* 감사종류코드 */,
    /*감사종류코드*/ MAX(Z.AUD_EXEC_DVSN_CD_NM) AS AUD_EXEC_DVSN_CD_NM,
    /*감사종류명*/ MAX(Z.ADT_MTTR_NM) AS ADT_MTTR_NM /* 감사사항명 */,
    /*감사사항명*/ MAX(Z.AUD) AS AUD,
    /*감사연번호*/ MAX(Z.ADT_RPRS_INST_CD) AS ADT_RPRS_INST_CD /* 감사대표기관코드 */,
    /*감사대상기관코드*/ MAX(Z.AUD_REP_ORG_NM) AS AUD_REP_ORG_NM,
    /*감사대상기관명*/ SUM(Z.DSPS_RQT_AMT) AS DSPS_RQT_AMT /* 처분요구금액 */,
    /*재정적성과금액*/ SUM(Z.MEASURES_AM) AS MEASURES_AM,
    /*조치금액 : 추징회수보전금액 + 환급금액 + 추급금액*/ SUM(Z.EXP_EFT_AM) AS EXP_EFT_AM,
    /*기대효과금액 : 예산절감금액 + 수입증대금액 + 재산증가금액 + 지출합리화금액 + 국민부담경감금액(극민편익)*/ SUM(Z.BGT_RTRC_AMT) AS BGT_RTRC_AMT /* 예산절감금액 */,
    /*예산절감금액*/ SUM(Z.INCM_INCRS_AMT) AS INCM_INCRS_AMT /* 수입증가금액 */,
    /*수입증대금액*/ SUM(Z.PRPT_INCRS_AMT) AS PRPT_INCRS_AMT /* 재산증가금액 */,
    /*재산증가금액*/ SUM(Z.EXPND_RATI_AMT) AS EXPND_RATI_AMT /* 지출합리화금액 */,
    /*지출합리화금액*/ SUM(Z.PPL_BRDN_RDT_AMT) AS PPL_BRDN_RDT_AMT /* 국민부담경감금액 */,
    /*국민부담경감금액(극민편익)*/ SUM(Z.EPFC_EFCT_AMT) AS EPFC_EFCT_AMT /* 사후효과금액 */
FROM (SELECT A.ADT_YR /* 감사년도 */,
    A.ADT_NO /* 감사번호 */,
    A.DSPS_RQT_SQNO /* 처분요구순번 */,
    C.SPRVSN_DEPT_CD /* 주관부서코드 */,
    F_DPT_INFO(C.SPRVSN_DEPT_CD ,'1') AS F_DPT_INFO,
    C.ADT_KDCD /* 감사종류코드 */,
    F_CODE_NM('1000007',C.ADT_KDCD) AS F_CODE_NM,
    /*감사종류명*/ C.ADT_MTTR_NM AS ADT_MTTR_NM /* 감사사항명 */,
    F_MNG_KND_AUDYRNO(C.ADT_YR, C.ADT_NO, C.ADT_KDCD, '-') AS F_MNG_KND_AUDYRNO,
    C.ADT_RPRS_INST_CD /* 감사대표기관코드 */,
    F_ORG_INFO(C.ADT_RPRS_INST_CD,'1') AS F_ORG_INFO,
    (NVL(B.PNRT_PRSRV_AMT, 0) + NVL(B.RMBR_AMT, 0) + NVL(B.SPLP_AMT, 0) + NVL(B.BGT_RTRC_AMT, 0) + NVL(B.INCM_INCRS_AMT, 0) + NVL(B.PPL_BRDN_RDT_AMT, 0) + NVL(B.PRPT_INCRS_AMT, 0) + NVL(B.EXPND_RATI_AMT, 0) + NVL(B.EPFC_EFCT_AMT, 0)) AS DSPS_RQT_AMT /* 처분요구금액 */,
    (NVL(B.PNRT_PRSRV_AMT, 0) + NVL(B.RMBR_AMT, 0) + NVL(B.SPLP_AMT, 0)) AS PNRT_PRSRV_AMT /* 추징회수보전금액 */,
    /*추징회수보전금액 + 환급금액 + 추급금액 =조치금액*/ (NVL(B.BGT_RTRC_AMT, 0) + NVL(B.INCM_INCRS_AMT, 0) + NVL(B.PRPT_INCRS_AMT, 0) + NVL(B.EXPND_RATI_AMT, 0) + NVL(B.PPL_BRDN_RDT_AMT, 0)) AS BGT_RTRC_AMT /* 예산절감금액 */,
    /*기대효과금액*/ NVL(B.BGT_RTRC_AMT, 0) AS BGT_RTRC_AMT /* 예산절감금액 */,
    /*예산절감금액*/ NVL(B.INCM_INCRS_AMT, 0) AS INCM_INCRS_AMT /* 수입증가금액 */,
    /*수입증대금액*/ NVL(B.PRPT_INCRS_AMT, 0) AS PRPT_INCRS_AMT /* 재산증가금액 */,
    /*재산증가금액*/ NVL(B.EXPND_RATI_AMT, 0) AS EXPND_RATI_AMT /* 지출합리화금액 */,
    /*지출합리화금액*/ NVL(B.PPL_BRDN_RDT_AMT, 0) AS PPL_BRDN_RDT_AMT /* 국민부담경감금액 */,
    /*국민부담경감금액(극민편익)*/ NVL(B.EPFC_EFCT_AMT, 0) AS EPFC_EFCT_AMT /* 사후효과금액 */
FROM TBBADEAR001M A , TBBADEAR002D B , TBBADDED001M C WHERE 1 = 1 AND A.CVLCPT_ADT_YR = B.CVLCPT_ADT_YR AND A.ADT_NO = B.ADT_NO AND A.DSPS_RQT_SQNO = B.DSPS_RQT_SQNO AND A.CVLCPT_ADT_YR = C.CVLCPT_ADT_YR AND A.ADT_NO = C.ADT_NO AND A.CVLCPT_ADT_YR = #CVLCPT_ADT_YR# /*화면 인자값 감사년도*/ AND A.ADT_NO = #ADT_NO# /*화면 인자값 감사순번*/ ) Z GROUP BY Z.CVLCPT_ADT_YR , Z.ADT_NO