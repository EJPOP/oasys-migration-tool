SELECT DISTINCT A.ADT_YR AS ADT_YR /* 감사년도 */,
    A.ADT_NO /* 감사번호 */,
    F_MNG_KND_AUDYRNO(C.ADT_YR, C.ADT_NO, C.ADT_KDCD, '-') AS F_MNG_KND_AUDYRNO,
    /*감사연번호*/ C.ADT_MTTR_NM AS ADT_MTTR_NM /* 감사사항명 */
FROM TB_BADEAR001M A , TB_BADEAR002D B , TB_BADDED001M C WHERE 1 = 1 AND A.ADT_YR = B.ADT_YR AND A.ADT_NO = B.ADT_NO AND A.DSPS_RQT_SQNO = B.DSPS_RQT_SQNO AND A.ADT_YR = C.ADT_YR AND A.ADT_NO = C.ADT_NO AND A.ADT_YR = #CVLCPT_ADT_YR# /*화면 인자값 감사년도*/ AND A.ADT_NO = #ADT_NO# /*화면 인자값 감사순번 */