SELECT D1.QSTNAI_YR||'-'||D1.QSTNAI_NO AS QSTNAI_YR,
    /*질의서번호*/ D1.QSTNAI_TIT_NM AS QSTNAI_TIT_NM,
    /*질의서제목*/ F_CODE_NM('1000374',D1.NTAS_RQ_MTRL_HNDL_STA_CD) AS F_CODE_NM,
    /*처리상태명*/ D1.SITT_NO AS SITT_NO /* 회기번호 */,
    D1.RQR_NM AS RQR_NM /* 요청자명 */,
    D1.SRECS_SRNG_MBCMT_NM AS SRECS_SRNG_MBCMT_NM /* 심사재심의심사위원명 */,
    D1.CMT_CD AS CMT_CD /* 위원회코드 */,
    F_CODE_NM('1000091',D1.CMT_CD) AS F_CODE_NM /* 위원회명 */,
    D1.RQT_YMD AS RQT_YMD /* 요구일자 */,
    D1.SMT_RQ_DT AS SMT_RQ_DT /* 실국제출기한 */,
    D1.NTAS_SMT_DDLN_DT AS NTAS_SMT_DDLN_DT /* 국회제출기한 */,
    F_CODE_NM('1000092',D1.NTAS_RQ_MTRL_DVSN_CD) AS F_CODE_NM /* 자료구분명 */,
    F_CODE_NM('1000093',D1.RQ_SHP_CD) AS F_CODE_NM /* 요구형태명 */
FROM TBCSPFPL001M D1 where 1=1 D1.QSTNAI_YR = #strYear# D1.QSTNAI_NO = #strQstnai# D1.QSTNAI_TIT_NM LIKE '%'||#strQstnaiTitleNm#||'%' D1.SITT_NO LIKE '%'||#strSittNo#||'%' D1.RQR_NM LIKE '%'||#strRqrNm#||'%' D1.SRECS_SRNG_MBCMT_NM = #SRECS_SRNG_MBCMT_NM# D1.RQT_YMD >= #strRqDtFrom# D1.RQT_YMD &lt;= #strRqDtTo# D1.SRECS_SRNG_MBCMT_NM = #SRECS_SRNG_MBCMT_NM# D1.CMT_CD = #strCmtCd# D1.NTAS_RQ_MTRL_DVSN_CD = #strMtrlDvsnCd# D1.RQ_SHP_CD = #strRqShpCd# D1.NTAS_RQ_MTRL_HNDL_STA_CD = #strNtasRqMtrlHndlStaCd# ORDER BY D1.QSTNAI_NO ASC