SELECT SUBSTR(A.USE_STR_DT, 0, 4) || '. ' || SUBSTR(A.USE_STR_DT, 5, 2) || '. ' || SUBSTR(A.USE_STR_DT, 7, 2) AS SUBSTR,
    A.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    B.USER_NM /* 사용자명 */,
    A.USE_FEE,
    NVL(A.RMN_NM, '-') AS RMN_NM
FROM TBCSPSAE501D A , TBDCMACM001M B WHERE A.TRGT_NOP_ENO = B.TRGT_NOP_ENO AND SUBSTR(A.USE_STR_DT, 5, 2) = SUBSTR(#strFromDt#, 5, 2) AND A.USE_FEE > 0 AND A.PD_YN = 'Y' ORDER BY A.USE_STR_DT