SELECT A.BGNG_YMD /* 시작일자 */,
    A.END_YMD /* 종료일자 */
FROM ( SELECT TO_CHAR(WEEK_START, 'YYYYMMDD') AS BGNG_YMD /* 시작일자 */,
    TO_CHAR(WEEK_END, 'YYYYMMDD') AS END_YMD /* 종료일자 */
FROM ( SELECT TRUNC (START_DT + LEVEL, 'D') + 1 AS TRUNC,
    TRUNC (START_DT + LEVEL, 'D') + 7 AS TRUNC
FROM ( SELECT TO_DATE(TO_CHAR(SYSDATE, 'YYYYMMDD')) - 1 AS TO_DATE,
    ADD_MONTHS(TO_CHAR(SYSDATE, 'YYYYMMDD'), 1) AS END_YMD /* 종료일자 */
FROM DUAL ) CONNECT BY LEVEL <= END_YMD - START_DT ) GROUP BY WEEK_START, WEEK_END ORDER BY WEEK_START ) A WHERE ROWNUM = 1