SELECT D1.TPOF_RCPT_YM /* 제보접수연월 */,
    D1.TPOF_INFO_RPTP_SQNO /* 제보정보보고서순번 */,
    CASE WHEN SUBSTR(D1.TPOF_INFO_TPCD, 0, 2) = '01' THEN F_CODE_NM('1000607', '0100') ELSE F_CODE_NM('1000607', '0200') END AS SUBSTR,
    F_CODE_NM('1000607', D1.TPOF_INFO_TPCD) AS F_CODE_NM,
    F_CODE_NM('1000608', D2.TPOF_ATRZ_RPTR_SECD) AS TPOF_ATRZ_RPTR_SECD /* 제보결재보고자구분코드 */,
    F_DPT_INFO(D3.SRECS_DEPT_CD, '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    D3.USER_NM /* 사용자명 */,
    F_CODE_NM('1000173', D3.JBGD_SECD) AS JBGD_NM /* 직급명 */
FROM TB_CSPDCP501M D1 ,TB_CSPDCP503L D2 ,TBDCMACM001M D3 WHERE 1=1 AND D1.TPOF_RCPT_YM = D2.TPOF_RCPT_YM(+) AND D1.TPOF_INFO_RPTP_SQNO = D2.TPOF_INFO_RPTP_SQNO(+) AND D2.TPOF_APRVR_ID = D3.USER_ID AND D2.TPOF_ATRZ_RPTR_SECD IN (2, 3) AND D1.TPOF_RCPT_YM= #CVLCPT_RCPT_YM# AND D1.TPOF_INFO_RPTP_SQNO = #CVLCPT_INFO_RPTP_SQNO# ORDER BY TPOF_ATRZ_RPTR_SECD