SELECT D1.EVL_YR AS EVL_YR /* 평가년도 */,
    D1.HLYR_DVSN_CD AS HLYR_DVSN_CD /* 반기구분코드 */,
    D1.EVL_INDEX_CD AS EVL_INDEX_CD /* 평가지표코드 */,
    D1.EVL_INDEX_NM AS EVL_INDEX_NM /* 평가지표명 */,
    D2.EVL_STAD_CD AS EVL_STAD_CD /* 평가기준코드 */,
    D2.EVL_STAD_NM AS EVL_STAD_NM /* 평가기준명 */
FROM TBCSPAOE002M D1 , TBCSPAOE003D D2 WHERE 1=1 AND D1.EVL_YR = D2.EVL_YR AND D1.HLYR_DVSN_CD = D2.HLYR_DVSN_CD AND D1.EVL_INDEX_CD = D2.EVL_INDEX_CD AND D1.EVL_YR = #strEvlYr# AND D1.HLYR_DVSN_CD = #strHlyrDvsnCd# AND D1.EVL_INDEX_CD = #strEvlIndexCd# ORDER BY D2.EVL_STAD_CD