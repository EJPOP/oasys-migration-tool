SELECT T1.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    T1.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    T1.USE_YN /* 사용여부 */,
    T1.ABR_DPT_NM_1,
    T1.ABR_DPT_NM_2,
    T1.FRML_DPT_NM,
    T1.STND_DPT_CD,
    T1.DPT_SNO,
    T1.HIRK_DPT_CD,
    F_DPT_INFO(T1.HIRK_DPT_CD,'1') AS F_DPT_INFO
FROM TBDCMACM002M T1 WHERE 1=1 AND ( T1.CNSTN_MBCMT_DEPT_NM LIKE '%'||#{srchDptNm}||'%' OR T1.ABR_DPT_NM_1 LIKE '%'||#{srchDptNm}||'%' OR T1.ABR_DPT_NM_2 LIKE '%'||#{srchDptNm}||'%' OR T1.FRML_DPT_NM LIKE '%'||#{srchDptNm}||'%' ) AND T1.USE_YN = #{srchUseYn} ORDER BY DECODE(T1.USE_YN, 'Y',T1.DPT_SNO,'9999'), T1.DPT_CD