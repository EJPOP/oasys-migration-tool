SELECT A.TPOF_SORT_SQNO /* 제보정렬순번 */,
    NVL(C.TPOF_STP_SE_CVN,'') AS ADT_STP_NM /* 감사단계명 */,
    B.TPOF_RPTP_NM /* 제보보고서명 */,
    B.TPOF_RPTP_KDCD /* 제보보고서종류코드 */,
    A.CVLCPT_ATRZ_DMND_ID /* 민원결재요청아이디 */,
    A.CVLCPT_RCPT_YR AS CVLCPT_RCPT_YR /* 민원접수년도 */,
    A.CVLCPT_RCPT_NO AS CVLCPT_RCPT_NO /* 민원접수번호 */,
    A.CVLCPT_STP_SECD AS CVLCPT_STP_SECD /* 민원단계구분코드 */,
    A.CVLCPT_DOC_ID /* 민원문서아이디 */,
    A.TPOF_ATRZ_SECD /* 제보결재구분코드 */,
    A.TPOF_ATRZ_DOC_SECD /* 제보결재문서구분코드 */,
    A.CVLCPT_CLM_SECD /* 민원청구구분코드 */
FROM TB_CSPDCP609M A INNER JOIN TB_CSPDCP603M B ON A.CVLCPT_RCPT_YR = B.CVLCPT_RCPT_YR AND A.CVLCPT_CLM_SECD = B.CVLCPT_CLM_SECD AND A.CVLCPT_RCPT_NO = B.CVLCPT_RCPT_NO AND A.CVLCPT_STP_SECD = B.CVLCPT_STP_SECD AND A.CVLCPT_DOC_ID = B.CVLCPT_DOC_ID LEFT OUTER JOIN TB_CSPDCP601M C ON A.CVLCPT_RCPT_YR = C.CVLCPT_RCPT_YR AND A.CVLCPT_CLM_SECD = C.CVLCPT_CLM_SECD AND A.CVLCPT_RCPT_NO = C.CVLCPT_RCPT_NO AND A.CVLCPT_STP_SECD = C.CVLCPT_STP_SECD WHERE A.CVLCPT_ATRZ_DMND_ID = #ATRZ_DMND_ID# AND A.TPOF_ATRZ_DOC_SECD = #CRU_ATRZ_DOC_SECD# ORDER BY A.TPOF_SORT_SQNO