SELECT WRT_YMD /* 작성일자 */,
    NVL(NOP_CNT_1,0) AS NOP_CNT_1,
    NVL(NOP_CNT_2,0) AS NOP_CNT_2,
    NVL(NOP_CNT_3,0) AS NOP_CNT_3,
    NVL(NOP_CNT_4,0) AS NOP_CNT_4,
    NVL(NOP_CNT_8,0) AS NOP_CNT_8,
    NVL(MEAL_NOP_CNT,0) AS MEAL_NOP_CNT,
    NVL(LOGIN_CNT,0) AS LOGIN_CNT,
    NVL(RM_MEAL_NOP_CNT,0) AS RM_MEAL_NOP_CNT,
    NVL(MEAL_NOP_CNT,0) AS MEAL_NOP_CNT,
    CASE WHEN NVL(NOP_CNT_8, 0) = 0 THEN 0 ELSE TRUNC(NVL(RM_MEAL_NOP_CNT, 0)/NVL(NOP_CNT_8, 0)* 100,2) END AS NOP_CNT_8,
    CASE WHEN NVL(LOGIN_CNT, 0) = 0 THEN 0 ELSE TRUNC(NVL(RM_MEAL_NOP_CNT, 0)/NVL(LOGIN_CNT, 0)* 100,2) END AS LOGIN_CNT,
    RMRK_CN /* 비고내용 */
FROM TBCSPMWF001M WHERE 1=1 AND WRT_YMD BETWEEN #strWRIT_DT_S# AND #strWRIT_DT_E#