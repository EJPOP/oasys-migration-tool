SELECT ROWNUM,
    T1.LINK_YR /* 연계년도 */,
    T1.DATA_DMND_SQNO /* 자료요청순번 */,
    /*자료요청순번*/ T2.FRS_MDM_SQNO AS FRS_MDM_SQNO /* 포렌식매체순번 */,
    T2.MDM_DVSN_CD,
    T2.ATTR_DVSN_CD,
    T2.MDM_CNT,
    T2.DRIV_CD,
    T2.DTIL_DVSN_CD,
    T2.WAY_DVSN_CD,
    T2.USR_AFFI_TXT,
    T2.USR_RMK_TXT,
    T2.USR_ASSI_TASK,
    T2.RMRK_CN /* 비고내용 */
FROM TBCSPIIT028M T1 ,TBCSPIIT028D T2 WHERE 1=1 AND T1.LINK_YR ||'-'|| T1.DATA_DMND_SQNO = #DATA_DMND_SQNO# AND T1.LINK_YR = TO_CHAR(SYSDATE,'YYYY') AND T1.DATA_DMND_SQNO = #DATA_DMND_SQNO# AND T1.ATRZ_DOC_NO = #ATRZ_DOC_NO# AND T1.LINK_YR = T2.LINK_YR AND T1.DATA_DMND_SQNO = T2.DATA_DMND_SQNO ORDER BY TO_NUMBER(T2.FRS_MDM_SQNO)