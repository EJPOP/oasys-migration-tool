SELECT SUBSTR(T1.LINK_TRRC_DT,1,8) AS PROF_RBPRSN_LINK_DT /* 증명책임자연계일시 */,
    CASE WHEN T1.TASK_SECD = '01' THEN '공지사항' ELSE T1.TASK_SECD END AS TASK_SECD /* 업무구분코드 */,
    T1.LINK_PRCS_STTS_SECD /* 연계처리상태구분코드 */,
    COUNT(1) AS 1
FROM IS_NOTIINFO T1 WHERE SUBSTR(T1.LINK_TRRC_DT,1,8) BETWEEN #strConDtStr# AND #strConDtEnd# GROUP BY SUBSTR(T1.LINK_TRRC_DT,1,8) ,T1.TASK_SECD ,T1.LINK_PRCS_STTS_SECD ORDER BY SUBSTR(T1.LINK_TRRC_DT,1,8) DESC ,T1.TASK_SECD ,T1.LINK_PRCS_STTS_SECD