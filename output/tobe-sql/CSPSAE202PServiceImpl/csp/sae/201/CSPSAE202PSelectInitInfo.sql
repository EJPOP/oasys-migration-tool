    A.EDU_DVSN_NM,
    B.EDU_CAT_NM,
    C.CRS_NM /* 과정명 */,
    C.CRS_CD,
    SUBSTR(E.EDU_BGNG_YMD, 0, 4) || '.' || SUBSTR(E.EDU_BGNG_YMD, 5, 2) || '.' || SUBSTR(E.EDU_BGNG_YMD, 7, 2) || ' ~ ' || SUBSTR(E.EDU_END_YMD, 0, 4) || '.' || SUBSTR(E.EDU_END_YMD, 5, 2) || '.' || SUBSTR(E.EDU_END_YMD, 7, 2) AS SUBSTR,
    E.CRS_CDN /* 과정기수 */,
    1,
    'N' AS DMT_UE_YN,
    'N' AS SNDY_LODG_YN,
    'N' AS BUS_UE_YN,
    '00' AS WRK_PRD_TXT1,
    '00' AS WRK_PRD_TXT2
FROM TBCSPSAE101C A , TBCSPSAE102C B , TBCSPSAE103C C , TBCSPSAE201D E WHERE 1=1 AND B.EDU_DVSN_CD = A.EDU_DVSN_CD AND C.EDU_DVSN_CD = B.EDU_DVSN_CD AND C.EDU_CAT_CD = B.EDU_CAT_CD AND E.CRS_CD = C.CRS_CD AND E.LINK_YR = #LINK_YR# AND E.CRS_CD = #strEDU_CRS_CD# AND E.CRS_CDN = #CRS_CDN#