    TTO.BZTRP_SQNO /* 출장순번 */,
    TTO.FT_SQNO /* 출장조순번 */,
    F_ORG_INFO(TTO.INST_CD,'1') AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    F_CODE_NM('1000001',TTO.RGN_CD) AS F_CODE_NM /* 지역 */,
    TTO.ADT_BGNG_YMD /* 감사시작일자 */,
    TTO.ADT_END_YMD /* 감사종료일자 */,
    PSON.ADT_DCN /* 감사일수 */,
    PSON.BZTRP_BGNG_YMD /* 출장시작일자 */,
    PSON.BZTRP_END_YMD /* 출장종료일자 */,
    PSON.BZTRP_DCN /* 출장일수 */
FROM TB_BADDED008L TTO , TB_BADDED002M SITU , ( SELECT BZTRP_YR /* 출장년도 */,
    BZTRP_SQNO /* 출장순번 */,
    FT_SQNO /* 출장조순번 */,
    FT_TRGT_INST_SQNO /* 출장조대상기관순번 */,
    MIN(ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(ADT_DCN) AS ADT_DCN /* 감사일수 */,
    MIN(BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    MAX(BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */
FROM TBBADDED006L WHERE BZTRP_YR =#BZTRP_YR# /* 출장년도*/ GROUP BY BZTRP_YR, BZTRP_SQNO, FT_SQNO, FT_TRGT_INST_SQNO ) PSON WHERE 1 = 1 AND TTO.BZTRP_YR = SITU.BZTRP_YR AND TTO.BZTRP_SQNO = SITU.BZTRP_SQNO AND PSON.BZTRP_YR = TTO.BZTRP_YR AND PSON.BZTRP_SQNO = TTO.BZTRP_SQNO AND PSON.FT_SQNO = TTO.FT_SQNO AND PSON.FT_TRGT_INST_SQNO = TTO.FT_TRGT_INST_SQNO AND TTO.BZTRP_YR = #BZTRP_YR# AND TTO.BZTRP_SQNO = #BZTRP_SQNO# AND TTO.FT_SQNO =#FT_SQNO#