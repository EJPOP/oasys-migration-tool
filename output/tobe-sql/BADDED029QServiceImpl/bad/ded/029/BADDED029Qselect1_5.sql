SELECT M.FT_SQNO /* 출장조순번 */,
    /*팀번호 MIN(M.AUD_STR_DT) AS AUD_STR_DT , /*감사시작일*/ MAX(M.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    /*감사종료일*/ SUM(M.ADT_DCN) AS ADT_DCN /* 감사일수 */,
    /*감사일수*/ MIN(M.BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    /*출장시작일*/ MAX(M.BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    /*출장종료일*/ SUM(M.BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */
FROM (SELECT PTD.BZTRP_YR /* 출장년도 */,
    PTD.BZTRP_SQNO /* 출장순번 */,
    PTD.FT_SQNO /* 출장조순번 */,
    PTD.FT_TRGT_INST_SQNO /* 출장조대상기관순번 */,
    MIN(PTD.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(PTD.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(PTD.ADT_DCN) AS ADT_DCN /* 감사일수 */,
    MIN(PTD.BZTRP_BGNG_YMD) AS BZTRP_BGNG_YMD /* 출장시작일자 */,
    MAX(PTD.BZTRP_END_YMD) AS BZTRP_END_YMD /* 출장종료일자 */,
    MAX(PTD.BZTRP_DCN) AS BZTRP_DCN /* 출장일수 */
FROM TBBADDED006L PTD, TBBADDED002M SITU WHERE 1 = 1 AND PTD.BZTRP_YR = SITU.BZTRP_YR AND PTD.BZTRP_SQNO = SITU.BZTRP_SQNO AND SITU.BZTRP_SECD = '2' AND SITU.BZTRP_YR = #BZTRP_YR# /*인자값 출장년도*/ AND SITU.BZTRP_SQNO = #BZTRP_SQNO# /*인자값 출장순번*/ AND PTD.FT_SQNO = #FT_SQNO# /*상위의쿼리(SQL_SUB)의 팀번호*/ GROUP BY PTD.BZTRP_YR, PTD.BZTRP_SQNO, PTD.FT_SQNO, PTD.FT_TRGT_INST_SQNO ) M GROUP BY M.FT_SQNO