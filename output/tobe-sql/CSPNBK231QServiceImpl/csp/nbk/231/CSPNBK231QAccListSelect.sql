SELECT /* CSPNBK231QAccListSelect */ '0' AS MOUI_DVSN_CD,
    '개인' AS MOUI_DVSN_CD_NM,
    D4.MOUI_NO,
    D4.ACC_KND,
    D4.OAA_DT,
    D4.DBO_BLL_BLNC /* 지출관계산서잔액 */,
    '잔액증명서 출력' AS RPT_BTN,
    '거래내역조회' AS DEAL_LIST_BTN
FROM ( SELECT NVL(MAX(D1.MOUI_NO), #mouiNo#) AS MOUI_NO,
    '출자금' AS ACC_KND,
    MAX(D1.INV_OAA_DT) AS INV_OAA_DT,
    NVL(MAX(D1.INV_BL), 0) AS DBO_BLL_BLNC /* 지출관계산서잔액 */,
    1
FROM TBCSPNBK101M D1 WHERE D1.MOUI_NO = #mouiNo# UNION ALL SELECT NVL(MAX(D2.MOUI_NO), #mouiNo#) AS MOUI_NO,
    '대출금' AS ACC_KND,
    MAX(D2.LEND_OAA_DT) AS LEND_OAA_DT,
    NVL(MAX(D2.LEND_BL), 0) AS DBO_BLL_BLNC /* 지출관계산서잔액 */,
    2
FROM TBCSPNBK202M D2 WHERE D2.LEND_KND_CD = '1' AND D2.MOUI_NO = #mouiNo# UNION ALL SELECT NVL(MAX(D3.MOUI_NO), #mouiNo#) AS MOUI_NO,
    '임차보증대출금' AS ACC_KND,
    MAX(D3.LEND_OAA_DT) AS LEND_OAA_DT,
    NVL(MAX(D3.LEND_BL), 0) AS DBO_BLL_BLNC /* 지출관계산서잔액 */,
    3
FROM TBCSPNBK202M D3 WHERE D3.LEND_KND_CD = '2' AND D3.MOUI_NO = #mouiNo# ) D4 ORDER BY ORD