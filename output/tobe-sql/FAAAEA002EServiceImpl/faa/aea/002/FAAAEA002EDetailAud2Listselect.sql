SELECT DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */,
    SUM(CASE WHEN REL_INST_CD IS NOT NULL THEN 1 ELSE 0 END) AS REL_INST_CD /* 관계기관코드 */,
    SUM(CASE WHEN RLPR_NM IS NOT NULL THEN 1 ELSE 0 END) AS RLPR_NM /* 관계자명 */,
    NVL(SUM(DSPS_RQT_AMT),0) AS DSPS_RQT_AMT /* 처분요구금액 */
FROM TBFAAAEA021M WHERE (INST_CD = #INST_CD# OR INST_CD IN (SELECT BFR_INST_CD /* 이전기관코드 */
FROM TBFAAAEA023C WHERE INST_CD = #INST_CD# ) ) WHERE ( INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' OR INST_CD IN (SELECT BFR_INST_CD /* 이전기관코드 */
FROM TBFAAAEA023C WHERE INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' ) ) AND CVLCPT_ADT_YR = #CVLCPT_ADT_YR# AND DEL_YN = 'N' GROUP BY DSPS_RQT_KND_CVN