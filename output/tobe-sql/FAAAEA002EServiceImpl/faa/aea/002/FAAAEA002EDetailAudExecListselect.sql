SELECT D2.CVLCPT_ADT_YR /* 민원감사년도 */,
    D2.ADT_FLD_NM /* 감사분야명 */,
    D2.ADT_MTTR_CN /* 감사사항내용 */,
    D2.ADT_KND_NM /* 감사종류명 */,
    D2.ADT_BGNG_YMD||' ~ '||D2.ADT_END_YMD AS ADT_YMD /* 감사일자 */,
    D2.ADT_CPSC_NOPE /* 감사연인원인원수 */,
    D2.CPADT_EMPHS_CN /* 위탁대행감사중점내용 */
FROM TBFAAAEA020L D1 INNER JOIN TBFAAAEA019M D2 ON D1.INST_CD = D2.INST_CD AND D1.CVLCPT_ADT_YR = D2.CVLCPT_ADT_YR AND D1.ADT_SQNO = D2.ADT_SQNO WHERE D1.DEL_YN = 'N' AND D2.DEL_YN = 'N' AND D2.INST_CD = #INST_CD# AND D2.INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' AND D2.CVLCPT_ADT_YR >= #strAudSYrExec# AND D2.CVLCPT_ADT_YR AND D2.ADT_FLD_SECD = #ADT_FLD_SECD# ORDER BY D2.CVLCPT_ADT_YR DESC, D2.ADT_BGNG_YMD DESC