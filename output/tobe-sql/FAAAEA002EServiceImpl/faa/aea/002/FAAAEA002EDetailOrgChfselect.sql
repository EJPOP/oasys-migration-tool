SELECT INLDR_CDNM /* 기관장코드명 */,
    CASE WHEN FACP_SE_YN = '1' THEN '현직' ELSE '전직' END AS FACP_SE_YN /* 전현직구분여부 */,
    CASE WHEN NMNT_YMD IS NOT NULL THEN SUBSTRING(NMNT_YMD,0,4)||'.'||SUBSTRING(NMNT_YMD,5,2)||'.'||SUBSTRING(NMNT_YMD,7,2) ELSE NMNT_YMD END AS NMNT_YMD /* 임명일자 */,
    CASE WHEN TNR_EXPRY_YMD IS NOT NULL THEN SUBSTRING(TNR_EXPRY_YMD,0,4)||'.'||SUBSTRING(TNR_EXPRY_YMD,5,2)||'.'||SUBSTRING(TNR_EXPRY_YMD,7,2) ELSE TNR_EXPRY_YMD END AS TNR_EXPRY_YMD /* 임기만료일자 */,
    MAIN_ACBG_CN /* 주요학력내용 */,
    MAIN_CRR_CN /* 주요경력내용 */
FROM TBFAAAEA015M WHERE INST_CD = #INST_CD# WHERE INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' AND FRS_MDM_SQNO = #FRS_MDM_SQNO#