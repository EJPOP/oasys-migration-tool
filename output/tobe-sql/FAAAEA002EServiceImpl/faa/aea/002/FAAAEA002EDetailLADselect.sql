SELECT D1.NTFCTN_YR||'-'||D1.NTFCTN_SQNO AS NTFCTN_YR /* 통보년도 */,
    F_CODE_NM('1000650', D1.NTFCTN_SECD) AS F_CODE_NM,
    F_ORG_INFO(D1.JRSD_INST_CD, '1') AS F_ORG_INFO,
    D1.NTFCTN_NM /* 통보명 */,
    D1.HRM_AMT /* 손해금액 */,
    CASE WHEN D1.BAI_RPT_YMD IS NOT NULL THEN SUBSTRING(D1.BAI_RPT_YMD,0,4)||'.'||SUBSTRING(D1.BAI_RPT_YMD,5,2)||'.'||SUBSTRING(D1.BAI_RPT_YMD,7,2) ELSE D1.BAI_RPT_YMD END AS BAI_RPT_YMD /* 감사원보고일자 */,
    F_CODE_NM('1000202', D1.OCRN_SECD) AS F_CODE_NM,
    F_CODE_NM('1000201', D1.VIOL_SECD) AS VIOL_NM /* 비위명 */,
    D1.SELF_CPS_AMT /* 자체변상금액 */,
    D1.DOC_ID /* 문서아이디 */,
    D2.ENAIS_ATCH_FILE_NM /* ENAIS첨부파일명 */
FROM TB_BADGDA001M D1 LEFT OUTER JOIN ECM_FILE_LIST_VIEW2 D2 ON D1.DOC_ID = D2.CVLCPT_DOC_ID WHERE 1=1 AND D1.NTFCTN_YR = #LINK_YR# AND D1.NTFCTN_SQNO = #NTFCTN_SQNO# AND D1.NTFCTN_INST_CD = #INST_CD# AND D1.NTFCTN_SECD = #NTFCTN_SECD#