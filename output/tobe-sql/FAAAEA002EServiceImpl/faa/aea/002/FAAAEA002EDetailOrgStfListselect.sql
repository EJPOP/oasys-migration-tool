SELECT INST_CD /* 기관코드 */,
    FRS_MDM_SQNO /* 포렌식매체순번 */,
    CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    JBGD_NM /* 직급명 */,
    INFO_RLPR_NM /* 정보관계자명 */,
    CASE WHEN DSPS_RQT_TRPR_BRDT IS NOT NULL THEN SUBSTRING(DSPS_RQT_TRPR_BRDT,0,4) ELSE DSPS_RQT_TRPR_BRDT END AS DSPS_RQT_TRPR_BRDT /* 처분요구대상자생년월일 */,
    CASE WHEN NOW_ASSPO_YMD IS NOT NULL THEN SUBSTRING(NOW_ASSPO_YMD,0,4)||'.'||SUBSTRING(NOW_ASSPO_YMD,5,2)||'.'||SUBSTRING(NOW_ASSPO_YMD,7,2) ELSE NOW_ASSPO_YMD END AS NOW_ASSPO_YMD /* 현재보직일자 */
FROM TBFAAAEA016M WHERE INST_CD = #INST_CD# WHERE INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' WHERE INST_CD = #INST_CD# WHERE INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' ORDER BY CNSTN_MBCMT_DEPT_NM ASC, NOW_ASSPO_YMD DESC