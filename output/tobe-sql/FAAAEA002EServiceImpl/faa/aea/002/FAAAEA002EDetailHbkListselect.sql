SELECT F_CODE_NM ('1000406', CVLCPT_TASK_SECD) AS F_CODE_NM,
    HBK_TIT_NM,
    TO_CHAR(FRST_REG_DT,'yyyy.MM.dd') AS FRST_REG_DT /* 최초등록일시 */,
    CASE WHEN CVLCPT_DOC_ID IS NOT NULL THEN '파일다운로드' ELSE '' END AS DOC_NM /* 문서명 */,
    CVLCPT_DOC_ID /* 민원문서아이디 */
FROM TBFAAAEA017M WHERE 1=1 AND HBK_TIT_NM LIKE '%'||#strHbkTit#||'%' AND BZ_YR >= #strSBzYr# AND BZ_YR AND INST_CD = #INST_CD# AND INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' AND INST_CD = #INST_CD# AND INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' ORDER BY BZ_YR DESC