SELECT CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    ADTR_NM /* 감사인명 */,
    OCPT_NM /* 직종명 */,
    JBGD_NM /* 직급명 */,
    CASE WHEN ADT_DEPT_TRANS_YMD IS NOT NULL THEN SUBSTRING(ADT_DEPT_TRANS_YMD,0,4)||'.'||SUBSTRING(ADT_DEPT_TRANS_YMD,5,2)||'.'||SUBSTRING(ADT_DEPT_TRANS_YMD,7,2) ELSE ADT_DEPT_TRANS_YMD END AS ADT_DEPT_TRANS_YMD /* 감사부서전보일자 */,
    CASE WHEN FACP_SE_YN = '1' THEN '현직' ELSE '전직' END AS FACP_SE_YN /* 전현직구분여부 */
FROM TBFAAAEA014M WHERE INST_CD = #INST_CD# WHERE INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' WHERE INST_CD = #INST_CD# WHERE INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' ORDER BY CNSTN_MBCMT_DEPT_NM, OCPT_NM, JBGD_NM, ADT_DEPT_TRANS_YMD DESC, ADTR_NM, FACP_SE_YN