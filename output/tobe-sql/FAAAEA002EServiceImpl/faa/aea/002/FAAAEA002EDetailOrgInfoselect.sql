SELECT D1.KORN_INST_NM /* 한글기관명 */,
    D1.ENG_INST_NM /* 영문기관명 */,
    D1.OTSD_EXPRT_ADDR /* 외부전문가주소 */,
    D1.WEB_SITE_ADDR /* 웹사이트주소 */,
    D1.CRU_TELNO /* 부패전화번호 */,
    D2.FRS_MDM_SQNO /* 포렌식매체순번 */,
    D2.INFO_RLPR_NM /* 정보관계자명 */,
    CASE WHEN D2.DSPS_RQT_TRPR_BRDT IS NOT NULL THEN SUBSTRING(D2.DSPS_RQT_TRPR_BRDT,0,4)||'.'||SUBSTRING(D2.DSPS_RQT_TRPR_BRDT,5,2)||'.'||SUBSTRING(D2.DSPS_RQT_TRPR_BRDT,7,2) ELSE D2.DSPS_RQT_TRPR_BRDT END AS DSPS_RQT_TRPR_BRDT /* 처분요구대상자생년월일 */,
    CASE WHEN D2.NMNT_YMD IS NOT NULL THEN SUBSTRING(D2.NMNT_YMD,0,4)||'.'||SUBSTRING(D2.NMNT_YMD,5,2)||'.'||SUBSTRING(D2.NMNT_YMD,7,2) ELSE D2.NMNT_YMD END AS NMNT_YMD /* 임명일자 */,
    CASE WHEN D2.TNR_EXPRY_YMD IS NOT NULL THEN SUBSTRING(D2.TNR_EXPRY_YMD,0,4)||'.'||SUBSTRING(D2.TNR_EXPRY_YMD,5,2)||'.'||SUBSTRING(D2.TNR_EXPRY_YMD,7,2) ELSE TNR_EXPRY_YMD END AS TNR_EXPRY_YMD /* 임기만료일자 */,
    D2.MAIN_ACBG_CN /* 주요학력내용 */,
    D2.MAIN_CRR_CN /* 주요경력내용 */
FROM TBFAAAEA010M D1 LEFT OUTER JOIN TBFAAAEA015M D2 ON D1.INST_CD = D2.INST_CD AND D2.DEL_YN = 'N' AND D2.FACP_SE_YN = '1' WHERE D1.INST_CD = #INST_CD# WHERE D1.INST_CD LIKE REPLACE(#INST_CD#,'000','') || '%' AND D1.DEL_YN = 'N'