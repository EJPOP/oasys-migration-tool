SELECT F_DPT_INFO(D3.CHR_DPT_CD, '1') AS TKCG_DEPT_NM /* 담당부서명 */,
    F_CODE_NM('1000173', D3.JBGD_SECD) AS JBGD_NM /* 직급명 */,
    F_USR_INFO(D3.PTCPT_CNB, '2') AS INFO_RLPR_NM /* 정보관계자명 */,
    D3.TSK_NM AS TSK_NM /* 업무명 */,
    D3.WGT_RTE AS WGT_RTE /* 비중 */,
    D3.CTB_RT AS CTB_RT /* 기여비율 */,
    D4.CTB_RTE_SUM AS CTB_RTE_SUM /* 합계 */
FROM ( SELECT D1.CHR_DPT_CD,
    D2.JBGD_SECD /* 직급구분코드 */,
    D2.PTCPT_CNB,
    D1.TSK_NM,
    D1.WGT_RTE,
    D2.CTB_RT /* 기여비율 */
FROM TBCSPAOE026M D1 , TBCSPAOE027M D2 WHERE 1 = 1 AND D1.EVL_YR = D2.EVL_YR AND D1.HLYR_DVSN_CD = D2.HLYR_DVSN_CD AND D1.CHR_DPT_CD = D2.CHR_DPT_CD AND D1.TSK_NO = D2.TSK_NO AND D1.EVL_YR = #strEvlYr# AND D1.HLYR_DVSN_CD = #strHlyrDvsnCd# AND D1.CHR_DPT_CD = #strDeptCd# ) D3 ,( SELECT D1.CHR_DPT_CD,
    SUM(D2.CTB_RT) AS CTB_RT /* 기여비율 */
FROM TBCSPAOE026M D1 , TBCSPAOE027M D2 WHERE 1 = 1 AND D1.EVL_YR = D2.EVL_YR AND D1.HLYR_DVSN_CD = D2.HLYR_DVSN_CD AND D1.CHR_DPT_CD = D2.CHR_DPT_CD AND D1.TSK_NO = D2.TSK_NO AND D1.EVL_YR = #strEvlYr# AND D1.HLYR_DVSN_CD = #strHlyrDvsnCd# AND D1.CHR_DPT_CD = #strDeptCd# GROUP BY D1.CHR_DPT_CD ) D4 WHERE D3.CHR_DPT_CD = D4.CHR_DPT_CD