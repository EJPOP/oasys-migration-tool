SELECT /* CSPNBK211MouiListSelect */ D1.MOUI_NO,
    D1.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    D2.USER_NM /* 사용자명 */,
    D1.MOUI_DVSN_CD,
    DECODE( D1.MOUI_DVSN_CD, '0', D2.USER_NM, D1.OGNZ_NM ) AS MOUI_DVSN_CD,
    SUBSTR(D2.CRU_RRNO_SCRTY, 1, 6) || '-*******' AS CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    D2.JBGD_SECD /* 직급구분코드 */,
    F_CODE_NM ( '1000173', D2.JBGD_SECD ) AS F_CODE_NM,
    D3.CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    D1.EX_DVD_RTS_TG_YN,
    D1.SAL_SEI_YN,
    D1.SCS_YN,
    D1.RMRK_CN /* 비고내용 */
FROM TBCSPNBK001M D1 , TBDCMACM001M D2 , TBDCMACM002M D3 WHERE D1.TRGT_NOP_ENO = D2.TRGT_NOP_ENO AND D2.SRECS_DEPT_CD = D3.SRECS_DEPT_CD AND (D1.SCS_YN = 'N' OR (D1.SCS_YN = 'Y' AND SUBSTR(D1.SCS_DT,1,4) = #dvdYr#+1)) AND JOI_DT <> #dvdYr#+1 ORDER BY MOUI_NO