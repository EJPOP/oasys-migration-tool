SELECT D1.QSTNAI_NO AS QSTNAI_NO /* 질의서번호 */,
    D1.QSTNAI_YR AS QSTNAI_YR /* 연도 */,
    D1.QSTNAI_YR|| '-' ||D1.QSTNAI_NO AS QSTNAI_YR /* 질의서연번호 */,
    D1.SRECS_SRNG_MBCMT_NM AS SRECS_SRNG_MBCMT_NM /* 심사재심의심사위원명 */,
    D1.CMT_CD AS CMT_CD /* 위원회명 */,
    D1.RQT_YMD AS RQT_YMD /* 요구일자 */,
    D1.NTAS_RQ_MTRL_DVSN_CD /* 자료구분코드 */,
    D1.RQ_SHP_CD AS RQ_SHP_CD /* 요구형태코드 */,
    D1.NTAS_RQ_MTRL_HNDL_STA_CD AS NTAS_RQ_MTRL_HNDL_STA_CD /* 처리상태코드 */,
    D1.QSTNAI_TIT_NM AS QSTNAI_TIT_NM /* 질의서제목 */,
    D1.FNL_WRTANS_REGI_DT AS FNL_WRTANS_REGI_DT,
    /*답변서제출일자*/ D1.WRTANS_PRSR_NM AS WRTANS_PRSR_NM,
    /*답변서제출자명*/ D1.NTAS_SMT_DDLN_DT AS NTAS_SMT_DDLN_DT,
    /*국회제출기한*/ D1.SMT_RQ_DT AS SMT_RQ_DT,
    /*제출일자*/ D1.SITT_NO AS SITT_NO,
    /*회기번호*/ D1.RQR_NM AS RQR_NM,
    /*요청자명*/ (SELECT D6.CVLCPT_DOC_ID AS CVLCPT_DOC_ID /* 민원문서아이디 */
FROM TBCSPFPL006M D6 WHERE D1.QSTNAI_YR=D6.QSTNAI_YR and D1.QSTNAI_NO=D6.QSTNAI_NO AND D6.NTAS_RQ_MTRL_DOC_KND_CD = 'A1') AS QSTNAIPDF_DOC_ID, (SELECT D6.CVLCPT_DOC_ID AS CVLCPT_DOC_ID /* 민원문서아이디 */
FROM TBCSPFPL006M D6 WHERE D1.QSTNAI_YR=D6.QSTNAI_YR and D1.QSTNAI_NO=D6.QSTNAI_NO AND D6.NTAS_RQ_MTRL_DOC_KND_CD = 'A2') AS QSTNAIHWP_DOC_ID, (SELECT D6.CVLCPT_DOC_ID AS CVLCPT_DOC_ID /* 민원문서아이디 */
FROM TBCSPFPL006M D6 WHERE D1.QSTNAI_YR=D6.QSTNAI_YR and D1.QSTNAI_NO=D6.QSTNAI_NO AND D6.NTAS_RQ_MTRL_DOC_KND_CD = 'A3') AS QSTNAIETC_DOC_ID FROM TBCSPFPL001M D1 where 1=1 D1.QSTNAI_YR = #strYear# D1.QSTNAI_NO = #strQstnai# D1.QSTNAI_TIT_NM LIKE '%'||#strQstnaiTitleNm#||'%' D1.SRECS_SRNG_MBCMT_NM LIKE '%'||#strCmtrNm#||'%' D1.RQT_YMD > #strRqDtFrom# D1.RQT_YMD &lt; #strRqDtTo# D1.CMT_CD = #strCmtCd# D1.NTAS_RQ_MTRL_DVSN_CD = #strMtrlDvsnCd# D1.RQ_SHP_CD = #strRqShpCd# D1.WRTANS_PRSR_NM LIKE '%'||#strWrtansPrsrNm#||'%' ORDER BY D1.QSTNAI_NO ASC