SELECT D1.EVL_YR,
    D1.HLYR_DVSN_CD,
    D2.USER_NM /* 사용자명 */,
    D1.NEVLT_CNB /* 피평가자 전산번호 */,
    F_DPT_INFO(D2.SRECS_DEPT_CD, '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    F_CODE_NM('1000173', D2.JBGD_SECD) AS JBGD_NM /* 직급명 */,
    D1.EVL_CMTR_CNB,
    (SELECT AS USER_NM /* 사용자명 */
FROM TBDCMACM001M WHERE TRGT_NOP_ENO = D1.EVL_CMTR_CNB) AS EVL_CMTR_NM FROM ( SELECT EVL_YR,
    HLYR_DVSN_CD,
    NEVLT_CNB /* 피평가자 전산번호 */,
    EVL_CMTR_CNB
FROM TBCSPAOE032M WHERE EVL_YR = #strEvlYr# AND HLYR_DVSN_CD = #strHlyrDvsnCd# AND EVL_CMTR_DVSN_CD = #strEvlGrpCd# AND NVL(EVL_DTM_YN, 'N') = 'Y' /*평가확정된 사람만 나오면 된다*/ GROUP BY EVL_YR, HLYR_DVSN_CD, NEVLT_CNB, EVL_CMTR_CNB ) D1 , TBDCMACM001M D2 WHERE D1.NEVLT_CNB = D2.TRGT_NOP_ENO ORDER BY D2.USER_NM