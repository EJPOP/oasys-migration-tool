    SALL_CAT_SNO /* 소분류순번 */,
    EVL_NO /* 평가번호 */,
    RSCH_ASMT_TTL AS RSCH_ASMT_TTL /* 연구과제제목 */,
    REPLACE(LPAD(' ', 5*(LEVEL-1)), ' ', '&nbsp') || DECODE(LEVEL, 1, '', '└RE: ') || RSCH_ASMT_TTL AS REPLACE /* 제목명 */,
    PLCY_EVL_TXT /* 정책평가내용 */,
    OTSD_EXPRT_RGTR_ENO /* 외부전문가등록자직원전산번호 */,
    F_USR_INFO(OTSD_EXPRT_RGTR_ENO, '2') AS RGTR_NM /* 등록자명 */,
    TO_CHAR(FRST_REG_DT, 'YYYY.MM.DD') AS FRST_REG_DT /* 최초등록일시 */,
    NVL(SRCH_YN, 'N') AS SRCH_YN /* 조회여부 */,
    HIRK_EVL_NO /* 상위평가번호 */,
    '원래의 글--------------------------------------------' || CHR(10) || RSCH_ASMT_TTL AS CHR,
    (SELECT COUNT(*)
FROM TBBADCMR006L A WHERE A.INFO_YR = BOARD.INFO_YR AND A.SALL_CAT_SNO = BOARD.SALL_CAT_SNO AND A.HIRK_EVL_NO = BOARD.EVL_NO) AS DEL_CNT /*뎃글 갯수*/ FROM ( SELECT INFO_YR /* 정보년도 */,
    SALL_CAT_SNO,
    EVL_NO,
    RSCH_ASMT_TTL /* 연구과제제목 */,
    PLCY_EVL_TXT,
    OTSD_EXPRT_RGTR_ENO /* 외부전문가등록자직원전산번호 */,
    HIRK_EVL_NO,
    FRST_REG_DT /* 최초등록일시 */,
    SRCH_YN
FROM TBBADCMR006L WHERE INFO_YR = #INFO_YR# AND SALL_CAT_SNO = #strSallCatSno# AND SUBSTR(PLCY_REGI_DH, 1, 4) = #INFO_YR# ) BOARD START WITH HIRK_EVL_NO IS NULL CONNECT BY PRIOR EVL_NO = HIRK_EVL_NO