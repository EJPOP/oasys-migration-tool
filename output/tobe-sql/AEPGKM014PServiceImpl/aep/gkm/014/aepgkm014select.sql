SELECT A.CVLCPT_ADT_YR AS CVLCPT_ADT_YR /* 민원감사년도 */,
    A.ADT_NO AS ADT_NO /* 감사번호 */,
    A.CVLCPT_ADT_YR||'-'|| A.ADT_NO AS ADT_CLSF_NO /* 감사분류번호 */,
    TO_CHAR(TO_DATE( A.ADT_BGNG_YMD, 'YYYYMMDD'), 'YYYY.MM.DD')||'~'||TO_CHAR(TO_DATE( A.ADT_END_YMD, 'YYYYMMDD'), 'YYYY.MM.DD') AS TO_CHAR,
    A.ADT_MTTR_NM AS ADT_MTTR_NM /* 감사사항명 */,
    A.INST_CD AS INST_CD /* 기관코드 */,
    A.ADT_BGNG_YMD AS ADT_BGNG_YMD /* 감사시작일자 */,
    A.ADT_END_YMD AS ADT_END_YMD /* 감사종료일자 */,
    A.ADT_KDCD AS ADT_KDCD /* 감사종류코드 */,
    F_CODE_NM('1000007', A.ADT_KDCD) AS F_CODE_NM,
    A.SPVR_DEPT_CD AS SPVR_DEPT_CD /* 감리원부서코드 */,
    A.CVLCPT_ADT_YR || '-' || A.ADT_NO || '-' || B.DSPS_RQT_SQNO AS CVLCPT_ADT_YR /* 민원감사년도 */,
    B.DSPS_RQT_SQNO AS DSPS_RQT_SQNO /* 처분요구순번 */,
    B.DSPS_RQT_KDCD AS DSPS_RQT_KDCD /* 처분요구종류코드 */,
    F_CODE_NM('1000002',B.DSPS_RQT_KDCD) AS DSPS_RQT_KND_CVN /* 처분요구종류코드값명 */,
    B.DSPS_RQT_NM AS DSPS_RQT_NM /* 처분요구명 */,
    F_DPT_INFO(A.SPVR_DEPT_CD,'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */
FROM ( SELECT C.ADT_YR AS ADT_YR /* 감사년도 */,
    C.ADT_NO AS ADT_NO /* 감사번호 */,
    MAX(C.ADT_MTTR_NM) AS ADT_MTTR_NM /* 감사사항명 */,
    MIN(A.INST_CD) AS INST_CD /* 기관코드 */,
    MAX(A.FT_SQNO) AS FT_SQNO /* 출장조순번 */,
    MIN(B.ADT_BGNG_YMD) AS ADT_BGNG_YMD /* 감사시작일자 */,
    MAX(B.ADT_END_YMD) AS ADT_END_YMD /* 감사종료일자 */,
    MAX(B.ADT_DCN) AS ADT_DCN /* 감사일수 */,
    MAX(C.INST_SECD) AS INST_SECD /* 기관구분코드 */,
    MAX(C.ADT_KDCD) AS ADT_KDCD /* 감사종류코드 */,
    MAX(C.SPVR_DEPT_CD) AS SPVR_DEPT_CD /* 감리원부서코드 */
FROM TBBADDED008L A ,TBBADDED002M B ,TBBADDED001M C WHERE 1=1 AND A.BZTRP_YR = B.BZTRP_YR AND A.BZTRP_SQNO = B.BZTRP_SQNO AND B.BZTRP_SECD = '1' AND C.ADT_YR = B.REL_ADT_YR AND C.ADT_NO = B.REL_ADT_NO A.INST_CD = #INST_CD# C.ADT_YR = #CVLCPT_ADT_YR# C.ADT_KDCD = #ADT_KDCD# C.ADT_MTTR_NM LIKE '%' || #AUD_SBJ_NM# || '%' C.ADT_YR||C.ADT_NO = #ADT_NO# GROUP BY C.ADT_YR ,C.ADT_NO ) AS A ,TB_BADEAR001M B ,TB_BADEAR002D C ,TB_BADEAR004M D WHERE A.CVLCPT_ADT_YR = B.ADT_YR AND A.ADT_NO = B.ADT_NO AND B.ADT_YR = C.ADT_YR AND B.ADT_NO = C.ADT_NO AND B.DSPS_RQT_SQNO = C.DSPS_RQT_SQNO B.ADT_YR = D.ADT_YR B.ADT_NO = D.ADT_NO B.DSPS_RQT_SQNO = D.DSPS_RQT_SQNO D.EXTT_NM LIKE '%' || #ETR_PEN_NAM# || '%' B.DSPS_RQT_NM LIKE '%' || #TIT_TXT# || '%' AND C.CFMTN_STTS_SECD !='0' ORDER BY A.CVLCPT_ADT_YR DESC ,A.ADT_NO