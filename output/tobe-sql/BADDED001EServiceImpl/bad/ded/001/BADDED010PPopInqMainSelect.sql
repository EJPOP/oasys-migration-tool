    CASE WHEN D1.CVLCPT_ADT_YR >= '2016' THEN D1.ADT_KDCD ELSE '' END AS CVLCPT_ADT_YR /* 민원감사년도 */,
    CASE WHEN D1.CVLCPT_ADT_YR >= '2016' THEN F_CODE_NM('1000739',D1.ADT_KDCD) ELSE '-' END AS CVLCPT_ADT_YR /* 민원감사년도 */,
