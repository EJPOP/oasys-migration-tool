FROM TABLE(F_AUTH_DEPT(#strCnb#, '', 'Y', #SRECS_DEPT_CD#))) /*직급이 원장일 경우 'ALL' 그외는 'DEPTLW'로 셋팅*/ /* AND D1.AUD_YR */ AND SUBSTR(D1.ADT_KDCD,3,1) = #strAudYrNoKndCd# AND D1.ADT_GRNT_NO = #ADT_GRNT_NO# AND D1.ADT_MTTR_NM LIKE '%'||#strAudSbjNm#||'%' AND D1.ADT_KDCD = #ADT_KDCD# AND D1.MNG_DEPT_CD = #MNG_DEPT_CD# /*감사통제코드가 1:-(해당없음), 2:Y(통제)*/ AND D1.ADT_CNTRL_SECD IN ('1','2') /*감사진행코드 1:진행중, 2: 종료*/ AND D3.ENFC_ATRZ_YMD IS NULL /*감사진행코드 1:진행중, 2: 종료*/ AND D3.ENFC_ATRZ_YMD IS NOT NULL ) T1 ) ORDER BY INPUT_DT DESC, CVLCPT_ADT_YR DESC,DECODE(SUBSTR(ADT_GRNT_NO,1,1),'9',2,1),ADT_GRNT_NO DESC