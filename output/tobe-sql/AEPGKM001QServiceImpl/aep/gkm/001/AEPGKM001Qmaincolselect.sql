SELECT '' AS COL_ID,
    CASE WHEN SET_DVSN_CD = 'B' AND EVL_CHGR_DVSN_CD = '1' THEN '댓글' WHEN SET_DVSN_CD = 'B' AND EVL_CHGR_DVSN_CD = '2' THEN '질문' WHEN SET_DVSN_CD = 'B' AND EVL_CHGR_DVSN_CD = '3' THEN '답변' ELSE KLD_ACTV_NM END AS SET_DVSN_CD,
    SET_DVSN_CD,
    A.EVL_YR,
    A.EVL_DVSN_CD,
    C.SC_GRN_TG_CD,
    EVL_CHGR_DVSN_CD
FROM TBAEPGKM011M AS A, TBAEPGKM012L AS B, TBAEPGKM006M AS C WHERE 1=1 AND A.EVL_YR = B.EVL_YR AND A.EVL_DVSN_CD = B.EVL_DVSN_CD AND B.SC_GRN_TG_CD = C.SC_GRN_TG_CD AND A.EVL_YR = #EVL_YR# AND A.EVL_DVSN_CD = #EVL_DVSN_CD# ORDER BY SET_DVSN_CD DESC, CASE EVL_CHGR_DVSN_CD WHEN '1' THEN 3 WHEN '2' THEN 1 WHEN '3' THEN 2 END