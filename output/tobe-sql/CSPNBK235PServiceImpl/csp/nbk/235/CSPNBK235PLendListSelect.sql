SELECT /* CSPNBK235PLendListSelect */ D1.LEND_KND_CD,
    D1.MOUI_NO,
    D1.LEND_DEAL_DT,
    D1.LEND_DEAL_DT_SLN,
    TO_CHAR(D1.PET_DT, 'YYYYMMDDHH24MISS') AS PET_DT /* 조작일시 */,
    DECODE( D1.DEPTS_PMT_DVSN_CD, 'D', D1.DEAL_PRN, 0) AS DEPTS_PMT_DVSN_CD,
    DECODE( D1.DEPTS_PMT_DVSN_CD, 'C', D1.DEAL_PRN, 0) AS DEPTS_PMT_DVSN_CD,
    D1.INT_AM,
    D1.LEND_BL,
    D1.LAG_WDL_RMK
FROM TBCSPNBK205L D1 WHERE D1.LEND_KND_CD = #lendKndCd# AND D1.MOUI_NO = #mouiNo# AND D1.CNL_YN = 'N' AND D1.LEND_DEAL_DT BETWEEN #lendDealBeginDt# AND #lendDealEndDt# ORDER BY D1.PET_DT DESC