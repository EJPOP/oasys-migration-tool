SELECT /* CSPNBK235PInvListSelect */ D1.MOUI_NO,
    D1.INV_DEAL_DT,
    D1.INV_DEAL_SLN,
    TO_CHAR(D1.PET_DT, 'YYYYMMDDHH24MISS') AS PET_DT /* 조작일시 */,
    DECODE( D1.DEPTS_PMT_DVSN_CD, 'C', D1.INV_AM, 0) AS DEPTS_PMT_DVSN_CD,
    DECODE( D1.DEPTS_PMT_DVSN_CD, 'D', D1.INV_AM, 0) AS DEPTS_PMT_DVSN_CD,
    D1.DEAL_AF_LEDG_BL,
    D1.RMRK_CN /* 비고내용 */
FROM TBCSPNBK102L D1 WHERE D1.INV_DEAL_DT BETWEEN #invDealBeginDt# AND #invDealEndDt# AND D1.MOUI_NO = #mouiNo# AND D1.CNL_YN = 'N' ORDER BY D1.PET_DT DESC