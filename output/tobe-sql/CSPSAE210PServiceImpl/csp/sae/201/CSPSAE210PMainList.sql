    A.SV_TIT_CD,
    B.SV_QEST_NO,
    B.SUB_QEST_NO,
    B.QEST_DVSN_CD,
    B.SV_QEST_TXT,
    B.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    DECODE(SUB_QEST_NO, NULL, 'N', 'Y') AS SUB_QEST_NO
FROM TBCSPSAE121C A , TBCSPSAE122C B WHERE B.SV_TIT_CD = A.SV_TIT_CD AND A.SV_TIT_CD = #strSV_TIT_CD# AND B.SV_QEST_NO = #strSV_QEST_NO#