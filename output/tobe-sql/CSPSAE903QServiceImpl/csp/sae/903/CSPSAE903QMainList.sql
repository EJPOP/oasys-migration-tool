/*cspsae903 CSPSAE903QMainList*/ SELECT ROWNUM,
    T.*
FROM ( SELECT A.GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    A.RSCH_ASMT_TTL /* 연구과제제목 */,
    A.INST_CD /* 기관코드 */,
    F_ORG_INFO(A.INST_CD,'1') AS PROF_RBPRSN_INST_NM /* 증명책임자기관명 */,
    A.CRS_NM /* 과정명 */,
    A.USR_CNB,
    F_USR_INFO(A.USR_CNB, '2') AS USER_NM /* 사용자명 */,
    DECODE(A.EDU_RCMD_YN,'Y','추천','N','비추천') AS EDU_RCMD_YN,
    A.FRST_REG_DT /* 최초등록일시 */,
    A.ENAIS_PST_INQ_NMTM /* ENAIS게시물조회횟수 */
FROM TBCSPSAE903M A WHERE 1=1 AND SUBSTR(A.FRST_REG_DT,1,4) = #strYR_CD# AND A.EDU_RCMD_YN = #strEDU_RCMD_YN# AND A.INST_CD = #INST_CD# ORDER BY TO_NUMBER(A.GRNDS_EXMN_AEX_SQNO) ) T ORDER BY NUM DESC