SELECT *
FROM( SELECT ROW_NUMBER() OVER(ORDER BY A.FRST_REG_DT) AS GRNDS_EXMN_AEX_SQNO /* 현장조사수용비순번 */,
    A.APL_NO,
    F_USR_INFO(A.APL_USR, '2') AS F_USR_INFO,
    CASE WHEN A.SMT_YN = 'Y' THEN TO_CHAR(A.SBMSN_YMD,'YYYYMMDD') ELSE '' END AS SBMSN_YMD /* 제출일자 */,
    F_CODE_NM('1001000',NVL(PRGRS_STTS_SECD,'10')) AS F_CODE_NM,
    A.SLCT_RPD_NM /* 청구명 */,
    A.SMT_YN,
    CASE WHEN A.APL_USR = #S_CNB# THEN 'Y' ELSE 'N' END AS APL_USR
FROM TBCSPSAH001M A WHERE 1=1 AND ((F_USR_INFO(#S_CNB#,'5') = '122600' AND A.SMT_YN='Y') OR A.APL_USR = #S_CNB#) AND REPLACE(A.CLM_NM,' ','') LIKE REPLACE('%$strSCH_TITLE$%',' ','') AND ((TO_CHAR(A.SBMSN_YMD,'YYYYMMDD') BETWEEN #strACP_DH_START# AND #strACP_DH_END#) OR SBMSN_YMD IS NULL) AND A.PRGRS_STTS_SECD = #PRGRS_STTS_SECD# ORDER BY GRNDS_EXMN_AEX_SQNO ) WHERE 1=1 ORDER BY GRNDS_EXMN_AEX_SQNO DESC