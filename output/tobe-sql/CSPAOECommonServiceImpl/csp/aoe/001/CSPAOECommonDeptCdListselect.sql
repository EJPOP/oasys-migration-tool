SELECT /* CSPAOECommonDeptCdListselect */ D1.SRECS_DEPT_CD AS DEPT_CD /* 부서코드 */,
    F_DPT_INFO(D1.SRECS_DEPT_CD, '1') AS DEPT_NM /* 부서명 */,
    NVL(D2.HIRK_DPT_CD, D1.SRECS_DEPT_CD) AS HIRK_DPT_CD
FROM ( SELECT SRECS_DEPT_CD /* 심사재심의부서코드 */
FROM TBDCMACM002M WHERE DEPT_TPCD = 'S' /*지원부서만 가져온다.*/ AND USE_YN = 'Y' GROUP BY SRECS_DEPT_CD UNION SELECT CHR_DPT_CD
FROM TBCSPAOE026M GROUP BY CHR_DPT_CD /*부서테이블에서 지원부서이면서 사용여부가 와이인 데이터만 가져오게 되면 이미 등록된 부서이지만 폐지됬을 경우를 대비해서 26테이블로 유니온 한다*/ ) D1 , TBDCMACM002M D2 WHERE D1.SRECS_DEPT_CD = D2.SRECS_DEPT_CD ORDER BY D1.SRECS_DEPT_CD