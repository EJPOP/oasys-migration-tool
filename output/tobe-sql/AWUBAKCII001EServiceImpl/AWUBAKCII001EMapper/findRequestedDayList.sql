/* AWUBAKCII001EMapper.findRequestedDayList 견학신청된 날짜 조회 */ SELECT VI_ID,
    VI_NM,
    HMP_VI_ID,
    VI_EML,
    TO_CHAR(TO_DATE(VI_DY, 'YYYYMMDD'), 'YYYY-MM-DD') AS "start",
    TO_CHAR(TO_DATE(VI_DY, 'YYYYMMDD'), 'YYYY-MM-DD') AS "end",
    VI_TM,
    VI_HPN,
    VI_GRP,
    VI_PWD,
    VI_PEN_CNT,
    ETC_MTTR_CN /* 기타사항내용 */,
    CNL_DH,
    HLDY_YN,
    VI_APL_CMPT_YN,
    CASE WHEN VI_TM = '1' THEN '#D21010' ELSE '#002BC6' END AS VI_TM,
    CASE WHEN VI_TM = '1' THEN '#D21010' ELSE '#002BC6' END AS VI_TM,
    CASE WHEN VI_TM = '1' THEN '오전:' || VI_PEN_CNT || '명' || DECODE(VI_APL_CMPT_YN,'C',' (취소)', NULL) ELSE '오후:' || VI_PEN_CNT || '명'||DECODE(VI_APL_CMPT_YN,'C',' (취소)', NULL) END AS VI_TM
FROM TBAWUBAKCII001M WHERE 1=1 AND VI_DY BETWEEN #{searchStart} AND #{searchEnd} AND HLDY_YN IS NULL ORDER BY VI_TM ASC