SELECT TO_CHAR(TO_DATE(A.VI_DT), 'YYYY-MM-DD') || ' ' || TO_CHAR(TO_DATE(A.VI_HM, 'HH24:MI'), 'HH24:MI') AS TO_CHAR,
    A.VI_SLN,
    A.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    A.BLT_BRDV_NM,
    A.INFO_RLPR_NM /* 정보관계자명 */,
    F_CODE_NM('1000173', A.JBGD_SECD) AS JBGD_NM /* 직급명 */,
    A.SLCT_OOFC_NM /* 청탁근무처명 */,
    A.VI_PEN_NAM,
    A.TPOF_NOPE /* 제보인원수 */,
    A.CAR_NO,
    F_CODE_NM('1000284',A.VI_PPS_CD) AS F_CODE_NM,
    A.VI_PPS_TXT,
    F_CODE_NM('1000285',A.VI_WAY_CD) AS F_CODE_NM,
    A.VI_WAY_TXT,
    A.CRU_TELNO /* 부패전화번호 */,
    A.IO_NO,
    F_CODE_NM('1000001', A.VI_PEN_RGN_CD) AS F_CODE_NM,
    TO_CHAR(TO_DATE(A.REN_DT), 'YYYY-MM-DD') || ' ' || TO_CHAR(TO_DATE((A.REN_HH||A.REN_MM), 'HH24:MI'), 'HH24:MI') AS TO_CHAR
FROM TBCSPOEP001M A WHERE 1=1 AND A.VI_DT &gt;= #fromYmd# AND A.VI_DT &lt;= #toYmd# AND A.OGDP_DEPT_CD = #OGDP_DEPT_CD# AND A.JBGD_SECD = #JBGD_SECD# AND A.TRGT_NOP_ENO = #TRGT_NOP_ENO# AND A.SLCT_OOFC_NM LIKE '%'||#workPlace#||'%' AND A.CAR_NO LIKE '%'||#carNo#||'%' AND A.VI_PEN_NAM LIKE '%'||#name#||'%' ORDER BY A.VI_DT DESC, A.VI_HM DESC