SELECT T1.EXPRT_SRNO,
    T2.ACTPLN_SRNO,
    T1.EXPRT_DVSN_CD,
    F_EBSC_CODE_NM('10000024', T1.EXPRT_DVSN_CD) AS F_EBSC_CODE_NM,
    T1.EXPRT_SPH_CD,
    F_EBSC_CODE_NM('10000025', T1.EXPRT_SPH_CD) AS F_EBSC_CODE_NM,
    T1.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    F_USR_INFO(T1.TRGT_NOP_ENO,'2') AS USER_NM /* 사용자명 */,
    TO_CHAR(TO_DATE(T1.SELECTN_DT),'YYYY-MM-DD') AS TO_CHAR,
    T2.SRECS_DEPT_CD /* 심사재심의부서코드 */,
    T2.JBGD_SECD /* 직급구분코드 */,
    F_CODE_NM('1000173',T2.JBGD_SECD) AS JBGD_NM /* 직급명 */,
    F_DPT_INFO(T2.SRECS_DEPT_CD,'1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */
FROM TBHRMCEX001M T1 ,TBHRMCEX002M T2 WHERE T1.EXPRT_SRNO = T2.EXPRT_SRNO AND T1.EXPRT_SRNO = #EXPRT_SRNO# AND T2.ACTPLN_SRNO = #ACTPLN_SRNO# AND T2.ATRZ_DOC_NO = #ATRZ_DOC_NO#