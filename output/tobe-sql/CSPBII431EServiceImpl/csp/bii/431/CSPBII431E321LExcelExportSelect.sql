SELECT A.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    A.TRN_STR_DT,
    A.TRN_END_DT,
    A.TRN_DVSN_CD,
    A.TRN_CRS_NM,
    A.TRN_ORG_NM,
    DGUARD.DECRYPT('TB_ENC','EIN',A.CRU_RRNO_SCRTY) AS DECRYPT
FROM TBCSPBII321L A, TBCSPBII300T B WHERE A.CRU_RRNO_SCRTY = B.CRU_RRNO_SCRTY