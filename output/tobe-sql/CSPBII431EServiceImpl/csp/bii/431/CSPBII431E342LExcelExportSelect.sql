SELECT A.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    A.WRK_STR_DT,
    A.WRK_END_DT,
    A.SLCT_OOFC_NM /* 청탁근무처명 */,
    A.JBPS_NM /* 직위명 */,
    A.CHR_OW_NM,
    DGUARD.DECRYPT('TB_ENC','EIN',A.CRU_RRNO_SCRTY) AS DECRYPT
FROM TBCSPBII342L A, TBCSPBII300T B WHERE A.CRU_RRNO_SCRTY = B.CRU_RRNO_SCRTY