SELECT A.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    A.ISPCT_STR_DT,
    A.ISPCT_END_DT,
    A.TNG_ISPCT_ORG_NM,
    A.ISPCT_STUD_SPH_NM,
    A.NAT_NM,
    A.ENF_ORG_NM,
    DGUARD.DECRYPT('TB_ENC','EIN',A.CRU_RRNO_SCRTY) AS DECRYPT
FROM TBCSPBII322L A, TBCSPBII300T B WHERE A.CRU_RRNO_SCRTY = B.CRU_RRNO_SCRTY