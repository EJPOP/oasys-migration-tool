SELECT A.CRU_RRNO_SCRTY /* 부패주민등록번호보안 */,
    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    A.OFOD_DT,
    A.APM_DVSN_NM,
    A.JBPS_NM /* 직위명 */,
    A.JBGD_NM /* 직급명 */,
    A.APM_DPT_NM,
    A.OFOD_OFI_NM,
    DGUARD.DECRYPT('TB_ENC','EIN',A.CRU_RRNO_SCRTY) AS DECRYPT
FROM TBCSPBII341L A, TBCSPBII300T B WHERE A.CRU_RRNO_SCRTY = B.CRU_RRNO_SCRTY