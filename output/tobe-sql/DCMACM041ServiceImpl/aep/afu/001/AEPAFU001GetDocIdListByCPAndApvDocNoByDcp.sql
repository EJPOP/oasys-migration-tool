SELECT A.SLCT_SORT_SQNO /* 청탁정렬순번 */,
    NVL(C.SLCT_STP_SE_CDNM,'') AS ADT_STP_NM /* 감사단계명 */,
    B.SLCT_RPTP_NM /* 청탁보고서명 */,
    A.CVLCPT_ATRZ_DMND_ID /* 민원결재요청아이디 */,
    A.CVLCPT_RCPT_YR AS CVLCPT_RCPT_YR /* 민원접수년도 */,
    A.CVLCPT_RCPT_RCPT AS CVLCPT_RCPT_RCPT /* 민원접수번호 */,
    A.CVLCPT_STP_SECD AS CVLCPT_STP_SECD /* 민원단계구분코드 */,
    A.CVLCPT_DOC_ID /* 민원문서아이디 */,
    A.SLCT_ATRZ_SECD /* 청탁결재구분코드 */,
    A.SLCT_ATRZ_DOC_SECD /* 청탁결재문서구분코드 */,
    A.CVLCPT_CLM_SECD /* 민원청구구분코드 */
FROM TB_DCPDCP609M A INNER JOIN TB_DCPDCP603M B ON A.CVLCPT_RCPT_YR = B.CVLCPT_RCPT_YR AND A.CVLCPT_CLM_SECD = B.CVLCPT_CLM_SECD AND A.CVLCPT_RCPT_RCPT = B.CVLCPT_RCPT_RCPT AND A.CVLCPT_STP_SECD = B.CVLCPT_STP_SECD AND A.CVLCPT_DOC_ID = B.CVLCPT_DOC_ID LEFT OUTER JOIN TB_DCPDCP601M C ON A.CVLCPT_RCPT_YR = C.CVLCPT_RCPT_YR AND A.CVLCPT_CLM_SECD = C.CVLCPT_CLM_SECD AND A.CVLCPT_RCPT_RCPT = C.CVLCPT_RCPT_RCPT AND A.CVLCPT_STP_SECD = C.CVLCPT_STP_SECD WHERE A.CVLCPT_ATRZ_DMND_ID = #ATRZ_DMND_ID#