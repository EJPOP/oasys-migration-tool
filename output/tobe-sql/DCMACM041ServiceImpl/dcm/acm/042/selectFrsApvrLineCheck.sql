SELECT B.TRGT_NOP_ENO /* 대상인원직원전산번호 */,
    B.USER_NM /* 사용자명 */,
    B.USER_ID /* 사용자아이디 */,
    B.INSD_JBGD_SECD /* 내부직급구분코드 */,
    F_DPT_INFO(B.SRECS_DEPT_CD, '1') AS CNSTN_MBCMT_DEPT_NM /* 자문위원부서명 */,
    B.ONR_PST_NM
FROM ( SELECT SUBSTR(REGEXP_REPLACE(USR_DFN_TXT_2, '[^[:digit:]]'), (LEVEL - 1) * 4 + 1, 4) AS TRGT_NOP_ENO /* 대상인원직원전산번호 */
FROM ( SELECT USR_DFN_TXT_2
FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1001017' AND CD = '000000000' AND 1 AND A.CVLCPT_DOC_ID IN #strDocIdList[]# AND A.SLCT_RPTP_KDCD = #SLCT_RPTP_KDCD# ) ) CONNECT BY SUBSTR(REGEXP_REPLACE(USR_DFN_TXT_2, '[^[:digit:]]'), (LEVEL - 1) * 4 + 1, 4) > 0 ) A INNER JOIN TBDCMACM001M B ON (A.TRGT_NOP_ENO = B.TRGT_NOP_ENO) ORDER BY B.INSD_JBGD_SECD, B.JBGD_SECD ASC