SELECT A.CVLCPT_CLM_SECD /* 민원청구구분코드 */,
    A.TPOF_RCPT_YR /* 제보접수년도 */,
    A.TPOF_RCPT_NO /* 제보접수번호 */,
    A.TPOF_CVLPR_MPNO /* 제보민원인휴대전화번호 */,
    C.CLM_RCPT_NO /* 청구접수번호 */,
    C.SNDNG_YMD /* 발송일자 */,
    C.SMS_CN /* SMS내용 */,
    C.SNDNG_TELNO /* 발송전화번호 */,
    C.RCPTN_TELNO /* 수신전화번호 */
FROM TB_CSPDCP101M A ,TB_CSPEAR001M B ,TB_CSPEAR008H C WHERE 1=1 AND A.CVLCPT_CLM_SECD = #CLM_SECD# AND A.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND A.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT# AND A.CVLCPT_CLM_SECD = B.CLM_SECD AND A.TPOF_RCPT_YR = B.CLM_YR AND A.TPOF_RCPT_NO = B.CLM_RCPT_NO AND A.CVLCPT_CLM_SECD = C.CLM_SECD AND A.TPOF_RCPT_YR = C.CLM_YR AND A.TPOF_RCPT_NO = C.CLM_RCPT_NO ORDER BY C.CLM_RCPT_NO DESC