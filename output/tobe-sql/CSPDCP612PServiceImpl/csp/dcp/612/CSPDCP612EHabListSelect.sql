SELECT D1.TPOF_KDCD /* 제보종류코드 */,
    F_CODE_NM('1000047', D1.TPOF_KDCD) AS F_CODE_NM,
    TO_CHAR(D1.TPOF_RCPT_DT, 'YYYY-MM-DD HH:MM') AS CRU_RCPT_DT /* 부패접수일시 */,
    D1.TPOF_RCPT_YR||'-민원-'||D1.TPOF_ACTL_RCPT_NO AS TPOF_RCPT_YR /* 제보접수년도 */,
    D1.TPOF_NM /* 제보명 */,
    D1.TPOF_CVLPR_NM /* 제보민원인명 */
FROM TB_CSPDCP101M D1 ,TB_CSPDCP105L D2 WHERE 1=1 AND D1.CVLCPT_CLM_SECD = D2.TPOF_MCA_CLM_SECD AND D1.TPOF_RCPT_YR = D2.TPOF_MCA_RCPT_YR AND D1.TPOF_RCPT_NO = D2.TPOF_MCA_CVLCPT_RCPT_NO AND D2.CVLCPT_CLM_SECD = #CLM_SECD# AND D2.TPOF_RCPT_YR = #CVLCPT_RCPT_YR# AND D2.TPOF_RCPT_NO = #CVLCPT_RCPT_RCPT#