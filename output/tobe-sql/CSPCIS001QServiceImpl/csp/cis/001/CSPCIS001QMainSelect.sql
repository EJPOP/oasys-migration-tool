SELECT A.INSP_REGI_DT,
    A.INSP_REGI_NO,
    A.INSP_LVL_CNT,
    A.INSP_DVSN_CD,
    A.INSP_TIT_ENN,
    A.INSP_ECN,
    A.INSP_USR_EID,
    A.INSP_USR_NAM,
    A.INSP_ATT_FL_EID,
    A.INSP_DOC_TYP_CD,
    A.SLCT_STTS_YN /* 청탁상태여부 */
FROM TBCSPCIS001M A WHERE 1=1 AND (A.INSP_ECN = #cnb# OR (A.INSP_REGI_DT , A.INSP_REGI_NO) IN (SELECT A.INSP_REGI_DT,
    A.INSP_REGI_NO
FROM TBCSPCIS001M A WHERE A.INSP_ECN = #cnb#)) AND INSP_DVSN_CD = #dvsn# AND INSP_REGI_DT &gt;= #fromYmd# AND INSP_REGI_DT &lt;= #toYmd# ORDER BY INSP_REGI_DT DESC, INSP_REGI_NO DESC, INSP_LVL_CNT