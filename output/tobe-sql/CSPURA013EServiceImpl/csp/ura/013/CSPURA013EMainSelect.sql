SELECT DISTINCT A.RSCH_YR AS RSCH_YR /* 연구년도 */,
    A.RSCH_ASMT_SQNO /* 연구과제순번 */,
    A.RSCH_ASMT_TTL /* 연구과제제목 */,
    A.RSCH_SE_CN /* 연구구분내용 */,
    A.RSCH_TYPE_TYPE /* 연구유형명 */,
    A.RSCH_PRD_CN /* 연구기간내용 */,
    A.RSCH_MCNT /* 연구개월수 */,
    A.RSCH_RT /* 연구비율 */,
    A.RMRK_CN /* 비고내용 */
FROM TB_CSPURA008M A , TB_CSPURA009D B WHERE A.RSCH_YR = B.RSCH_YR(+) AND A.RSCH_ASMT_SQNO = B.RSCH_ASMT_SQNO(+) AND A.RSCH_YR BETWEEN #fromSTD_YE# AND #toSTD_YE# AND B.TRGT_NOP_ENO IN (SELECT TRGT_NOP_ENO /* 대상인원직원전산번호 */
FROM TBDCMACM001M WHERE SRECS_DEPT_CD = #SRECS_DEPT_CD#) AND B.RSCH_ROLE_ROLE = #RSCH_ROLE_ROLE# AND A.RSCH_TYPE_TYPE LIKE '%' || #strTASK_TYP_NM# || '%' AND B.TRGT_NOP_ENO IN (SELECT TRGT_NOP_ENO /* 대상인원직원전산번호 */
FROM TBDCMACM001M WHERE USER_NM LIKE '%' || #strUSR_NAM# || '%') AND A.RSCH_ASMT_TTL LIKE '%' || #strSTD_TASK_NM# || '%' AND A.RMRK_CN LIKE '%' || #strRMK_SBJ# || '%' ORDER BY A.RSCH_YR, A.RSCH_RT, A.RSCH_SE_CN