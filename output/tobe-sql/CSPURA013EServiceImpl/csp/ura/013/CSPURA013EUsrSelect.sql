FROM TBDCMACM013C WHERE CMM_CD_DVSN_CD = '1000173' AND CD = B.JBGD_SECD) AS CD_NM, B.USER_NM, A.RSCH_ROLE_ROLE FROM TB_CSPURA009D A, TBDCMACM001M B, TBDCMACM002M C WHERE A.RSCH_PIC_PIC = B.TRGT_NOP_ENO AND B.SRECS_DEPT_CD = C.SRECS_DEPT_CD AND A.RSCH_YR = #RSCH_YR# AND A.RSCH_SQNO = #RSCH_ASMT_SQNO# ORDER BY B.USER_NM