    A.FRS_MDM_SQNO /* 포렌식매체순번 */,
    SUM(A.RLPR_SQNO) AS DSPS_RQT_NOPE /* 처분요구인원수 */,
    SUM(A.PNRT_PRSRV_AMT) AS DSPS_RQT_AMT /* 처분요구금액 */
FROM (SELECT A.INFO_YR /* 정보년도 */,
    A.INFO_SQNO /* 정보순번 */,
    B.RLPR_SQNO /* 관계자순번 */,
    B.PNRT_PRSRV_AMT /* 추징회수보전금액 */
FROM TBCSPBII120L A /*감사정보관련처분내역*/ , (SELECT S1.ADT_YR /* 감사년도 */,
