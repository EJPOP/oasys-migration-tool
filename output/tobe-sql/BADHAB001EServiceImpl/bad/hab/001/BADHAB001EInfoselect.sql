    F_DPT_INFO(T1.ETC_LBSVC_MNG_DEPT_CD, '1') AS PRCS_DEPT_NM /* 처리부서명 */,
    ( SELECT TO_CHAR(TO_DATE(X1.TRF_DT),'YYYY-MM-DD') || '(' || F_DPT_INFO (X1.TRF_BEF_CHR_DPT_CD, '1') || '->' || F_DPT_INFO (T1.ETC_LBSVC_MNG_DEPT_CD, '1') || ')' AS ETC_LBSVC_MNG_DEPT_CD /* 기타용역관리부서코드 */
