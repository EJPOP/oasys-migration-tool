    S1.DSPS_RQT_KDCD /* 처분요구종류코드 */,
    S2.PNRT_PRSRV_AMT /* 추징회수보전금액 */,
    COUNT(S3.RLPR_SQNO) AS RLPR_SQNO /* 관계자순번 */
FROM TBBADEAR001M S1 /*처분요구사항*/ , TBBADEAR002D S2 /*처분요구사항상세*/ , TBBADEAR007M S3 /*관계자기본*/ WHERE 1=1 AND S1.CVLCPT_ADT_YR = S2.CVLCPT_ADT_YR AND S1.ADT_NO = S2.ADT_NO AND S1.DSPS_RQT_SQNO = S2.DSPS_RQT_SQNO AND S1.CVLCPT_ADT_YR = S3.CVLCPT_ADT_YR AND S2.ADT_NO = S3.ADT_NO AND S1.DSPS_RQT_SQNO = S3.DSPS_RQT_SQNO GROUP BY S1.CVLCPT_ADT_YR, S1.ADT_NO, S1.DSPS_RQT_SQNO, S1.DSPS_RQT_KDCD, S2.PNRT_PRSRV_AMT ) B WHERE 1=1 AND A.CVLCPT_ADT_YR = B.CVLCPT_ADT_YR AND A.ADT_NO = B.ADT_NO AND A.DSPS_RQT_SQNO = B.DSPS_RQT_SQNO ) A WHERE 1=1 GROUP BY A.INFO_YR, A.FRS_MDM_SQNO ) T2 WHERE 1=1 AND T1.INFO_YR = T2.INFO_YR AND T1.FRS_MDM_SQNO = T2.FRS_MDM_SQNO /* 사무처리부 정보사항화면에서는 이송접수일자와 처리지시가 입력된 건만 조회 */ AND T1.TRNF_RCPT_YMD IS NOT NULL AND T1.PRCS_DRCTN_SECD IS NOT NULL AND T1.ETC_LBSVC_MNG_DEPT_CD IN (SELECT SRECS_DEPT_CD /* 심사재심의부서코드 */
FROM TABLE(F_AUTH_DEPT(#strCnb#, #strDptAuth#, '', #strDptCd#))) AND T1.INFO_YR = #INFO_YR# AND T1.ETC_LBSVC_MNG_DEPT_CD = #ETC_LBSVC_MNG_DEPT_CD# AND T1.FRS_MDM_SQNO = #FRS_MDM_SQNO# AND T1.INFO_YR = #INFO_YR# AND T1.FRS_MDM_SQNO = #FRS_MDM_SQNO# ORDER BY T1.TRNF_RCPT_YMD DESC, T1.FRS_MDM_SQNO DESC