    DECODE(T1.PRCS_DRCTN_SECD,'4', NVL(T1.DSPS_RQT_AMT,0), NVL(T2.DSPS_RQT_AMT,0)) AS DSPS_RQT_AMT /* 처분요구금액 */,
    T1.CMPTN_YMD /* 완료일자 */,
    T1.PRCS_PIC_ENO /* 처리담당자직원전산번호 */,
    F_USR_INFO(T1.PRCS_PIC_ENO, '2') AS PRCS_PIC_NM /* 처리담당자명 */,
    T1.ADT_RMRK_CN /* 감사비고내용 */,
    T1.ADT_DATA_YR AS ADT_DATA_YR /* 감사자료년도 */,
    T1.ADT_DATA_DEPT_CD AS ADT_DATA_DEPT_CD /* 감사자료부서코드 */,
    T1.ADT_DATA_SQNO AS ADT_DATA_SQNO /* 감사자료순번 */,
